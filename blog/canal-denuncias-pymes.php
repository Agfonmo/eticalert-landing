        <h2 id="errores-pymes">Los 4 errores más comunes de las pymes al implantar el canal</h2>

        <ol>
          <li>
            <p><strong>Usar un buzón de correo.</strong> Es la solución más frecuente y la menos válida. Un buzón de correo no garantiza anonimato, no gestiona plazos ni genera libro-registro. La AIPI puede sancionarlo aunque técnicamente "exista" un canal.</p>
          </li>
          <li>
            <p><strong>No nombrar al RSII.</strong> El canal existe, pero nadie está formalmente designado como responsable. La ley exige que el RSII esté identificado y que el informante sepa a quién se dirige su denuncia.</p>
          </li>
          <li>
            <p><strong>No informar a los empleados.</strong> El canal existe pero nadie lo conoce. La Ley 2/2023 exige que los empleados sean informados de la existencia del canal, cómo acceder a él y qué protecciones tienen como informantes.</p>
          </li>
          <li>
            <p><strong>No gestionar los plazos.</strong> El acuse de recibo en 7 días y la resolución en 3 meses son obligaciones procedimentales. Incumplirlas genera infracción aunque la denuncia de fondo se haya tramitado correctamente.</p>
          </li>
        </ol>

