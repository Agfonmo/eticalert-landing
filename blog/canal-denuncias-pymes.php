        <h2 id="que-debe-incluir">Qué debe incluir un canal de denuncias válido para una pyme</h2>

        <p>La Ley 2/2023 no distingue entre grandes empresas y pymes en cuanto a los requisitos técnicos del canal. Los mismos estándares aplican a una empresa de 50 empleados y a una de 5.000. Un canal homologado debe incluir obligatoriamente:</p>

        <ul>
          <li><strong>Anonimato real.</strong> El informante puede enviar la denuncia sin revelar su identidad. El sistema no registra su IP ni sus datos personales.</li>
          <li><strong>Canal de comunicación bidireccional.</strong> El informante puede responder preguntas del gestor sin identificarse, usando un código de seguimiento.</li>
          <li><strong>Acuse de recibo en 7 días.</strong> El sistema debe confirmar la recepción de la denuncia en un máximo de 7 días hábiles.</li>
          <li><strong>Resolución en 3 meses.</strong> El gestor debe comunicar el resultado de la investigación al informante en un plazo máximo de 3 meses (prorrogable a 6 en casos complejos).</li>
          <li><strong>Libro-registro.</strong> Registro de todas las denuncias recibidas, tramitadas y resueltas, disponible para inspección de la AIPI.</li>
          <li><strong>Cifrado y seguridad de datos.</strong> Los datos de las denuncias deben estar protegidos técnicamente. El almacenamiento fuera de la UE puede generar conflictos con el RGPD.</li>
          <li><strong>Designación de un RSII.</strong> La empresa debe nombrar a un Responsable del Sistema Interno de Información, que puede ser una persona interna o una figura externa externalizada.</li>
        </ul>

        <h2 id="rsii-pymes">El RSII en una pyme: quién puede serlo</h2>

        <p>El RSII (Responsable del Sistema Interno de Información) es la persona encargada de gestionar las denuncias. En una pyme, puede ser:</p>

        <ul>
          <li>El director de RRHH o el responsable de cumplimiento, si existe.</li>
          <li>El gerente o socio de la empresa, en organizaciones pequeñas.</li>
          <li>Un asesor o despacho externo al que se externalice la gestión del canal.</li>
        </ul>

