        <h1>La AIPI ya sanciona: qué significa para tu empresa y cómo actuar</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a marzo 2026 · 6 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-03-15">15 de marzo de 2026</time> por el equipo de EticAlert</p>

        <p>Desde febrero de 2026, la Autoridad Independiente de Protección del Informante (AIPI) está plenamente operativa. El periodo de gracia informal que muchas pymes habían interpretado que existía ha terminado. Si tu empresa tiene obligación de disponer de un canal de denuncias y aún no lo ha implementado, el riesgo de sanción es real y creciente.</p>
        <p>En este artículo explicamos qué es la AIPI, qué puede hacer concretamente, qué ha cambiado en 2026 y qué pasos debe dar tu empresa de inmediato.</p>

        <h2 id="que-es-aipi">¿Qué es la AIPI?</h2>
        <p>La <strong>Autoridad Independiente de Protección del Informante</strong> es una autoridad administrativa independiente creada por la Ley 2/2023. No depende del Gobierno, actúa con plena autonomía funcional y tiene personalidad jurídica propia.</p>
        <p>Sus funciones principales son:</p>
        <ul>
          <li>Supervisar que las entidades obligadas dispongan de un Sistema Interno de Información conforme a la ley.</li>
          <li>Recibir informaciones externas cuando los canales internos de las organizaciones hayan fallado o el informante no confíe en ellos.</li>
          <li>Proteger al informante frente a posibles represalias, pudiendo dictar medidas cautelares.</li>
          <li>Investigar las denuncias que recibe e instruir expedientes sancionadores.</li>
          <li>Sancionar los incumplimientos de la normativa.</li>
        </ul>
        <p>Es importante entender que la AIPI no solo sanciona la ausencia de canal: puede sancionar también un canal que existe pero no cumple los requisitos técnicos o procedimentales exigidos.</p>

        <h2 id="que-ha-cambiado-2026">¿Qué ha cambiado en 2026?</h2>
        <p>Hasta 2025, la AIPI existía como organismo en constitución. Muchas empresas interpretaron que mientras no estuviera operativa no habría consecuencias prácticas. Ese razonamiento era incorrecto (la obligación legal existía desde 2023), pero en la práctica el enforcement era limitado.</p>
        <p>Esto ha cambiado de forma definitiva en 2026:</p>
        <ul>
          <li><strong>10 de febrero de 2026:</strong> la AIPI activa su sede electrónica oficial (sede.proteccioninformante.gob.es) y habilita el formulario para notificar la designación del RSII.</li>
          <li><strong>Inicio del plazo de 2 meses:</strong> todas las empresas con canal ya operativo deben comunicar su RSII antes del 10 de abril de 2026.</li>
          <li><strong>Inicio del enforcement real:</strong> la AIPI puede ahora requerir información a cualquier empresa obligada, verificar la adecuación de su sistema y comprobar si tienen RSII designado correctamente.</li>
          <li><strong>Fin del periodo de gracia:</strong> no existe ninguna disposición legal que ampare esperar más. El incumplimiento es sancionable desde el 1 de diciembre de 2023 para las empresas de 50 a 249 empleados.</li>
        </ul>

        <div class="callout">
          <p><strong>Dato importante:</strong> una empresa que fue requerida por la AIPI sin tener canal activo puede ser sancionada tanto por el incumplimiento original como por no haber respondido correctamente al requerimiento. El efecto acumulador del incumplimiento es real.</p>
        </div>

        <h2 id="que-puede-hacer-aipi">¿Qué puede hacer la AIPI concretamente?</h2>
        <p>Las empresas y sus responsables legales deben conocer el alcance real de las competencias de la AIPI:</p>
        <ul>
          <li><strong>Requerir documentación:</strong> la AIPI puede exigir a cualquier empresa obligada que acredite que dispone de un canal operativo, que tiene procedimiento documentado, y que el RSII está correctamente designado.</li>
          <li><strong>Inspeccionar el sistema:</strong> puede revisar si el canal cumple los requisitos técnicos (confidencialidad, cifrado, anonimato, acuses automáticos, plazos) y procedimentales (gestión de casos, independencia del RSII).</li>
          <li><strong>Verificar la realidad del canal:</strong> no basta con tenerlo "en papel" o con un formulario de contacto genérico. La AIPI comprueba que el canal es real, está operativo y se gestiona conforme a la ley.</li>
          <li><strong>Comprobar el RSII:</strong> verificar que ha sido designado formalmente, que tiene independencia funcional y que su designación ha sido comunicada correctamente.</li>
          <li><strong>Instruir expedientes sancionadores:</strong> iniciar procedimientos que pueden derivar en las sanciones económicas previstas en la ley.</li>
          <li><strong>Dictar medidas cautelares:</strong> en casos de represalias contra informantes, puede actuar con carácter urgente.</li>
        </ul>

        <h2 id="regimen-sancionador">Régimen sancionador detallado</h2>
        <p>El artículo 63 y siguientes de la Ley 2/2023 establecen el régimen sancionador:</p>
        <ul>
          <li><strong>Infracciones muy graves</strong> (entre las que se encuentra no disponer de canal obligatorio): multa de hasta <strong>1.000.000€</strong> para personas jurídicas y hasta <strong>300.000€</strong> para personas físicas. Pueden ir acompañadas de amonestación pública y prohibición de beneficios o subvenciones públicas.</li>
          <li><strong>Infracciones graves</strong> (canal que no cumple los requisitos): multa de hasta <strong>600.000€</strong> para personas jurídicas y hasta <strong>200.000€</strong> para personas físicas.</li>
          <li><strong>Infracciones leves</strong> (incumplimientos procedimentales): multa de hasta <strong>100.000€</strong> para personas jurídicas y hasta <strong>25.000€</strong> para personas físicas.</li>
        </ul>
        <p>Un aspecto crítico que muchos directivos desconocen: la ley establece explícitamente la responsabilidad individual de administradores y directivos cuando el incumplimiento se derive de una falta de diligencia o de la omisión de sus deberes de supervisión. La responsabilidad no recae únicamente sobre la empresa como entidad: puede afectar directamente al cargo.</p>

        <h2 id="que-debe-hacer-mi-empresa">¿Qué debe hacer mi empresa ahora?</h2>
        <p>Si tu empresa está obligada por la Ley 2/2023 (50+ empleados, sector público, facturación &gt;10M€), estos son los pasos inmediatos que debes dar:</p>
        <ol>
          <li><strong>Verificar el canal:</strong> ¿Dispones de un canal de denuncias operativo que cumpla los requisitos técnicos de la ley? Si no, activarlo inmediatamente es la prioridad.</li>
          <li><strong>Designar formalmente al RSII:</strong> el nombramiento debe constar en un documento formal aprobado por el órgano competente (consejo de administración, junta general). No vale una designación verbal o informal.</li>
          <li><strong>Notificar el RSII a la AIPI:</strong> a través del formulario oficial en sede.proteccioninformante.gob.es, con certificado digital o Autofirma, antes del 10 de abril de 2026.</li>
          <li><strong>Asegurar el funcionamiento real:</strong> el canal debe estar activo, con procedimiento documentado, plazos de respuesta configurados y acuses automáticos en marcha.</li>
          <li><strong>Documentar todo:</strong> conservar la política del SII aprobada, el acta de nombramiento del RSII, la confirmación de notificación a la AIPI y el registro de cualquier denuncia recibida y gestionada.</li>
        </ol>

