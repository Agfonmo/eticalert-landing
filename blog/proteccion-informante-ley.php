        <h1>Protección del informante: qué garantías tiene quien denuncia según la Ley 2/2023</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a enero 2026 · 9 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-01-15">15 de enero de 2026</time> por el equipo de EticAlert</p>

        <p>El mayor freno para que alguien use un canal de denuncias no es no saber cómo funciona: es el miedo. Miedo a perder el trabajo, miedo a ser marginado por los compañeros, miedo a que el jefe se entere y tome represalias. La Ley 2/2023 fue diseñada precisamente para eliminar ese miedo, estableciendo un sistema robusto de protección para quienes se atreven a denunciar. Este artículo explica qué garantías ofrece la ley y cómo un buen canal técnico las hace efectivas.</p>

        <h2 id="prohibicion-represalias">Principio fundamental: prohibición absoluta de represalias</h2>
        <p>El núcleo de la protección del informante en la Ley 2/2023 es la <strong>prohibición absoluta de represalias</strong>. Cualquier acto de la empresa que perjudique al informante como consecuencia de su denuncia está prohibido, independientemente de su forma. Esto es aplicable desde el momento en que se presenta la denuncia hasta mucho tiempo después.</p>
        <p>Esta prohibición se aplica no solo al despido, que es la represalia más obvia, sino a cualquier forma de trato perjudicial que pueda conectarse causalmente con la denuncia. Y la ley presume esa conexión causal, invirtiendo la carga de la prueba.</p>

        <h2 id="que-es-represalia">Qué se considera represalia: lista completa</h2>
        <p>La Ley 2/2023 proporciona una lista no exhaustiva de conductas que se consideran represalias. La lista es más amplia de lo que muchos directivos esperan:</p>
        <ul>
          <li><strong>Despido o extinción del contrato</strong>, incluyendo el despido declarado procedente si la empresa no puede probar que se basa en motivos ajenos a la denuncia.</li>
          <li><strong>Suspensión del contrato</strong>, incluyendo suspensiones disciplinarias que se adopten tras la denuncia.</li>
          <li><strong>Degradación o denegación de ascenso</strong> que hubiera correspondido según los criterios ordinarios de la empresa.</li>
          <li><strong>Cambio de funciones, lugar de trabajo o turno</strong> que implique un perjuicio objetivo para el denunciante.</li>
          <li><strong>Reducción de salario</strong>, aunque sea formalmente voluntaria (ej. "acordada" bajo presión).</li>
          <li><strong>Evaluaciones de desempeño negativas</strong> que no se correspondan con el rendimiento real y aparezcan tras la denuncia.</li>
          <li><strong>Exclusión de formación, ascensos o cualquier ventaja disponible para otros empleados</strong>.</li>
          <li><strong>Intimidación, acoso, hostigamiento u ostracismo</strong>: el informante es ignorado por los compañeros, excluido de reuniones, sometido a comentarios hostiles.</li>
          <li><strong>Listas negras</strong>: la empresa (o sus agentes) comunica a otros empleadores o al sector que el informante "es conflictivo".</li>
          <li><strong>Daño reputacional</strong>: difusión de información perjudicial sobre el informante en redes profesionales, en el sector o entre compañeros.</li>
          <li><strong>Revelación de la identidad</strong> del informante sin su consentimiento, especialmente cuando se había comprometido confidencialidad.</li>
          <li><strong>Terminación de un contrato de servicios o suministro</strong> con un proveedor o cliente que ha denunciado.</li>
          <li><strong>Derivación a autoridades judiciales o administrativas</strong> con el objetivo de hostigar al informante (ej. una denuncia penal sin fundamento real).</li>
        </ul>

        <h2 id="inversion-carga-prueba">Inversión de la carga de la prueba</h2>
        <p>Este es el mecanismo jurídico más poderoso de la ley para proteger al informante. Su funcionamiento es el siguiente:</p>
        <p>Si el informante sufre algún perjuicio después de presentar la denuncia, y hay una conexión temporal entre la denuncia y el perjuicio, se presume que el perjuicio es una represalia. A partir de ese momento, corresponde a la empresa demostrar que el perjuicio se debe a causas ajenas e independientes de la denuncia.</p>
        <p>En la práctica, esto significa que si un empleado es despedido dos semanas después de presentar una denuncia, la empresa tiene que demostrar activamente que el despido se debe a causas objetivas preexistentes (rendimiento, restructuración, causa disciplinaria) y no a la denuncia. Si no puede hacerlo, el despido puede ser declarado nulo.</p>
        <p>El despido nulo —no improcedente, sino nulo— implica la readmisión obligatoria del trabajador con todos sus derechos. Es la consecuencia más grave para la empresa en términos laborales.</p>

        <h2 id="a-quien-protege">A quién protege la ley</h2>
        <p>La protección no se limita al informante directo. La Ley 2/2023 extiende la protección a:</p>
        <ul>
          <li><strong>El informante</strong> que presenta la comunicación.</li>
          <li><strong>Los familiares del informante</strong> que puedan ser objeto de represalias indirectas (ej. el cónyuge que trabaja en la misma empresa).</li>
          <li><strong>Los compañeros y personas del entorno laboral</strong> del informante que le hayan apoyado o ayudado durante el proceso de denuncia.</li>
          <li><strong>Los facilitadores</strong>: personas que, sin ser el informante, han contribuido a facilitar la denuncia (ej. alguien que ayudó al informante a redactar la denuncia o a recopilar documentación).</li>
          <li><strong>Personas jurídicas</strong> vinculadas al informante (ej. la empresa del informante si este es autónomo o proveedor).</li>
        </ul>

        <h2 id="medidas-apoyo">Medidas de apoyo al informante</h2>
        <p>La ley no solo prohíbe las represalias: también prevé medidas positivas de apoyo al informante que enfrenta represalias o teme enfrentarlas:</p>
        <ul>
          <li><strong>Asesoramiento jurídico:</strong> el informante tiene derecho a recibir asesoramiento legal sobre sus derechos y el procedimiento aplicable, con independencia de que pueda sufrir o ya esté sufriendo represalias.</li>
          <li><strong>Asistencia financiera:</strong> en situaciones de represalia grave (ej. despido), el informante puede solicitar asistencia financiera para cubrir los costes del proceso judicial mientras dure la disputa.</li>
          <li><strong>Apoyo psicológico:</strong> acceso a servicios de apoyo psicológico para afrontar el estrés y el impacto emocional derivado de la denuncia y sus consecuencias.</li>
        </ul>
        <p>Estas medidas las gestiona la AIPI y, en su caso, las autoridades autonómicas competentes.</p>

        <h2 id="consecuencias-represaliar">Consecuencias de represaliar: infracción muy grave</h2>
        <p>Para la empresa, tomar represalias contra un informante no solo tiene consecuencias laborales (nulidad del despido). Es también una <strong>infracción muy grave</strong> de la Ley 2/2023, con una sanción de hasta <strong>1.000.000€</strong> para personas jurídicas y hasta 300.000€ para personas físicas.</p>
        <p>Adicionalmente, la empresa puede ser condenada a pagar una indemnización al informante represaliado por todos los daños y perjuicios causados: daño moral, lucro cesante, daño reputacional, gastos legales y cualquier otro perjuicio que el informante pueda acreditar.</p>
        <p>La combinación de sanción administrativa (hasta 1M€) + nulidad del despido (readmisión + salarios de tramitación) + indemnización judicial puede ser extraordinariamente costosa para una empresa mediana.</p>

