        <h2 id="aprobacion">Aprobación, comunicación y revisión</h2>

        <p>El protocolo sigue el mismo circuito formal que la política: lo redacta el RSII (o el asesor externo), lo aprueba el órgano de administración y se comunica a la plantilla. Tres detalles que conviene no saltarse:</p>

        <ul>
          <li><strong>Acta de aprobación.</strong> Guarda el acuerdo firmado con fecha. En una inspección, la AIPI comprueba que el procedimiento estaba aprobado <em>antes</em> de las denuncias que tramitaste con él.</li>
          <li><strong>Coherencia con el canal real.</strong> Si el protocolo dice "acuse en 7 días" pero tu buzón de email no registra fechas, no puedes demostrar el cumplimiento. El documento y la herramienta tienen que contarse la misma historia.</li>
          <li><strong>Versionado.</strong> Cada revisión genera una versión nueva con fecha. Las denuncias se tramitan con la versión vigente en el momento de su recepción.</li>
        </ul>

        <h2 id="errores">Los 4 errores que más detecta la AIPI en los protocolos</h2>

        <ol>
          <li><strong>Plazos genéricos.</strong> "Se responderá lo antes posible" no es un plazo. Los 7 días hábiles y los 3 meses deben figurar textualmente.</li>
          <li><strong>No regular el conflicto de interés.</strong> Si la denuncia afecta al propio RSII y el protocolo no prevé quién tramita en su lugar, el sistema queda bloqueado — y eso es responsabilidad de la empresa.</li>
          <li><strong>Olvidar a la persona afectada.</strong> El procedimiento debe garantizar sus derechos (información, audiencia, presunción de inocencia). Un protocolo volcado solo en el informante está incompleto.</li>
          <li><strong>No prever la derivación a Fiscalía.</strong> El art. 9.2.j la exige expresamente como parte del contenido mínimo del procedimiento.</li>
        </ol>

