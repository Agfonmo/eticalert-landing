        <p>¿Pueden integrarse política y protocolo en un único documento? Sí, la ley no lo impide, y en pymes pequeñas es frecuente. Aun así, conviene mantenerlos separados: la política casi nunca cambia, mientras que el protocolo se ajusta cuando cambia el RSII, el proveedor del canal o el flujo de tramitación. Tener dos documentos te permite actualizar uno sin tener que volver a llevar el otro al órgano de administración.</p>

        <h2 id="fases">Las 7 fases que el protocolo debe regular (con sus plazos)</h2>

        <p>El esqueleto de cualquier protocolo conforme a la Ley 2/2023 es la secuencia de fases por las que pasa una comunicación. Estas son las siete, con los plazos legales en negrita:</p>

        <ol>
          <li><strong>Recepción.</strong> La comunicación entra por cualquiera de los canales habilitados (formulario web, verbal, postal). El sistema debe permitir el anonimato y generar un código de seguimiento para la comunicación bidireccional con el informante.</li>
          <li><strong>Acuse de recibo — máximo 7 días hábiles.</strong> El RSII confirma la recepción al informante (art. 9.2.b). Si la denuncia es anónima, el acuse se publica en el hilo del código de seguimiento.</li>
          <li><strong>Admisión a trámite.</strong> El RSII verifica que la comunicación entra en el ámbito material del canal y no es manifiestamente infundada. La inadmisión debe motivarse y comunicarse al informante. Recomendado: fijar un plazo interno (por ejemplo, 15 días hábiles).</li>
          <li><strong>Investigación.</strong> Comprobación diligente de los hechos: solicitud de información adicional al informante, entrevistas, revisión documental. Aquí el protocolo debe regular la confidencialidad, el conflicto de interés del RSII y los derechos de la persona afectada (presunción de inocencia, derecho a ser informada — art. 9.2.f).</li>
          <li><strong>Resolución y respuesta — máximo 3 meses</strong> desde el acuse de recibo, <strong>prorrogable a 6</strong> en casos de especial complejidad con notificación al informante (art. 9.2.d). La respuesta indica si se adoptan medidas, sin revelar datos de terceros.</li>
          <li><strong>Derivación, si procede.</strong> Si los hechos pueden ser constitutivos de delito, el protocolo debe prever la remisión al Ministerio Fiscal (art. 9.2.j); si afectan a los intereses financieros de la UE, a la Fiscalía Europea.</li>
          <li><strong>Registro y conservación.</strong> Anotación en el libro-registro y conservación de la documentación. Los datos personales solo se conservan mientras sean necesarios; el libro-registro, un mínimo de <strong>10 años</strong>.</li>
        </ol>

        <div class="callout"><p><strong>El reloj corre solo:</strong> los plazos de 7 días y 3 meses son los dos incumplimientos más fáciles de acreditar en una inspección, porque se comprueban con fechas. Un canal con control de plazos automático (como el de <a href="/funcionalidades" style="color:var(--accent);">EticAlert</a>, que alerta al RSII antes de cada vencimiento) elimina este riesgo de raíz.</p></div>

