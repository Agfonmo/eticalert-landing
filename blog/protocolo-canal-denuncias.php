          <p>Este protocolo desarrolla el procedimiento de gestión de las informaciones recibidas a través del Sistema Interno de Información de [NOMBRE DE LA EMPRESA], en cumplimiento de los artículos 5.2.h y 9 de la Ley 2/2023, de 20 de febrero. Complementa la Política del Sistema Interno de Información aprobada el [DD/MM/AAAA].</p>

          <p><strong>2. RESPONSABLES DE LA TRAMITACIÓN</strong></p>
          <p>La tramitación corresponde al RSII, [NOMBRE / CARGO]. En caso de ausencia o conflicto de interés, asume la tramitación [CARGO SUSTITUTO / PROVEEDOR EXTERNO]. El informante puede excluir de la tramitación a personas concretas cuando concurra conflicto de interés, indicándolo en el formulario de comunicación.</p>

          <p><strong>3. RECEPCIÓN Y ACUSE DE RECIBO</strong></p>
          <p>Las comunicaciones se reciben a través de [URL DEL CANAL] y de los demás medios previstos en la Política. El RSII remitirá acuse de recibo al informante en un plazo máximo de <strong>siete días hábiles</strong>. En comunicaciones anónimas, el acuse se emitirá a través del código de seguimiento.</p>

          <p><strong>4. ADMISIÓN A TRÁMITE</strong></p>
          <p>En un plazo orientativo de [15] días hábiles, el RSII resolverá sobre la admisión. Serán inadmitidas, de forma motivada: (a) las comunicaciones ajenas al ámbito material del sistema; (b) las manifiestamente infundadas; (c) las que carezcan de verosimilitud mínima. La inadmisión se comunicará al informante, informándole de la posibilidad de acudir al canal externo de la AIPI.</p>

          <p><strong>5. INSTRUCCIÓN E INVESTIGACIÓN</strong></p>
          <p>El RSII practicará las diligencias necesarias: requerimientos de información al informante, entrevistas, revisión de documentación y sistemas. Toda persona que participe en la instrucción queda sujeta al deber de confidencialidad. La persona afectada será informada de los hechos relatados de forma sucinta, sin revelar la identidad del informante, y tendrá derecho a ser oída y a la presunción de inocencia.</p>

          <p><strong>6. FINALIZACIÓN</strong></p>
          <p>La instrucción finalizará mediante informe del RSII con una de estas conclusiones: (a) archivo por falta de indicios; (b) traslado al órgano competente para la adopción de medidas disciplinarias; (c) remisión al Ministerio Fiscal si los hechos pudieran ser constitutivos de delito (o a la Fiscalía Europea si afectan a los intereses financieros de la UE). El resultado se comunicará al informante en un plazo máximo de <strong>tres meses</strong> desde el acuse de recibo, prorrogable a seis en casos de especial complejidad.</p>

          <p><strong>7. REGISTRO Y CONSERVACIÓN</strong></p>
          <p>Toda comunicación se anotará en el libro-registro con su fecha de recepción, código, estado de tramitación y resolución. El libro-registro se conservará durante un mínimo de diez años. Los datos personales que no resulten necesarios se suprimirán conforme al art. 32 de la Ley 2/2023 y al RGPD.</p>

          <p><strong>8. REVISIÓN DEL PROTOCOLO</strong></p>
          <p>El protocolo se revisará [anualmente] y siempre que cambien el RSII, el proveedor del canal o la normativa aplicable. Las versiones quedarán archivadas con su fecha de aprobación.</p>

