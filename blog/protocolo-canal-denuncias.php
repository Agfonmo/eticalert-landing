        <h1>Protocolo del canal de denuncias (Ley 2/2023): qué debe contener y modelo por secciones</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a junio 2026 · 11 minutos de lectura</p>
