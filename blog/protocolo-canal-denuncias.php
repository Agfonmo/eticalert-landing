        <p>Tener la plataforma del canal de denuncias activa no basta: la <a href="/blog/ley-2-2023-canal-de-denuncias" style="color:var(--accent);">Ley 2/2023</a> exige que exista un <strong>procedimiento documentado de gestión de las informaciones</strong> (art. 5.2.h). Ese documento es el <strong>protocolo del canal de denuncias</strong>: el "manual de instrucciones" interno que dice qué pasa desde que alguien envía una comunicación hasta que se cierra el expediente — quién interviene, en qué orden y con qué plazos. En esta guía te explicamos qué debe contener exactamente y te dejamos un modelo orientativo por secciones para adaptarlo a tu organización.</p>

