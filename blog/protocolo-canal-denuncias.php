        <h2 id="que-es">Qué es el protocolo del canal de denuncias y qué exige la ley</h2>

        <p>El protocolo (también llamado procedimiento de gestión de informaciones o procedimiento de tramitación de denuncias) es el documento operativo que regula el ciclo de vida completo de una comunicación dentro del sistema interno de información. La Ley 2/2023 no le da un nombre concreto, pero sí obliga a que exista y a que cumpla unos mínimos:</p>

        <ul>
          <li>El art. 5.2.h exige que el sistema cuente con <strong>procedimientos de gestión de las informaciones recibidas</strong>.</li>
          <li>El art. 9 detalla el contenido mínimo de ese procedimiento: acuse de recibo en 7 días hábiles, comunicación con el informante, plazo máximo de respuesta de 3 meses, respeto a la presunción de inocencia y derechos de la persona afectada.</li>
          <li>El art. 26 obliga a llevar un <strong>libro-registro</strong> de las informaciones recibidas, que el protocolo debe regular.</li>
        </ul>

        <p>Es decir: la ley describe el resultado y tu protocolo describe el camino. Si la AIPI inspecciona tu sistema, pedirá tres documentos — la política, el protocolo y el libro-registro — y comprobará que lo que dice el papel coincide con lo que hace el canal en la práctica.</p>

        <h2 id="diferencias">Protocolo, política y libro-registro: en qué se diferencian</h2>

