        <h1>Canal de denuncias para empresas de construcción: obligaciones y riesgos específicos</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a febrero 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el 14 de febrero de 2026 por el equipo de EticAlert</p>

        <p>El sector de la construcción es uno de los que más expuestos están a las infracciones que cubre la Ley 2/2023. No solo por la complejidad de las cadenas de subcontratación, sino por la frecuencia de licitaciones públicas, los riesgos laborales inherentes al sector y la interacción constante con el medioambiente. Una constructora sin canal de denuncias no solo incumple la ley: está más expuesta que la mayoría a los riesgos que el canal está diseñado para detectar.</p>

        <h2 id="por-que-sensible">Por qué la construcción es especialmente sensible</h2>
        <p>El sector de la construcción tiene características estructurales que lo hacen especialmente relevante desde el punto de vista de la Ley 2/2023:</p>
        <ul>
          <li><strong>Alta dependencia de la contratación pública:</strong> muchas constructoras trabajan fundamentalmente para administraciones públicas, lo que las sitúa directamente en el ámbito de la normativa anticorrupción.</li>
          <li><strong>Cadenas de subcontratación complejas:</strong> en una obra pueden coexistir la constructora principal, varias subcontratas especializadas y proveedores de materiales, todos con acceso a información sobre posibles irregularidades.</li>
          <li><strong>Riesgos laborales elevados:</strong> el sector de la construcción es uno de los que registra más accidentes laborales graves, y el incumplimiento de la normativa de prevención de riesgos es denunciable.</li>
          <li><strong>Impacto medioambiental:</strong> vertidos, gestión de residuos de construcción y demolición (RCD), contaminación del suelo son riesgos reales en el sector.</li>
        </ul>

        <h2 id="infracciones-tipicas">Infracciones típicas del sector</h2>
        <p>Basándonos en los tipos de infracciones más frecuentes en el sector, el canal de una constructora debería estar especialmente preparado para recibir comunicaciones sobre:</p>
        <ul>
          <li>Irregularidades en licitaciones públicas: acceso previo a información reservada, acuerdos de no concurrencia, fraccionamiento de contratos para eludir umbrales de publicidad.</li>
          <li>Subcontratación encubierta o en cadena que excede los límites legales.</li>
          <li>Ocultación de accidentes laborales o condiciones de trabajo inseguras.</li>
          <li>Trabajadores sin contrato ni alta en Seguridad Social en las obras.</li>
          <li>Vertidos o gestión irregular de residuos de construcción y demolición.</li>
          <li>Falsificación de certificados de calidad de materiales.</li>
          <li>Incumplimiento del Plan de Seguridad de la obra.</li>
          <li>Facturación falsa entre constructora y subcontratas.</li>
        </ul>

        <h2 id="quien-puede-denunciar">¿Quién puede denunciar en una constructora?</h2>
        <p>Una de las particularidades del sector es que los posibles denunciantes no son solo los empleados directos de la constructora. La Ley 2/2023 obliga a que el canal sea accesible también para:</p>
        <ul>
          <li>Trabajadores de las subcontratas que operen en las obras de la empresa.</li>
          <li>Proveedores de materiales y equipos que conozcan irregularidades en el proceso.</li>
          <li>Autónomos con encargos en las obras.</li>
          <li>Técnicos externos (arquitectos, aparejadores, coordinadores de seguridad) que supervisan las obras.</li>
          <li>Empleados de la propia empresa en cualquier área (administración, dirección de obras, oficina técnica).</li>
        </ul>
        <p>Esto significa que la URL del canal debe comunicarse activamente a toda la cadena de proveedores y subcontratistas, no solo a la plantilla propia.</p>

        <h2 id="contratacion-publica">Riesgo de exclusión de contratación pública</h2>
        <p>Para una constructora que trabaja con administraciones públicas, la exclusión de la contratación pública es probablemente la consecuencia más temida de una sanción por la Ley 2/2023. Y no es un riesgo teórico.</p>
        <p>Según la Ley 2/2023, la comisión de infracciones graves o muy graves puede acarrear una prohibición de contratar con el sector público de <strong>hasta 3 años</strong>. Para una constructora con obra pública como parte relevante de su cartera, esto es potencialmente devastador.</p>
        <p>Adicionalmente, el incumplimiento de la ley puede aparecer en inspecciones de la AIPI que resulten en resoluciones públicas, lo que afecta a la imagen de la empresa en futuros procesos de licitación.</p>

        <div style="border-left:3px solid var(--accent);background:var(--bg-tertiary);padding:1.25rem 1.5rem;border-radius:0 8px 8px 0;margin:2rem 0;">
          <strong style="color:var(--accent);">Importante:</strong> Muchos pliegos de contratación pública ya incorporan la exigencia de disponer de canal de denuncias como requisito de solvencia técnica o como criterio de valoración. No tenerlo puede excluirte de licitaciones incluso si la AIPI no ha sancionado todavía.
        </div>

        <h2 id="adaptacion-canal">Adaptación del canal al sector</h2>
        <p>Un canal de denuncias para el sector de la construcción debe tener algunas características específicas:</p>
        <ul>
          <li><strong>Accesibilidad para trabajadores de obra:</strong> muchos trabajadores no tienen acceso habitual a un ordenador. El canal debe ser accesible desde móvil con un formulario claro y sencillo.</li>
          <li><strong>Código QR:</strong> colocar códigos QR en las instalaciones de obra (barraca de herramientas, vestuarios, comedores) que lleven directamente al canal.</li>
          <li><strong>Multi-idioma:</strong> en obras donde trabajan equipos de distintos países, el canal debe estar disponible en los idiomas relevantes (portugués, rumano, árabe, etc.).</li>
          <li><strong>Categorías específicas del sector:</strong> el formulario debe incluir categorías de infracciones adaptadas al sector (seguridad laboral, residuos, licitaciones, etc.).</li>
        </ul>

