        <h1>Sanciones por no tener canal de denuncias: desglose completo por tipo de infracción</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a marzo 2026 · 8 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el 5 de marzo de 2026 por el equipo de EticAlert</p>

        <p>Uno de los aspectos más importantes —y peor conocidos— de la Ley 2/2023 es su régimen sancionador. No basta con saber que hay multas: es fundamental entender cuáles son las infracciones concretas que pueden motivar una sanción, cuánto puede costar cada una y qué consecuencias adicionales hay más allá de la multa económica. Este artículo hace ese desglose con detalle.</p>

        <h2 id="tipos-infracciones">Tipos de infracciones según la Ley 2/2023</h2>
        <p>La Ley 2/2023 clasifica las infracciones en tres categorías, siguiendo la estructura clásica del derecho administrativo sancionador español:</p>
        <ul>
          <li><strong>Infracciones muy graves:</strong> las que afectan directamente a la esencia del sistema —represalias, ausencia total de canal, vulneración de la confidencialidad del informante.</li>
          <li><strong>Infracciones graves:</strong> las que implican un incumplimiento sustancial de la normativa —canal que no funciona correctamente, falta de respuesta, no designar RSII.</li>
          <li><strong>Infracciones leves:</strong> deficiencias formales o procedimentales que no anulan el sistema pero sí incumplen requisitos concretos.</li>
        </ul>
        <p>Es importante destacar que la misma empresa puede ser sancionada por varias infracciones simultáneamente si comete conductas que entran en diferentes categorías. Las sanciones son acumulables.</p>

        <h2 id="muy-graves">Infracciones muy graves: de 600.001€ a 1.000.000€</h2>
        <p>Las infracciones muy graves son las más severas y están directamente vinculadas a las conductas más lesivas para el sistema de protección del informante. La sanción para personas jurídicas (empresas, organizaciones) puede alcanzar el <strong>millón de euros</strong>.</p>
        <p><strong>Principales infracciones muy graves:</strong></p>
        <ul>
          <li><strong>No disponer de canal de denuncias</strong> cuando se está obligado por ley. Esta es la infracción más clara: si tu empresa tiene 50 o más empleados y no tiene canal operativo, incurres automáticamente en infracción muy grave.</li>
          <li><strong>Tomar represalias contra el informante</strong> o contra cualquiera de las personas protegidas (familiares, compañeros que apoyaron la denuncia). El despido, la degradación, el traslado forzoso, la exclusión de formación o la intimidación son formas reconocidas de represalia.</li>
          <li><strong>Vulnerar la confidencialidad del informante</strong>, revelando su identidad sin su consentimiento o en casos no previstos expresamente por la ley.</li>
          <li><strong>Obstruir o impedir</strong> las actuaciones de investigación o inspección de la AIPI.</li>
          <li><strong>No adoptar las medidas cautelares ordenadas</strong> por la AIPI durante un procedimiento de inspección.</li>
        </ul>
        <p>Para personas físicas (administradores, directivos responsables), la sanción por infracciones muy graves puede llegar a <strong>300.000€</strong>.</p>

        <div style="border-left:3px solid var(--accent);background:var(--bg-tertiary);padding:1.25rem 1.5rem;border-radius:0 8px 8px 0;margin:2rem 0;">
          <strong style="color:var(--accent);">Importante:</strong> El coste de no tener canal (mínimo 100.000€ por infracción leve, hasta 1.000.000€ por infracción muy grave) es infinitamente mayor que tenerlo. Con EticAlert, el canal cuesta desde 19€/mes para empresas obligadas.
        </div>

        <h2 id="graves">Infracciones graves: de 100.001€ a 600.000€</h2>
        <p>Las infracciones graves cubren situaciones en las que la empresa tiene canal pero este no funciona correctamente, o bien incumple obligaciones organizativas fundamentales.</p>
        <p><strong>Principales infracciones graves:</strong></p>
        <ul>
          <li><strong>No responder al informante en el plazo de 3 meses</strong> con información sobre las medidas adoptadas o en curso. La ley exige feedback al denunciante dentro de ese plazo; ignorar la comunicación es infracción grave.</li>
          <li><strong>No designar un Responsable del Sistema Interno de Información (RSII)</strong> o designar a alguien que no tiene las condiciones de independencia e imparcialidad exigidas.</li>
          <li><strong>No notificar la designación del RSII a la AIPI</strong> en el plazo establecido (desde febrero de 2026 hay un plazo de 2 meses para comunicarlo).</li>
          <li><strong>Canal que no garantiza el anonimato</strong> cuando el informante lo solicita.</li>
          <li><strong>No conservar el libro-registro</strong> o mantenerlo de forma incompleta o fraudulenta.</li>
          <li><strong>No acusar recibo de la comunicación</strong> dentro de los 7 días naturales establecidos por la ley.</li>
          <li><strong>Canal accesible solo para empleados</strong> y no para todos los colectivos obligatorios (proveedores, contratistas, candidatos, clientes, etc.).</li>
        </ul>
        <p>Para personas físicas, la sanción por infracciones graves puede llegar a <strong>200.000€</strong>.</p>

        <h2 id="leves">Infracciones leves: hasta 100.000€</h2>
        <p>Las infracciones leves corresponden a deficiencias formales o incumplimientos menores que no anulan el sistema pero sí vulneran requisitos concretos de la normativa.</p>
        <p><strong>Ejemplos de infracciones leves:</strong></p>
        <ul>
          <li>Política del Sistema Interno de Información desactualizada o incompleta.</li>
          <li>No publicar la URL del canal en la web corporativa de forma visible.</li>
          <li>No comunicar el canal a los empleados de forma periódica.</li>
          <li>Canal que solo acepta denuncias escritas y no permite comunicaciones verbales.</li>
          <li>Formulario de denuncia que solicita datos innecesarios o no está adaptado al RGPD.</li>
          <li>No incluir información sobre el proceso de investigación en la política del SII.</li>
        </ul>
        <p>Para personas físicas, la sanción leve máxima es de <strong>25.000€</strong>.</p>

        <h2 id="responsabilidad-administradores">Responsabilidad personal de administradores</h2>
        <p>Este es el aspecto que más sorprende a muchos directivos: la Ley 2/2023 no solo sanciona a la empresa como persona jurídica. Los administradores, consejeros y directivos pueden ser sancionados <strong>a título personal</strong> si se acredita que el incumplimiento deriva de su negligencia o falta de supervisión.</p>
        <p>La responsabilidad personal puede surgir en situaciones como:</p>
        <ul>
          <li>Haber aprobado decisiones que suponían una represalia contra un informante.</li>
          <li>Haber ignorado reiteradamente las advertencias del RSII sobre el mal funcionamiento del canal.</li>
          <li>Haber tomado la decisión de no implantar el canal pese a conocer la obligación legal.</li>
          <li>Haber ordenado la revelación de la identidad de un informante.</li>
        </ul>
        <p>Esto significa que incluso si la empresa paga la sanción corporativa, el administrador puede enfrentarse a una sanción personal adicional de hasta 300.000€ por infracciones muy graves.</p>

        <h2 id="otras-consecuencias">Otras consecuencias: más allá de la multa</h2>
        <p>Las sanciones económicas no son la única consecuencia del incumplimiento. La Ley 2/2023 y el ordenamiento jurídico español contemplan efectos adicionales que pueden ser igual o más perjudiciales que las multas para muchas empresas:</p>

        <p><strong>Pérdida de subvenciones y ayudas públicas (hasta 4 años):</strong></p>
        <p>La comisión de infracciones graves o muy graves puede suponer la pérdida de la posibilidad de obtener subvenciones, ayudas o financiación pública durante un período de hasta 4 años. Para empresas que trabajan habitualmente con fondos europeos, ayudas autonómicas o subvenciones estatales, este efecto puede ser enormemente costoso.</p>

        <p><strong>Exclusión de la contratación pública (hasta 3 años):</strong></p>
        <p>Las infracciones graves o muy graves pueden conllevar la prohibición de contratar con el sector público durante hasta 3 años. Para constructoras, consultoras, empresas de servicios o cualquier organización que concurra a licitaciones públicas, esto equivale a perder un mercado entero.</p>

        <p><strong>Daño reputacional:</strong></p>
        <p>La AIPI está obligada a publicar las resoluciones sancionadoras en los supuestos más graves. Esto significa que la sanción puede aparecer en medios de comunicación, en buscadores y en el historial de la empresa, con el consiguiente impacto sobre clientes, empleados y socios.</p>

        <p><strong>Indemnización al informante represaliado:</strong></p>
        <p>Si la empresa ha tomado represalias contra un informante, además de la sanción administrativa puede ser condenada a pagar una indemnización por daños y perjuicios en vía judicial. La ley presume que cualquier perjuicio laboral sufrido por un informante es consecuencia de la represalia, invirtiendo la carga de la prueba.</p>

