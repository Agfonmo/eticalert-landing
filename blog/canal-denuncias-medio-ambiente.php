        <h2 id="riesgos-denunciables">¿Qué irregularidades puede detectar el canal en el sector ambiental?</h2>
        <p>Los empleados y subcontratistas de empresas ambientales son quienes tienen acceso real a lo que ocurre en las instalaciones. En muchos casos son los únicos que pueden detectar irregularidades que los sistemas de control externo no ven. Los tipos más habituales de comunicaciones en el sector son:</p>

        <ul>
          <li><strong>Vertidos irregulares:</strong> descarga de efluentes fuera de las condiciones autorizadas, a horas de menor vigilancia o en puntos no controlados.</li>
          <li><strong>Manipulación de residuos:</strong> mezcla de residuos peligrosos con no peligrosos para reducir costes de gestión, o declaración incorrecta de la codificación LER (Lista Europea de Residuos).</li>
          <li><strong>Falsedad en registros de trazabilidad:</strong> documentos de control y seguimiento de residuos (e-SIR, documentos de traslado) con datos incorrectos o falseados.</li>
          <li><strong>Superación de límites de emisiones:</strong> incumplimiento de los valores límite de emisión (VLE) a la atmósfera sin notificación a la autoridad ambiental competente.</li>
          <li><strong>Presión para ocultar incidentes:</strong> directivos que instruyen a los responsables de medio ambiente a no notificar incidentes a la comunidad autónoma para evitar sanciones o el cierre preventivo de la instalación.</li>
          <li><strong>Contaminación del suelo no declarada:</strong> derrames, fugas o contaminaciones puntuales que no son reportadas al organismo competente como establece la normativa de suelos contaminados.</li>
        </ul>

        <h2 id="valor-canal">Por qué el canal tiene especial valor en el sector ambiental</h2>
        <p>En muchas empresas del sector ambiental, la persona que supervisa el cumplimiento medioambiental es la misma que gestiona las operaciones que generan el riesgo de incumplimiento. Hay un conflicto de interés estructural: el responsable de medio ambiente puede sentir presión para no reportar problemas porque hacerlo pone en riesgo el funcionamiento de la planta.</p>
        <p>El canal de denuncias anónimo permite que cualquier operario, técnico de laboratorio, responsable de turno o subcontratista pueda comunicar lo que detecta sin pasar por su superior jerárquico directo, que puede ser precisamente quien está tomando la decisión irregular.</p>
        <p>Además, desde el punto de vista de la empresa, tener canal activo y operativo puede ser relevante en caso de inspección: demuestra que la organización tiene mecanismos de detección interna, lo que la AIPI y otros organismos supervisores valoran positivamente en la evaluación del programa de cumplimiento.</p>

