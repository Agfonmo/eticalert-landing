        <h1>Política del Sistema Interno de Información: qué debe incluir y cómo redactarla</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a febrero 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el 25 de febrero de 2026 por el equipo de EticAlert</p>

        <p>Tener la herramienta técnica para el canal de denuncias es solo la mitad del trabajo. La Ley 2/2023 también exige que exista un documento formal —la <strong>política del Sistema Interno de Información</strong>— que regule cómo funciona ese canal, qué protecciones ofrece y cómo se tramitan las denuncias. Sin esta política, el canal está incompleto desde el punto de vista legal. Este artículo te explica qué debe contener y cómo redactarla.</p>

        <h2 id="que-es">¿Qué es la política del SII?</h2>
        <p>La política del Sistema Interno de Información (SII) es el documento corporativo que describe el funcionamiento del canal de denuncias de la empresa. Es equivalente a lo que en otras organizaciones se conoce como "política de compliance" o "protocolo de whistleblowing".</p>
        <p>No es un documento de marketing ni una declaración de intenciones genérica: es un documento operativo y jurídico que debe describir con precisión cómo se reciben las denuncias, quién las gestiona, en qué plazos, con qué garantías para el informante y con qué consecuencias para quien actúe de mala fe.</p>
        <p>La AIPI puede solicitar este documento durante una inspección. Su ausencia o su contenido deficiente puede considerarse infracción de la Ley 2/2023.</p>

        <h2 id="quien-aprueba">¿Quién aprueba la política del SII?</h2>
        <p>La política del Sistema Interno de Información debe ser aprobada formalmente por el <strong>órgano de administración de la empresa</strong>: el consejo de administración, el administrador único, el administrador solidario o mancomunado, o el equivalente en la estructura jurídica concreta.</p>
        <p>Esta aprobación formal es importante porque:</p>
        <ul>
          <li>Convierte la política en una norma corporativa de obligado cumplimiento para todos los empleados.</li>
          <li>Demuestra el compromiso de la dirección con el sistema.</li>
          <li>Atribuye la responsabilidad de cumplimiento al nivel más alto de la organización.</li>
          <li>Es el punto de partida para la notificación del RSII a la AIPI.</li>
        </ul>
        <p>Normalmente el RSII redacta la política y la somete a aprobación del órgano de administración. En empresas con asesoría jurídica, es habitual que el despacho proporcione un borrador que el RSII adapta a la realidad concreta de la organización.</p>

        <h2 id="contenido-minimo">Contenido mínimo obligatorio: 12 puntos esenciales</h2>
        <p>Basándonos en los requisitos de la Ley 2/2023 y en las orientaciones de la AIPI y la AEPD, la política del SII debe incluir al menos estos 12 elementos:</p>

        <p><strong>1. Objeto y propósito del sistema</strong></p>
        <p>Descripción del sistema, su objetivo (detectar y gestionar infracciones normativas) y su relación con los valores éticos de la empresa.</p>

        <p><strong>2. Ámbito de aplicación</strong></p>
        <p>A quién se aplica la política: empleados actuales, exempleados, candidatos, proveedores, contratistas, clientes, accionistas y cualquier persona con relación laboral o comercial con la empresa.</p>

        <p><strong>3. Canales de comunicación disponibles</strong></p>
        <p>URL del canal digital, número de teléfono si existe, posibilidad de denuncia presencial ante el RSII. Instrucciones claras sobre cómo acceder al canal.</p>

        <p><strong>4. Procedimiento de tramitación</strong></p>
        <p>Descripción del proceso desde que se recibe la denuncia hasta que se resuelve: acuse de recibo (7 días), análisis de admisión, instrucción (si procede), investigación, resolución y comunicación al informante.</p>

        <p><strong>5. Plazos</strong></p>
        <p>Plazos concretos: 7 días para el acuse de recibo, 3 meses para informar al denunciante sobre las medidas adoptadas, plazo de conservación de los datos (máximo 10 años).</p>

        <p><strong>6. El RSII: identidad del cargo y funciones</strong></p>
        <p>Cargo de la persona designada como RSII (no necesariamente el nombre, que puede cambiar), sus funciones, su independencia funcional y cómo contactarle de forma confidencial si es necesario.</p>

        <p><strong>7. Protección del informante: prohibición de represalias</strong></p>
        <p>Declaración expresa de que está prohibido cualquier tipo de represalia contra quien denuncia de buena fe. Descripción de las medidas de protección y del mecanismo de denuncia en caso de sufrir represalias.</p>

        <p><strong>8. Confidencialidad</strong></p>
        <p>Garantía de que la identidad del informante no será revelada sin su consentimiento, salvo en los casos previstos expresamente por la ley. Quién tiene acceso a los datos y bajo qué condiciones.</p>

        <p><strong>9. Tratamiento de datos conforme al RGPD</strong></p>
        <p>Base legal del tratamiento (obligación legal, art. 6.1.c RGPD), responsable del tratamiento, datos que se recogen, plazos de conservación y cómo ejercer los derechos de acceso, rectificación y supresión.</p>

        <p><strong>10. Infracciones denunciables</strong></p>
        <p>Lista de las categorías de infracciones que pueden comunicarse a través del canal: contratación pública, servicios financieros, seguridad laboral, medioambiente, protección de datos, fraude fiscal, infracciones penales, y cualquier otra conducta contraria al código ético de la empresa.</p>

        <p><strong>11. Denuncias de mala fe</strong></p>
        <p>Advertencia de que las denuncias realizadas con conocimiento de su falsedad o con ánimo de perjudicar a otro no están protegidas por la ley y pueden tener consecuencias disciplinarias, civiles y penales para quien las presenta.</p>

        <p><strong>12. Régimen disciplinario</strong></p>
        <p>Descripción de las consecuencias disciplinarias tanto para quien comete las infracciones denunciadas como para quien vulnera la confidencialidad del sistema o toma represalias contra un informante.</p>

        <h2 id="difusion">¿Cómo difundir la política?</h2>
        <p>Redactar la política no es suficiente: hay que asegurarse de que todos los grupos de interés la conocen. Las mejores prácticas incluyen:</p>
        <ul>
          <li>Publicarla en la intranet corporativa o en el área de documentos accesibles a todos los empleados.</li>
          <li>Incluirla en el manual de acogida de nuevas incorporaciones.</li>
          <li>Enviar un comunicado a todos los empleados al aprobarla, con enlace al texto completo.</li>
          <li>Mencionarla en el canal de denuncias accesible externamente (con enlace al texto).</li>
          <li>Incluir referencia a ella en los contratos con proveedores y colaboradores principales.</li>
        </ul>

        <h2 id="revision">Revisión periódica</h2>
        <p>La política del SII no es un documento estático. Debe revisarse al menos en estos momentos:</p>
        <ul>
          <li><strong>Anualmente</strong>, como mínimo, para verificar que sigue siendo adecuada y actualizada.</li>
          <li><strong>Cuando cambie el RSII</strong>: la política debe reflejar el nuevo cargo o nombre si se hace referencia explícita.</li>
          <li><strong>Cuando haya cambios normativos</strong>: nuevas instrucciones de la AIPI, resoluciones sancionadoras que establezcan nuevos estándares, o cambios en el RGPD.</li>
          <li><strong>Cuando la empresa cambie significativamente</strong>: fusiones, adquisiciones, cambio de actividad, apertura de nuevos países.</li>
        </ul>
        <p>Cada revisión debe quedar documentada, con la fecha de aprobación por el órgano de administración.</p>

