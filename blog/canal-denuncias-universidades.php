        <p>Para las universidades públicas, la obligación es doble: como administración pública y como organización con cientos o miles de empleados. Muchas universidades públicas gestionan, además, fondos europeos y contratos públicos de investigación, lo que las convierte en entidades de alto riesgo de inspección por la AIPI.</p>

        <h2 id="que-denunciar">¿Qué se puede denunciar en el canal de una universidad?</h2>

        <p>Las denuncias posibles en el ámbito universitario son especialmente variadas:</p>

        <ul>
          <li><strong>Irregularidades en contratación pública.</strong> Contratos adjudicados sin concurrencia, fraccionamiento de contratos para evitar umbrales, favoritismo en la selección de proveedores de investigación.</li>
          <li><strong>Fraude en gestión de fondos públicos y europeos.</strong> Justificaciones incorrectas de proyectos de investigación, uso de fondos para fines distintos al objeto de la convocatoria.</li>
          <li><strong>Acoso laboral o sexual.</strong> Entre docentes, entre personal de administración, o de docentes hacia personal subordinado o hacia alumnos en edad laboral.</li>
          <li><strong>Plagios o irregularidades académicas con consecuencias legales.</strong> Fraudes en trabajos fin de grado o máster con implicaciones en titulaciones oficiales.</li>
          <li><strong>Violaciones de protección de datos (RGPD).</strong> Gestión incorrecta de datos de alumnos o personal, accesos no autorizados, incidentes de seguridad no notificados.</li>
          <li><strong>Incumplimientos medioambientales.</strong> Gestión de residuos de laboratorio, vertidos de centros de investigación con instalaciones físicas.</li>
        </ul>

        <h2 id="quien-gestiona">¿Quién gestiona el canal en una universidad?</h2>

        <p>En una universidad, el RSII suele ser uno de estos perfiles:</p>

        <ul>
          <li>El <strong>Secretario General</strong> de la universidad, cuando el canal afecta a cuestiones institucionales de alto nivel.</li>
          <li>El <strong>responsable de Cumplimiento</strong> o la Unidad de Auditoría Interna, que tiene experiencia en gestión de riesgos e irregularidades.</li>
          <li>Una <strong>Comisión de Ética</strong> designada específicamente para esta función, especialmente en universidades con más de 500 empleados.</li>
          <li>Un <strong>asesor externo o despacho de abogados</strong> al que se externaliza la gestión, para garantizar independencia frente a la estructura jerárquica interna.</li>
        </ul>

        <p>EticAlert permite configurar múltiples usuarios RSII con distintos niveles de acceso, y el informante puede excluir a gestores con conflicto de interés directamente desde el formulario de denuncia.</p>

        <h2 id="especificidades">Particularidades del canal en el entorno universitario</h2>

        <h3>Alumnos como potenciales informantes</h3>

        <p>La Ley 2/2023 protege como informantes a trabajadores, pero también a personas en prácticas, voluntarios y, en algunos casos, a personas que hayan cesado su relación laboral. Los alumnos no son trabajadores, pero pueden ser testigos de irregularidades que afecten al ámbito laboral de la institución. La universidad puede optar por ampliar el ámbito del canal para admitir denuncias de alumnos sobre conductas de personal docente o administrativo.</p>

        <h3>Pluralidad de centros y campus</h3>

        <p>Una universidad con varios campus puede gestionar todas las denuncias desde un único canal centralizado, o disponer de canales diferenciados por campus o facultad. EticAlert permite configurar un único canal con el logo y nombre de la institución, visible para toda la comunidad universitaria.</p>

        <h3>Relación con el canal externo de la AIPI</h3>

        <p>La Ley 2/2023 establece también un canal externo gestionado por la <a href="/blog/aipi-sanciones-canal-denuncias" style="color:var(--accent);">Autoridad Independiente de Protección del Informante (AIPI)</a>. Los informantes pueden elegir libremente entre el canal interno de la universidad y el canal externo de la AIPI. El canal interno no sustituye al externo, y su existencia no impide que el informante acuda directamente a la AIPI.</p>

        <h2 id="pasos">Cómo activar el canal en una universidad</h2>

        <ol>
          <li><strong>Designa al RSII o a la Comisión de Ética.</strong> Documenta la designación formalmente: cargo, nombre y función dentro del sistema de canal de denuncias.</li>
          <li><strong>Activa EticAlert.</strong> Regístrate en <a href="/registro" style="color:var(--accent);">/registro</a> con el email institucional. La configuración del canal tarda menos de 30 minutos: nombre de la institución, categorías de denuncia, texto de bienvenida y datos del RSII.</li>
          <li><strong>Publica la URL en la web institucional.</strong> La Ley 2/2023 exige que el canal sea fácilmente accesible. Inclúyelo en la sección de transparencia, en el portal de empleados y en el manual de bienvenida.</li>
          <li><strong>Comunícalo a todo el personal.</strong> Envía una comunicación a todo el personal docente, investigador y de administración explicando la existencia del canal, cómo usarlo y las protecciones que garantiza la ley al informante.</li>
          <li><strong>Aprueba la Política de Sistema Interno de Información.</strong> Documenta el <a href="/blog/politica-sistema-interno-informacion" style="color:var(--accent);">procedimiento interno</a> de tramitación de denuncias y apruébalo por el órgano de gobierno competente (Consejo de Gobierno o equivalente).</li>
        </ol>

