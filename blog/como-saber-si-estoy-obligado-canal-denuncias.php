        <h2 id="casos-especiales" style="margin-top:3rem;">Casos especiales que generan dudas frecuentes</h2>

        <details style="background:var(--bg-secondary);border:1px solid var(--border);border-radius:var(--radius-lg);padding:1.25rem 1.5rem;margin-bottom:.875rem;">
          <summary style="font-weight:600;cursor:pointer;font-size:1rem;">Grupos empresariales: ¿se cuenta por empresa o por grupo?</summary>
          <div style="margin-top:.875rem;color:var(--text-secondary);font-size:.9375rem;">
            <p>La obligación se evalúa empresa por empresa, no por el grupo en su conjunto. Si la matriz tiene 200 empleados pero la filial española tiene 30, la filial no está obligada por el umbral de empleados (aunque puede estarlo por criterio sectorial).</p>
            <p>Sin embargo, las empresas de un mismo grupo de entre 50 y 249 empleados pueden <strong>compartir un único canal de denuncias</strong> conforme al artículo 11 de la Ley 2/2023. La responsabilidad del cumplimiento sigue siendo individual de cada empresa.</p>
            <p><strong>Atención en grupos internacionales:</strong> si la matriz extranjera ya tiene un canal de denuncias por exigencia de la legislación de su país, ese canal puede no cumplir los requisitos específicos de la Ley 2/2023 española (idioma, plazos, RSII, comunicación a la AIPI). Conviene revisar si el canal global cubre los requisitos locales.</p>
          </div>
        </details>

        <details style="background:var(--bg-secondary);border:1px solid var(--border);border-radius:var(--radius-lg);padding:1.25rem 1.5rem;margin-bottom:.875rem;">
          <summary style="font-weight:600;cursor:pointer;font-size:1rem;">Franquicias: ¿cada franquiciado computa por separado?</summary>
          <div style="margin-top:.875rem;color:var(--text-secondary);font-size:.9375rem;">
            <p>Sí. Cada empresa franquiciada es una persona jurídica independiente. Si la franquicia local tiene 30 empleados, no está obligada por el umbral de 50, aunque la red nacional tenga miles. El franquiciador no asume la obligación de las franquiciadas.</p>
            <p>La excepción sería si la franquicia local supera el umbral de 50 empleados por sí misma, o si su actividad la convierte en sujeto obligado sectorial (por ejemplo, una franquicia de agencia inmobiliaria que intermedie en compraventas).</p>
          </div>
        </details>

        <details style="background:var(--bg-secondary);border:1px solid var(--border);border-radius:var(--radius-lg);padding:1.25rem 1.5rem;margin-bottom:.875rem;">
          <summary style="font-weight:600;cursor:pointer;font-size:1rem;">Empresas en crecimiento: ¿cuándo nace exactamente la obligación?</summary>
          <div style="margin-top:.875rem;color:var(--text-secondary);font-size:.9375rem;">
            <p>La obligación nace en el momento en que la media de trabajadores de los últimos 12 meses supera 50. No hay un periodo de gracia adicional: una vez que se alcanza el umbral, la empresa tiene la obligación desde ese momento.</p>
            <p>En la práctica, conviene monitorizar el promedio mensual durante el año. Si en octubre de 2026 la media acumulada del año supera 50, la empresa debería tener el canal activo en ese momento.</p>
          </div>
        </details>

        <details style="background:var(--bg-secondary);border:1px solid var(--border);border-radius:var(--radius-lg);padding:1.25rem 1.5rem;margin-bottom:.875rem;">
          <summary style="font-weight:600;cursor:pointer;font-size:1rem;">ONGs y asociaciones: ¿están obligadas?</summary>
          <div style="margin-top:.875rem;color:var(--text-secondary);font-size:.9375rem;">
            <p>Depende. Una ONG o asociación privada que no recibe fondos públicos y tiene menos de 50 empleados no está obligada por la Ley 2/2023.</p>
            <p>Sin embargo, puede estarlo si: (1) recibe subvenciones públicas relevantes y está bajo el control efectivo de una administración; (2) gestiona contratos públicos de servicios; (3) es una fundación vinculada a un partido político o sindicato; (4) tiene más de 50 empleados; o (5) su actividad la convierte en sujeto obligado PBC (algunas fundaciones que canalizan recursos internacionalmente).</p>
            <p>Ver guía: <a href="/blog/canal-denuncias-ongs" style="color:var(--accent);">Canal de denuncias para ONGs y asociaciones →</a></p>
