        <h1>¿Estás obligado a tener canal de denuncias aunque tengas menos de 50 empleados?</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a mayo 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-05-26">26 de mayo de 2026</time> por el equipo de EticAlert</p>

        <p>El 90% de las empresas españolas tiene menos de 50 empleados. Muchas de ellas han dado por supuesto que la <a href="/blog/ley-2-2023-canal-de-denuncias" style="color:var(--accent);">Ley 2/2023</a> no les afecta porque no alcanzan ese umbral. Pero hay un matiz legal que cambia el análisis completamente: <strong>el umbral de 50 empleados no es el único criterio de obligación</strong>.</p>

        <p>La Directiva (UE) 2019/1937, que la Ley 2/2023 transpone al derecho español, establece que la obligación aplica también a entidades privadas que operen en sectores regulados por normativa europea de servicios financieros, prevención del blanqueo de capitales, seguridad del transporte o protección del medioambiente. Independientemente de cuántos trabajadores tengan.</p>

        <div class="callout">
          <p><strong>Clave:</strong> si tu actividad está regulada por alguna directiva o reglamento europeo de los sectores mencionados, puedes estar obligado a tener canal de denuncias aunque tengas 8, 15 o 30 empleados. La obligación viene de tu sector, no de tu plantilla.</p>
        </div>

        <h2 id="sector-blanqueo">1. Sujetos obligados por prevención del blanqueo de capitales</h2>
        <p>El segmento más amplio de empresas pequeñas potencialmente obligadas son los <strong>sujetos obligados por la Ley 10/2010</strong> de prevención del blanqueo de capitales y financiación del terrorismo. Esta ley transpone varias directivas europeas AML (anti-money laundering) y la Directiva Whistleblowing los incluye expresamente.</p>

        <p>Son sujetos obligados PBC, entre otros:</p>

