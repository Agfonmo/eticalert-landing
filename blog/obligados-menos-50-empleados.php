        <p>Una asesoría fiscal de 10 personas que ayuda a sus clientes a constituir sociedades, gestionar holding o planificar la tributación de operaciones inmobiliarias tiene muchas probabilidades de ser sujeto obligado PBC. Eso la pone en el ámbito de la Ley 2/2023 independientemente de su plantilla.</p>

        <h2 id="sector-financiero">2. Sector financiero y asegurador regulado</h2>
        <p>Las entidades financieras reguladas por normativa europea —aunque sean pequeñas— tienen obligaciones de canal que derivan directamente de sus licencias sectoriales.</p>

        <ul>
          <li><strong>Empresas de servicios de inversión</strong> (agencias de valores, sociedades de valores, ESIs) reguladas por MiFID II / LMVSI.</li>
          <li><strong>Gestoras de fondos de inversión y pensiones</strong>, incluyendo boutiques de gestión de activos alternativos.</li>
          <li><strong>Corredurías de seguros</strong> cuando presten servicios de distribución regulados bajo IDD.</li>
          <li><strong>Fintech reguladas bajo PSD2</strong> (entidades de pago, iniciación de pagos, información sobre cuentas).</li>
          <li><strong>Plataformas de financiación participativa</strong> (crowdlending, crowdfunding) reguladas bajo el Reglamento (UE) 2020/1503.</li>
          <li><strong>Intermediarios de crédito hipotecario</strong> bajo la Ley 5/2019.</li>
        </ul>

        <p>Una fintech de 25 empleados con licencia de entidad de pago tiene la misma obligación que una gran aseguradora. Su tamaño no la exime: su actividad regulada la obliga.</p>

        <h2 id="sector-transporte">3. Transporte y operadores logísticos</h2>
        <p>El sector del transporte tiene un perfil de riesgo específico relacionado con la seguridad operacional, el cumplimiento de tiempos de conducción, la gestión de mercancías peligrosas y el fraude en el transporte internacional. La normativa UE de seguridad del transporte es uno de los ejes expresos de la Directiva Whistleblowing.</p>

        <ul>
          <li><strong>Operadores de transporte aéreo</strong> certificados bajo normativa EASA (JAR/CS, Reg. 965/2012).</li>
          <li><strong>Navieras y operadores de transporte marítimo</strong> bajo el Convenio SOLAS y los Reglamentos UE de seguridad marítima.</li>
          <li><strong>Empresas ferroviarias y administradores de infraestructuras</strong> bajo la Directiva de Seguridad Ferroviaria (Directiva 2016/798).</li>
          <li><strong>Transportistas por carretera</strong> que operen en el ámbito regulado por el Reglamento (CE) 1071/2009 y las normativas de transporte de mercancías peligrosas (ADR).</li>
          <li><strong>Operadores de puertos y terminales logísticas</strong> sujetos a normativa de seguridad de instalaciones portuarias (ISPS/PBIP).</li>
        </ul>

        <h2 id="sector-medioambiente">4. Medioambiente y actividades industriales</h2>
        <p>Las empresas con actividades de impacto ambiental —que deben obtener autorizaciones o inscribirse en registros regulatorios— pueden estar en el ámbito de la Directiva Whistleblowing. No hace falta ser una gran industria: basta con tener una actividad regulada.</p>

        <ul>
          <li><strong>Gestores de residuos</strong> (recogida, transporte, tratamiento, valorización o eliminación) con autorización de la comunidad autónoma.</li>
          <li><strong>Empresas de tratamiento de aguas residuales</strong> industriales o urbanas.</li>
          <li><strong>Instalaciones sujetas a Autorización Ambiental Integrada (AAI)</strong> conforme a la Ley 16/2002 y la Directiva de Emisiones Industriales.</li>
          <li><strong>Operadores de instalaciones SEVESO</strong> (sustancias peligrosas en umbrales superiores o inferiores según la Directiva 2012/18/UE).</li>
          <li><strong>Empresas de tratamiento de suelos contaminados</strong> y gestión de residuos especiales (aceites usados, pilas, PCB, etc.).</li>
          <li><strong>Productores y gestores de residuos de aparatos eléctricos y electrónicos (RAEE)</strong>.</li>
        </ul>

        <div style="background:rgba(239,68,68,0.06);border:1px solid rgba(239,68,68,0.2);border-radius:var(--radius-lg);padding:1.5rem;margin:2rem 0;">
          <p style="font-weight:600;margin:0 0 0.5rem;">Ejemplo real:</p>
          <p style="margin:0;color:var(--text-secondary);">Una empresa de 18 empleados que recoge y gestiona residuos de construcción tiene autorización de la comunidad autónoma para operar como gestor de residuos. Su actividad está regulada por normativa ambiental europea. La Directiva Whistleblowing le aplica: está obligada a tener canal de denuncias aunque tenga 18 trabajadores.</p>
        </div>

        <h2 id="fondos-publicos">5. Entidades que reciben o gestionan fondos públicos</h2>
        <p>La Ley 2/2023 incluye expresamente entre los obligados a partidos políticos, sindicatos, organizaciones empresariales y fundaciones vinculadas a ellos que reciban o gestionen fondos públicos. Pero el perímetro puede ser más amplio.</p>

        <ul>
          <li><strong>Partidos políticos</strong> con financiación pública (subvenciones electorales, subvenciones de actividad ordinaria).</li>
          <li><strong>Sindicatos y organizaciones empresariales</strong> que reciban fondos del presupuesto público.</li>
          <li><strong>Fundaciones vinculadas a partidos, sindicatos o asociaciones empresariales</strong> con financiación pública.</li>
          <li><strong>Entidades privadas concesionarias de servicios públicos</strong> (limpieza viaria, transporte público concesionado, gestión de servicios sociales) que estén bajo el control efectivo de una administración.</li>
          <li><strong>Entidades privadas que gestionen fondos europeos</strong> (FEDER, FSE, FEADER) como organismos intermedios o beneficiarios principales.</li>
        </ul>

