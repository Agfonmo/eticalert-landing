        <h2 id="riesgo-aislamiento">El riesgo que nadie menciona: el aislamiento del canal</h2>
        <p>La <a href="/blog/ley-2-2023-canal-de-denuncias">Ley 2/2023</a> exige que el acceso a las denuncias esté estrictamente limitado al personal autorizado para gestionar el sistema. Cuando el canal está integrado en la misma plataforma donde el equipo de RRHH gestiona nóminas, vacaciones y desempeño, surge una pregunta legítima: <strong>¿quién más tiene acceso técnico a los datos del canal?</strong></p>
        <p>Los administradores globales de Factorial, los técnicos de RRHH con acceso amplio a la plataforma, y los propios empleados de soporte de Factorial pueden potencialmente acceder a zonas adyacentes a los datos del canal. Demostrar el aislamiento estricto ante una inspección de la AIPI es más complejo cuando el canal comparte plataforma con el resto de la gestión de personal.</p>
        <p>EticAlert es un sistema dedicado exclusivamente al canal de denuncias, con acceso restringido por diseño y auditable por separado.</p>

