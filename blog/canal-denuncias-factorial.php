          <li>Ya pagas Factorial y el canal de denuncias va incluido sin coste adicional.</li>
          <li>Tu empresa tiene procesos de RRHH bien establecidos en Factorial y quieres minimizar el número de herramientas.</li>
          <li>La sensibilidad de los casos esperados es baja y los gestores de RRHH son las personas apropiadas para gestionar el canal.</li>
        </ul>

