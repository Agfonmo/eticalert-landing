        <h2 id="que-incluye-factorial">¿Qué incluye el canal de denuncias de Factorial?</h2>
        <p>Factorial incluye su módulo de canal de denuncias en los planes de RRHH desde el plan básico. Según la información pública disponible, el módulo permite:</p>
        <ul>
          <li>Comunicaciones anónimas a través del canal integrado en el portal de RRHH.</li>
          <li>Asignación de responsables para gestionar cada denuncia.</li>
          <li>Seguimiento del estado de la denuncia desde el panel de administración.</li>
          <li>Datos almacenados en la UE con certificaciones ISO.</li>
          <li>Integración con el resto de módulos de Factorial: control horario, nómina, gastos, etc.</li>
        </ul>
        <p>Para quien ya usa Factorial como HRIS principal, el atractivo está claro: no hay que contratar ni aprender otra herramienta. El canal de denuncias aparece en el mismo panel donde ya se gestiona el equipo.</p>

