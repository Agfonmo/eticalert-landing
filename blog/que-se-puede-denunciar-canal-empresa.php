        <h1>¿Qué puede denunciar un empleado a través del canal de denuncias?</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a febrero 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-02-19">19 de febrero de 2026</time> por el equipo de EticAlert</p>

        <p>Una de las dudas más frecuentes entre empresas y empleados es qué puede denunciarse realmente a través del canal interno de denuncias y qué queda fuera de su ámbito. La respuesta es más amplia de lo que muchos piensan, pero también tiene límites claros. Este artículo lo explica con ejemplos concretos.</p>

        <h2 id="infracciones-cubiertas">¿Qué infracciones cubre la Ley 2/2023?</h2>
        <p>La Ley 2/2023 transpone la Directiva europea de whistleblowing pero va más allá: mientras la directiva cubría solo infracciones del Derecho de la UE, la ley española amplió el ámbito a <strong>infracciones del Derecho español en general</strong>. Esto significa que el canal puede recibir comunicaciones sobre una gama muy amplia de conductas.</p>
        <p>Las principales categorías de infracciones cubiertas son:</p>

        <p><strong>Contratación pública y subvenciones:</strong> irregularidades en procesos de licitación, adjudicaciones indebidas, uso fraudulento de subvenciones públicas, favoritismos en concursos.</p>

        <p><strong>Servicios financieros y mercados de valores:</strong> fraudes contables, manipulación de mercados, información privilegiada, blanqueo de capitales, irregularidades en la gestión de fondos de inversión o en seguros.</p>

        <p><strong>Seguridad de productos y seguridad alimentaria:</strong> comercialización de productos que no cumplen estándares de seguridad, adulteración de alimentos, ocultación de defectos en maquinaria o equipos.</p>

        <p><strong>Protección del medioambiente:</strong> vertidos ilegales, gestión irregular de residuos, emisiones contaminantes no declaradas, destrucción de espacios protegidos.</p>

        <p><strong>Protección de datos y privacidad:</strong> brechas de seguridad no notificadas, tratamiento ilegal de datos personales, venta de datos a terceros sin consentimiento.</p>

        <p><strong>Hacienda y tributación:</strong> evasión fiscal, facturas falsas, sociedades pantalla, operaciones en negro para evitar impuestos.</p>

        <p><strong>Infracciones penales:</strong> cualquier conducta tipificada como delito en el Código Penal: estafa, apropiación indebida, cohecho, tráfico de influencias, falsificación documental, delitos contra los trabajadores.</p>

        <p><strong>Seguridad laboral:</strong> incumplimiento de normas de prevención de riesgos laborales, accidentes no declarados, condiciones de trabajo ilegales.</p>

        <p><strong>Derecho de la competencia:</strong> prácticas colusorias, abuso de posición dominante, acuerdos de fijación de precios.</p>

        <h2 id="quejas-laborales">¿Y las quejas laborales? La diferencia importante</h2>
        <p>Este punto es el que más confusión genera. Las <strong>quejas laborales ordinarias</strong> —discrepancias con el jefe, disconformidad con un aumento de sueldo, conflictos entre compañeros por reparto de trabajo— <strong>no son, en principio, objeto del canal de denuncias</strong> de la Ley 2/2023.</p>
        <p>El canal de denuncias está pensado para infracciones normativas o conductas que afectan al interés general, no para resolver conflictos laborales individuales. Estos últimos tienen otros cauces: el servicio de mediación de RRHH, el sindicato o la Inspección de Trabajo.</p>
        <p>Sin embargo, hay zonas grises importantes:</p>
        <ul>
          <li>El <strong>acoso laboral o sexual</strong> sí puede denunciarse por el canal, porque implica una infracción normativa y a menudo conducta tipificada penalmente.</li>
          <li>La <strong>discriminación por razón de sexo, origen, orientación sexual, religión o discapacidad</strong> también es denunciable, al implicar una infracción de normas antidiscriminación.</li>
          <li>Los <strong>incumplimientos graves de las condiciones laborales</strong> (trabajo no declarado, horas no compensadas, incumplimiento del convenio) pueden tener componente infractor que los hace denunciables.</li>
        </ul>
        <p>Una buena práctica es que la política del SII defina claramente qué entra y qué no en el canal, y que exista un mecanismo alternativo claro para las quejas laborales que no sean infracciones normativas.</p>

        <h2 id="quien-puede-denunciar">¿Quién puede denunciar?</h2>
        <p>La Ley 2/2023 tiene un ámbito subjetivo muy amplio. No es un canal solo para empleados. Puede denunciar cualquier persona que haya conocido la información en un contexto laboral o profesional con la organización:</p>
        <ul>
          <li><strong>Empleados actuales</strong> (cualquier categoría: directivos, mandos intermedios, operarios, becarios).</li>
          <li><strong>Exempleados</strong>: personas que trabajaron en la empresa y conocieron los hechos durante su relación laboral.</li>
          <li><strong>Candidatos</strong>: personas en proceso de selección que hayan descubierto algo durante ese proceso.</li>
          <li><strong>Autónomos y trabajadores de empresas contratistas</strong> que presten servicios en la organización.</li>
          <li><strong>Proveedores y distribuidores</strong>.</li>
          <li><strong>Clientes</strong> que hayan tenido conocimiento de la infracción en el marco de su relación comercial.</li>
          <li><strong>Accionistas y socios</strong>.</li>
          <li><strong>Miembros del consejo de administración o de órganos equivalentes</strong>.</li>
          <li><strong>Voluntarios y trabajadores en prácticas</strong>.</li>
        </ul>

        <h2 id="pruebas">¿Se necesitan pruebas para denunciar?</h2>
        <p>No. La ley solo exige que el denunciante tenga <strong>indicios razonables de que los hechos son ciertos</strong> en el momento de la comunicación. No hace falta tener documentación completa, testigos o certeza absoluta.</p>
        <p>Esto es intencional: si se exigiera prueba plena para denunciar, el canal perdería toda su utilidad. En la práctica, el informante suele ser alguien con acceso limitado a la información completa. La investigación posterior es el mecanismo para determinar la veracidad de los hechos.</p>
        <p>Aportar documentación o evidencias disponibles es siempre útil y acelera la investigación, pero no es requisito para que la denuncia sea admitida.</p>

        <h2 id="mala-fe">Denuncias de mala fe</h2>
        <p>La protección de la Ley 2/2023 tiene una condición fundamental: <strong>la denuncia debe hacerse de buena fe</strong>. Quien denuncia sabiendo que los hechos son falsos, o con el único objetivo de perjudicar a otro, no está protegido por la ley.</p>
        <p>Las consecuencias de una denuncia de mala fe pueden incluir medidas disciplinarias, responsabilidad civil por daños al denunciado y, en casos graves, responsabilidad penal (calumnia, art. 205 del Código Penal, o acusación y denuncia falsa, art. 456 CP).</p>

        <h2 id="ejemplos">Ejemplos concretos de infracciones denunciables</h2>
        <p>Para que quede claro en la práctica, estos son algunos ejemplos concretos de lo que sí puede denunciarse:</p>
        <ul>
          <li>El director de compras acepta comisiones de un proveedor a cambio de adjudicarle contratos.</li>
          <li>La empresa vierte residuos industriales en un cauce fluvial sin tratamiento.</li>
          <li>Un directivo acosa sexualmente a una subordinada.</li>
          <li>Se falsifican facturas para inflar gastos y reducir la base imponible del Impuesto de Sociedades.</li>
          <li>Se comercializa un producto con un defecto de seguridad conocido sin retirar el stock.</li>
          <li>La empresa tiene empleados en situación irregular (sin contrato, sin alta en Seguridad Social).</li>
          <li>Se comparte con un competidor información sobre precios antes de una licitación pública.</li>
          <li>Un contable desvía fondos de la empresa a cuentas personales.</li>
          <li>Se oculta un accidente laboral grave para no notificarlo a la autoridad laboral.</li>
          <li>Se accede y vende la base de datos de clientes a un tercero sin consentimiento.</li>
        </ul>

