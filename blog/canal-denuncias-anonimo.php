        <h1>Canal de denuncias anónimo: ¿es realmente anónimo? Qué ve el gestor y qué no</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a junio 2026 · 9 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-06-02">2 de junio de 2026</time> por el equipo de EticAlert</p>

        <p>La primera pregunta que se hace cualquier empleado antes de usar el canal de denuncias de su empresa es siempre la misma: <strong>¿me van a identificar?</strong> Es una pregunta legítima. Y la respuesta depende, en gran parte, de si el canal está bien construido o no. En este artículo explicamos qué significa el anonimato real en un canal homologado, qué datos ve el gestor, qué datos no ve y qué garantías técnicas exige la Ley 2/2023 para proteger al informante.</p>

        <h2 id="anonimo-vs-confidencial">Anónimo vs. confidencial: una distinción clave</h2>

        <p>La <a href="/blog/ley-2-2023-canal-de-denuncias" style="color:var(--accent);">Ley 2/2023</a> reconoce dos modalidades de comunicación:</p>

        <ul>
          <li><strong>Denuncia anónima.</strong> El informante no revela su identidad en ningún momento. El gestor responsable del canal (RSII) nunca sabe quién ha enviado la denuncia. Se tramita igual que una denuncia identificada.</li>
          <li><strong>Denuncia confidencial.</strong> El informante facilita su nombre o datos de contacto, pero el gestor está obligado por ley a mantenerlos en secreto. No puede compartirlos con nadie sin el consentimiento expreso del denunciante, y está prohibido usarlos para identificar o presionar al informante.</li>
        </ul>

        <p>Un canal homologado debe permitir ambas modalidades. Si el sistema solo admite denuncias identificadas — con nombre y email obligatorios —, incumple los requisitos técnicos de la AIPI y puede acarrear <a href="/blog/aipi-sanciones-canal-denuncias" style="color:var(--accent);">sanciones de hasta 600.000€</a>.</p>

        <h2 id="que-ve-el-gestor">¿Qué ve el gestor cuando llega una denuncia anónima?</h2>

        <p>En un canal técnicamente correcto, el RSII (Responsable del Sistema Interno de Información) ve exclusivamente:</p>

        <ul>
          <li>El texto de la denuncia.</li>
          <li>Los archivos adjuntos que haya incluido el informante.</li>
          <li>La categoría de la denuncia (acoso, fraude, corrupción, etc.).</li>
          <li>La fecha y hora de recepción.</li>
          <li>Un <strong>código de seguimiento único</strong> que el informante puede usar para hacer seguimiento o responder sin identificarse.</li>
        </ul>

        <p>Lo que el gestor <strong>no</strong> ve (y no debe ver):</p>

        <ul>
          <li>Nombre ni apellidos del denunciante.</li>
          <li>Correo electrónico ni teléfono.</li>
          <li>Dirección IP desde la que se envió la denuncia.</li>
          <li>Metadatos del dispositivo o del navegador.</li>
          <li>Geolocalización.</li>
        </ul>

        <p>EticAlert no registra la IP del informante en ningún punto del proceso. El código de seguimiento que se entrega al denunciante es generado de forma aleatoria y no está vinculado a ningún dato personal en la base de datos.</p>

        <h2 id="codigo-seguimiento">El código de seguimiento: cómo funciona el anonimato en la comunicación posterior</h2>

        <p>Uno de los retos técnicos más importantes en un canal anónimo es permitir la comunicación bidireccional entre el gestor y el informante sin comprometer el anonimato. La solución es el código de seguimiento.</p>

        <p>Así funciona:</p>

        <ol>
          <li>El informante envía la denuncia. El sistema genera automáticamente un <strong>código alfanumérico único</strong> (por ejemplo, <code>ETA-7X2K9M</code>) y se lo muestra en pantalla al finalizar el envío.</li>
          <li>El informante guarda ese código. Es el único identificador de su denuncia — sin él, no puede acceder al estado ni responder mensajes.</li>
          <li>Cuando el gestor quiere pedir más información, envía un mensaje cifrado a través del panel. El informante puede leerlo introduciendo su código en el canal público, sin necesidad de crear cuenta ni revelar su identidad.</li>
          <li>El informante puede responder al gestor de la misma forma: usando el código, sin nombre, sin email, sin rastreo.</li>
        </ol>

        <p>Este sistema garantiza que la comunicación es real y útil para la investigación, sin comprometer el anonimato. El gestor nunca sabe quién está al otro lado del código.</p>

