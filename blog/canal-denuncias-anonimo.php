        <h2 id="excluir-gestor">Una protección adicional: excluir al gestor con conflicto de interés</h2>

        <p>Un escenario habitual es que el informante quiera denunciar a alguien que tiene acceso al propio canal — por ejemplo, al director de RRHH que es también el RSII, o a un socio de la firma que tiene acceso al panel. En ese caso, la denuncia podría llegar a manos de la persona denunciada.</p>

        <p>EticAlert permite al informante, desde el propio formulario público de denuncia, <strong>excluir a gestores específicos</strong> por conflicto de interés. Es una funcionalidad que no existe en la mayoría de los canales del mercado. La denuncia se enruta automáticamente a un gestor sin conflicto, sin necesidad de intervención manual ni de revelar por qué se excluye a alguien.</p>

        <h2 id="hash-verificable">Integridad verificable: el hash SHA-256</h2>

        <p>Además del anonimato, un canal homologado debe garantizar que el contenido de la denuncia no ha sido alterado desde su envío. EticAlert genera un <strong>hash SHA-256 verificable públicamente</strong> de cada denuncia en el momento de su recepción. Cualquier parte interesada puede verificar en <code>/verificar</code> que el contenido no ha sido modificado a posteriori.</p>

        <p>Esta funcionalidad es especialmente relevante en disputas legales en las que la empresa necesita demostrar que la denuncia fue tramitada tal y como se recibió.</p>

        <h2 id="represalias">¿Qué pasa si la empresa toma represalias?</h2>

        <p>La Ley 2/2023 prohíbe expresamente cualquier represalia contra el informante. Según el artículo 36, se consideran represalias:</p>

        <ul>
          <li>Despido o no renovación del contrato.</li>
          <li>Degradación o denegación de ascensos.</li>
          <li>Cambio de puesto, horario o lugar de trabajo sin justificación objetiva.</li>
          <li>Evaluaciones negativas o referencias laborales perjudiciales.</li>
          <li>Divulgación de la identidad del informante sin su consentimiento.</li>
          <li>Cualquier trato discriminatorio, desfavorable u hostil.</li>
        </ul>

        <p>El informante que sufra represalias puede denunciarlo ante la <a href="/blog/aipi-sanciones-canal-denuncias" style="color:var(--accent);">AIPI</a>, que puede imponer multas de hasta 1.000.000€ a la empresa. Además, existe una inversión de la carga de la prueba: si la empresa toma una medida perjudicial contra el informante, es la empresa quien debe demostrar que esa medida no es una represalia.</p>

        <h2 id="canal-mal-configurado">Señales de que el canal de tu empresa no garantiza anonimato real</h2>

        <p>Si tu empresa tiene un canal de denuncias pero no estás seguro de que sea realmente anónimo, fíjate en estas señales de alerta:</p>

        <ul>
          <li><strong>El formulario exige nombre o email.</strong> Si es obligatorio identificarse para enviar, el canal no admite denuncias anónimas y no cumple la ley.</li>
          <li><strong>No existe código de seguimiento.</strong> Sin código, el informante no puede comunicarse con el gestor sin identificarse — se rompe la cadena del anonimato.</li>
          <li><strong>El canal es un buzón de correo.</strong> Los emails llevan metadatos (IP, cliente de correo, a veces nombre real). No son anónimos por defecto.</li>
          <li><strong>El canal es un formulario de Google Forms o similar.</strong> Google registra datos de sesión y puede vincular el envío a una cuenta de Google si el usuario está logueado.</li>
          <li><strong>El proveedor no especifica dónde están los servidores.</strong> Si los datos están fuera de la UE, el RGPD puede no aplicarse y el control sobre los datos es menor.</li>
        </ul>

