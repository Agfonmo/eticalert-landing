        <h2 id="diferenciadores-eticalert">Las tres funcionalidades de EticAlert que marcan la diferencia</h2>

        <p><strong>1. El informante controla quién accede a su expediente</strong><br>
        En el formulario público, el informante puede indicar qué gestores internos no deben ver su caso — por ejemplo, si el denunciado es el propio director de RRHH. Esto no lo configura el administrador a posteriori: lo decide el propio informante en el momento de denunciar. La Ley 2/2023 exige que el canal garantice la gestión imparcial; esta funcionalidad lo hace posible sin depender de la buena voluntad interna.</p>

        <p><strong>2. Cada expediente cerrado es verificable con hash SHA-256</strong><br>
        Al cerrar un caso, EticAlert genera un hash criptográfico del expediente completo que puede verificarse públicamente en <code>/verificar</code> sin necesidad de acceder al panel. Ante una inspección de la AIPI o un procedimiento judicial, puedes demostrar que el expediente no ha sido modificado desde el cierre. Esto es integridad documental certificable, no solo un log interno.</p>

        <p><strong>3. El canal no se puede publicar si no cumple los requisitos legales</strong><br>
        Si no hay RSII nombrado, si la evidencia de nombramiento ha caducado, o si el plan contratado no cubre el número de empleados declarado, EticAlert bloquea la publicación del canal. No es un aviso — es un bloqueo real. Así se evita el escenario en que una empresa cree que cumple la ley pero su canal no está operativo.</p>

