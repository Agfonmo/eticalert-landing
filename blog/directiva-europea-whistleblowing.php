        <h1>Directiva europea 2019/1937: el origen del canal de denuncias obligatorio en España</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a marzo 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-03-08">8 de marzo de 2026</time> por el equipo de EticAlert</p>

        <p>Si tu empresa tiene un canal de denuncias, o está planteándose tenerlo, el origen de toda esa obligación está en Bruselas. La <strong>Directiva (UE) 2019/1937 del Parlamento Europeo y del Consejo</strong>, conocida como la Directiva Whistleblowing, marcó un antes y un después en la protección de los denunciantes y en la obligación de las empresas de crear canales internos de comunicación. España la transpuso tres años después con la Ley 2/2023. En este artículo explicamos de dónde viene todo, qué exige la directiva original y cómo se tradujo al derecho español.</p>

        <h2 id="que-es">¿Qué es la Directiva 2019/1937?</h2>
        <p>La Directiva (UE) 2019/1937, de 23 de octubre de 2019, es una norma de la Unión Europea que establece un marco mínimo de protección para las personas que comunican infracciones del Derecho de la Unión que han conocido en un contexto laboral. Su nombre oficial en español es <em>Directiva relativa a la protección de las personas que informen sobre infracciones del Derecho de la Unión</em>.</p>
        <p>Antes de esta directiva, no existía en Europa un estándar común de protección para los llamados <em>whistleblowers</em> (denunciantes de irregularidades). Cada Estado miembro tenía —o no tenía— su propia normativa, y las personas que comunicaban irregularidades a menudo sufrían represalias laborales sin mecanismos de defensa claros. La directiva vino a cambiar eso.</p>
        <p>La norma es vinculante: todos los Estados miembros de la UE estaban obligados a transponerla a su ordenamiento jurídico antes del <strong>17 de diciembre de 2021</strong>. España incumplió ese plazo y aprobó la Ley 2/2023 con más de un año de retraso.</p>

        <h2 id="por-que">¿Por qué se creó esta directiva?</h2>
        <p>La Comisión Europea identificó que en muchos Estados miembros las personas que comunicaban irregularidades no contaban con protección efectiva. Los estudios previos a la directiva mostraban que el miedo a las represalias (despido, degradación, acoso, litigios) disuadía a muchos potenciales denunciantes de actuar, lo que permitía que ilegalidades graves —corrupción, evasión fiscal, fraude, daños medioambientales— continuaran sin ser detectadas.</p>
        <p>El objetivo fue, por tanto, doble: proteger a las personas que informan y, al mismo tiempo, crear un mecanismo estructural que permitiera a las organizaciones detectar y corregir sus propios problemas antes de que llegaran a las autoridades externas.</p>
        <p>Casos sonados como los de Luxleaks, Panama Papers o Cambridge Analytica demostraron que los denunciantes habían sido clave para sacar a la luz esas irregularidades, pero que a menudo lo pagaron con su carrera profesional o incluso con procesos penales en su contra. La directiva reconoció explícitamente que el interés público en la información que aportaban justificaba una protección especial.</p>

        <h2 id="que-exige">¿Qué exige la directiva?</h2>
        <p>La Directiva 2019/1937 establece una serie de obligaciones que se dividen en dos grandes bloques: las que afectan a las organizaciones (canales internos) y las que afectan a los Estados (canales externos y protección legal).</p>
        <p><strong>Para las organizaciones privadas y públicas con 50 o más trabajadores:</strong></p>
        <ul>
          <li>Establecer canales de comunicación interna seguros, confidenciales y accesibles.</li>
          <li>Garantizar que los canales permiten comunicaciones tanto escritas como verbales.</li>
          <li>Designar a una persona o departamento imparcial, independiente y competente para gestionar las comunicaciones recibidas.</li>
          <li>Acusar recibo de cada comunicación en un plazo máximo de 7 días.</li>
          <li>Proporcionar información al denunciante sobre las medidas adoptadas en un plazo razonable (no superior a 3 meses).</li>
          <li>Proteger la identidad del denunciante de forma rigurosa; la revelación no autorizada está expresamente prohibida.</li>
          <li>Posibilitar las denuncias anónimas, aunque los Estados miembros no están obligados a garantizar su seguimiento si el informante no facilita ningún medio de contacto.</li>
        </ul>
        <p><strong>Para los Estados miembros:</strong></p>
        <ul>
          <li>Crear canales externos de denuncia (autoridades competentes) con las mismas garantías de confidencialidad y seguimiento.</li>
          <li>Prohibir cualquier forma de represalia contra el denunciante: despido, suspensión, degradación, discriminación, acoso, daño reputacional, revelación de identidad, entre otras.</li>
          <li>Establecer la inversión de la carga de la prueba: si el denunciante sufre un perjuicio, se presume que es una represalia y corresponde a la organización demostrar lo contrario.</li>
          <li>Prever sanciones efectivas, proporcionadas y disuasorias tanto para las represalias como para el incumplimiento de las obligaciones formales.</li>
        </ul>

        <div style="border-left:3px solid var(--accent);background:var(--bg-tertiary);padding:1.25rem 1.5rem;border-radius:0 8px 8px 0;margin:2rem 0;">
          <strong style="color:var(--accent);">Importante:</strong> La directiva establece un suelo mínimo de protección. Los Estados miembros pueden —y deben— ampliar este marco si su legislación nacional ofrece mayores garantías. Esto es exactamente lo que España hizo en algunos aspectos con la Ley 2/2023.
        </div>

        <h2 id="transposicion">¿Cómo la transpuso España?</h2>
        <p>España aprobó la <strong>Ley 2/2023, de 20 de febrero, reguladora de la protección de las personas que informen sobre infracciones normativas y de lucha contra la corrupción</strong>. Fue publicada en el BOE el 21 de febrero de 2023 con un retraso de más de 14 meses respecto al plazo marcado por la directiva.</p>
        <p>La transposición española no se limitó a copiar la directiva. La Ley 2/2023 amplió el ámbito de aplicación en algunos aspectos importantes. Mientras la directiva solo cubre infracciones del Derecho de la Unión Europea, la ley española extiende la protección a infracciones del derecho nacional en general, lo que amplía considerablemente el número de situaciones que pueden comunicarse por el canal.</p>
        <p>Adicionalmente, la ley española creó la <strong>Autoridad Independiente de Protección del Informante (AIPI)</strong>, organismo público encargado de gestionar el canal externo y de ejercer la potestad sancionadora sobre las organizaciones que incumplan la normativa. La AIPI inició su actividad plena en 2026.</p>

