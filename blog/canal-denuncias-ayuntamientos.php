        <h1>Canal de denuncias para ayuntamientos y entidades del sector público</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a febrero 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-02-05">5 de febrero de 2026</time> por el equipo de EticAlert</p>

        <p>La Ley 2/2023 no se aplica solo al sector privado. El sector público tiene sus propias obligaciones y, en algunos aspectos, requisitos más estrictos. Los ayuntamientos, en particular, tienen un umbral diferente al de las empresas privadas: la obligación no depende del número de empleados sino del <strong>número de habitantes del municipio</strong>. Este artículo explica qué municipios están obligados, qué requisitos deben cumplir y cuáles son las infracciones más frecuentes en el sector.</p>

        <h2 id="que-ayuntamientos">¿Qué ayuntamientos están obligados?</h2>
        <p>La Ley 2/2023 establece un criterio poblacional para los municipios, diferente al criterio de empleados que se aplica a las empresas privadas:</p>
        <ul>
          <li><strong>Municipios de más de 10.000 habitantes:</strong> obligados a disponer de canal de denuncias propio desde el 13 de junio de 2023 (primera fecha de cumplimiento obligatorio).</li>
          <li><strong>Municipios de menos de 10.000 habitantes:</strong> no están obligados a tener canal propio, pero pueden (y se recomienda que lo hagan) adherirse al canal de la diputación provincial correspondiente o de la mancomunidad o comarca a la que pertenezcan. La diputación actúa como canal compartido para los municipios menores.</li>
          <li><strong>Todas las entidades del sector público con independencia del tamaño:</strong> organismos públicos, agencias estatales, universidades públicas, empresas públicas y sociedades de economía mixta con participación pública mayoritaria.</li>
        </ul>
        <p>En España hay aproximadamente 1.200 municipios que superan el umbral de 10.000 habitantes. La mayoría de ellos tienen la obligación desde mediados de 2023, aunque el nivel de cumplimiento en el sector público local es, en general, inferior al del sector privado.</p>

        <h2 id="requisitos-adicionales">Requisitos adicionales del sector público</h2>
        <p>Además de los requisitos que se aplican a cualquier entidad obligada (canal técnico, RSII, libro-registro, política del SII), el sector público tiene algunos elementos adicionales:</p>
        <ul>
          <li><strong>Mayor visibilidad pública:</strong> el canal de denuncias de un ayuntamiento debe ser especialmente accesible y visible, dado que no solo los empleados municipales pueden denunciar, sino también los ciudadanos que tengan conocimiento de irregularidades en el ejercicio de la función pública.</li>
          <li><strong>Articulación con el control interno:</strong> los ayuntamientos tienen órganos de control interno (Intervención, Secretaría) que deben coordinarse con el RSII del canal de denuncias para evitar duplicidades y garantizar la coherencia del sistema.</li>
          <li><strong>Coordinación con la autoridad autonómica competente:</strong> algunas comunidades autónomas (Cataluña con la ACDI, Castilla y León, Navarra, etc.) tienen su propia autoridad de protección del informante, complementaria a la AIPI estatal. Los ayuntamientos de esas regiones deben atender también a la normativa autonómica.</li>
          <li><strong>Transparencia activa:</strong> la publicación del canal en la sede electrónica del ayuntamiento y en el portal de transparencia es obligatoria.</li>
        </ul>

        <h2 id="infracciones-denunciables">Infracciones denunciables en un ayuntamiento</h2>
        <p>Las infracciones que pueden comunicarse a través del canal de un ayuntamiento son las propias del sector público local:</p>
        <ul>
          <li><strong>Malversación de fondos públicos:</strong> uso de recursos municipales para fines privados o no autorizados.</li>
          <li><strong>Nepotismo y clientelismo:</strong> contratación de personal por relaciones personales o políticas al margen de los procesos de selección reglamentarios.</li>
          <li><strong>Conflictos de interés:</strong> cargos o funcionarios que participan en decisiones en las que tienen interés personal o familiar.</li>
          <li><strong>Irregularidades en licencias urbanísticas:</strong> concesión de licencias indebidas, prevaricación urbanística, trato favorable a promotores con relaciones políticas.</li>
          <li><strong>Licitaciones irregulares:</strong> adjudicaciones a empresas vinculadas a concejales, fraccionamiento de contratos, pliegos elaborados para favorecer a un proveedor concreto.</li>
          <li><strong>Corrupción en la gestión de subvenciones:</strong> justificación falsa de subvenciones, uso de fondos para fines distintos a los concedidos.</li>
          <li><strong>Acoso laboral o sexual en el entorno municipal:</strong> conductas de funcionarios o cargos contra compañeros o subordinados.</li>
        </ul>

        <h2 id="quien-puede-denunciar">¿Quién puede denunciar en un ayuntamiento?</h2>
        <p>El ámbito subjetivo del canal de un ayuntamiento es especialmente amplio:</p>
        <ul>
          <li>Funcionarios de carrera y personal laboral del ayuntamiento.</li>
          <li>Personal eventual y de confianza.</li>
          <li>Exempleados que conocieron los hechos durante su relación de servicio.</li>
          <li>Contratistas y proveedores del ayuntamiento.</li>
          <li>Ciudadanos que, en su relación con los servicios municipales, hayan tenido conocimiento de irregularidades.</li>
          <li>Representantes vecinales y miembros de asociaciones con relación con el ayuntamiento.</li>
        </ul>
        <p>Este último punto es especialmente relevante: <strong>los ciudadanos pueden denunciar irregularidades</strong> a través del canal del ayuntamiento. Esto lo diferencia del canal de una empresa privada, que está pensado fundamentalmente para personas con relación laboral o comercial.</p>

        <div style="border-left:3px solid var(--accent);background:var(--bg-tertiary);padding:1.25rem 1.5rem;border-radius:0 8px 8px 0;margin:2rem 0;">
          <strong style="color:var(--accent);">Importante:</strong> El canal de denuncias del ayuntamiento no sustituye al buzón de quejas y sugerencias ciudadanas. Son instrumentos diferentes: uno para irregularidades normativas, otro para la satisfacción del servicio. No deben confundirse ni fusionarse.
        </div>

        <h2 id="integracion-aipi">Integración con la AIPI y autoridades autonómicas</h2>
        <p>El canal interno del ayuntamiento coexiste con el canal externo de la AIPI (para infracciones del Derecho estatal) y, en su caso, con las autoridades autonómicas de protección del informante. La relación entre estos sistemas es la siguiente:</p>
        <ul>
          <li>El denunciante puede elegir si comunica la infracción a través del canal interno del ayuntamiento o directamente a la AIPI o a la autoridad autonómica.</li>
          <li>El RSII del ayuntamiento puede derivar un caso a la autoridad externa si la gravedad del asunto o la posible implicación del propio RSII lo hacen necesario.</li>
          <li>La AIPI puede inspeccionar el canal del ayuntamiento y sancionar si no cumple los requisitos legales, igual que haría con una empresa privada.</li>
        </ul>

