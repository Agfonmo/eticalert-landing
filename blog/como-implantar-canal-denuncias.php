        <h1>Cómo implantar un canal de denuncias en tu empresa: guía paso a paso</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a marzo 2026 · 10 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el 3 de marzo de 2026 por el equipo de EticAlert</p>

        <p>Implantar un canal de denuncias puede parecer un proceso complejo si no sabes por dónde empezar. En realidad, con el enfoque correcto y la herramienta adecuada, una empresa mediana puede tener su canal operativo y conforme a la Ley 2/2023 en cuestión de días. Esta guía te explica los 10 pasos del proceso, en el orden correcto y con el nivel de detalle necesario para que puedas ejecutarlo sin depender de una consultoría externa.</p>

        <h2 id="paso-1">Paso 1: Verificar si tu empresa está obligada</h2>
        <p>Antes de hacer nada, confirma que tu empresa entra dentro del ámbito de aplicación de la Ley 2/2023. Estás obligado si cumples alguna de estas condiciones:</p>
        <ul>
          <li>Tu empresa tiene <strong>50 o más trabajadores</strong> de media en los últimos 12 meses.</li>
          <li>Tienes <strong>facturación superior a 10 millones de euros</strong> en el último ejercicio, aunque no llegues a 50 empleados.</li>
          <li>Tu organización pertenece al <strong>sector público</strong>, independientemente del tamaño.</li>
          <li>Tu empresa opera en sectores financieros regulados con umbrales especiales (seguros, valores, etc.).</li>
        </ul>
        <p>Si no estás obligado, tener un canal sigue siendo una buena práctica de gestión. Si estás obligado y aún no lo tienes, estás en situación de incumplimiento desde diciembre de 2023. Actúa cuanto antes.</p>

        <h2 id="paso-2">Paso 2: Decisión y compromiso de la dirección</h2>
        <p>El canal de denuncias no es un proyecto de RRHH ni un tema puramente técnico. Es una decisión estratégica que requiere el compromiso del órgano de administración —consejo de administración, administrador único o equivalente. Esta implicación directiva es importante por varias razones:</p>
        <ul>
          <li>La <strong>política del Sistema Interno de Información</strong> debe ser aprobada formalmente por el órgano de administración.</li>
          <li>El <strong>nombramiento del RSII</strong> (paso 3) es responsabilidad del órgano de administración.</li>
          <li>En caso de sanción, la responsabilidad personal recae sobre los administradores.</li>
          <li>La cultura ética de la empresa se transmite desde arriba: si la dirección no está comprometida, los empleados percibirán el canal como un trámite burocrático.</li>
        </ul>
        <p>En esta fase, la dirección debe aprobar la decisión de implantar el canal y asignar recursos (persona responsable interna, presupuesto, plazo).</p>

        <h2 id="paso-3">Paso 3: Designar el RSII (Responsable del Sistema Interno de Información)</h2>
        <p>El Responsable del Sistema Interno de Información es la figura clave del sistema. La Ley 2/2023 exige que sea una persona que reúna estas condiciones:</p>
        <ul>
          <li><strong>Posición independiente e imparcial:</strong> no debe tener conflicto de interés con los asuntos que gestione. Típicamente es el Compliance Officer, el Director de RRHH, el Director Jurídico o el Director Financiero.</li>
          <li><strong>Empleado de la propia empresa</strong> (no puede externalizarse completamente, aunque sí puede apoyarse en asesores externos para la instrucción de casos).</li>
          <li><strong>Con autonomía funcional</strong>: no puede recibir instrucciones del área investigada en un caso concreto.</li>
        </ul>
        <p>Una vez designado, debes notificarlo formalmente a la AIPI a través del formulario oficial disponible en su sede electrónica. El plazo para realizarlo, desde la apertura del formulario en febrero de 2026, es de 2 meses (hasta el 10 de abril de 2026). Consulta nuestra <a href="/blog/rsii-guia-formulario-aipi" style="color:var(--accent);">guía completa sobre el RSII y el formulario AIPI →</a></p>

        <h2 id="paso-4">Paso 4: Elegir la herramienta para el canal</h2>
        <p>Esta es la decisión con mayor impacto en el coste y el tiempo de implantación. Hay tres grandes opciones:</p>

        <p><strong>Opción A: Desarrollo interno o a medida</strong></p>
        <p>Construir el sistema desde cero con recursos técnicos propios o mediante una empresa de desarrollo de software. Ventajas: control total, integración con sistemas internos. Inconvenientes: coste muy elevado (10.000-50.000€ o más), tiempo largo (meses), necesidad de mantenimiento continuo, riesgo de no cumplir todos los requisitos técnicos legales. Solo recomendable para grandes corporaciones con equipo tech potente.</p>

        <p><strong>Opción B: Consultoría especializada en compliance</strong></p>
        <p>Contratar un despacho de abogados o consultora de compliance que proporcione el canal como parte de un servicio integral. Ventajas: acompañamiento experto. Inconvenientes: coste elevado (1.500-5.000€ de implantación más tarifa mensual), proceso lento, dependencia del proveedor de servicios para cambios.</p>

        <p><strong>Opción C: Plataforma SaaS especializada</strong></p>
        <p>Usar un software en la nube diseñado específicamente para el canal de denuncias. Ventajas: activación inmediata (minutos), precio predecible y bajo (desde 19€/mes con EticAlert), requisitos técnicos cubiertos por la plataforma, actualizaciones automáticas ante cambios normativos. Inconvenientes: menor personalización que el desarrollo a medida.</p>
        <p>Para la inmensa mayoría de las pymes obligadas, la opción SaaS es la más sensata por coste, velocidad y nivel de cumplimiento. <strong>EticAlert</strong> está diseñado específicamente para este perfil: activación en 5 minutos, gratis hasta 20 empleados y desde 19€/mes para empresas obligadas.</p>

        <h2 id="paso-5">Paso 5: Configurar el canal</h2>
        <p>Una vez elegida la herramienta, hay que configurarla con los datos de tu empresa. La configuración mínima incluye:</p>
        <ul>
          <li>Nombre y datos de identificación de la empresa.</li>
          <li>Categorías de infracciones denunciables (adaptadas a tu sector y actividad).</li>
          <li>Datos del RSII (nombre, email de notificación, no se muestra al denunciante).</li>
          <li>Configuración del anonimato y sistema de seguimiento anónimo.</li>
          <li>Personalización de la URL del canal (ej: empresa.eticalert.com).</li>
          <li>Configuración de los plazos y recordatorios automáticos (7 días para acuse, 3 meses para respuesta).</li>
        </ul>

        <h2 id="paso-6">Paso 6: Redactar la política del Sistema Interno de Información</h2>
        <p>La política del SII es el documento que describe cómo funciona el canal, qué se puede denunciar, quién lo gestiona y qué protecciones tiene el informante. Es obligatoria y debe ser aprobada por el órgano de administración antes de publicar el canal.</p>
        <p>El contenido mínimo de esta política incluye: objeto y alcance, canales disponibles, procedimiento de tramitación, plazos, datos del RSII, protección del informante, régimen de confidencialidad, tratamiento de datos conforme al RGPD, tipos de infracciones denunciables y consecuencias de las denuncias de mala fe.</p>
        <p>Consulta nuestra <a href="/blog/politica-sistema-interno-informacion" style="color:var(--accent);">guía específica sobre la política del SII →</a></p>

        <h2 id="paso-7">Paso 7: Notificar la designación del RSII a la AIPI</h2>
        <p>Una vez designado el RSII y aprobada la política, debes notificarlo formalmente a la Autoridad Independiente de Protección del Informante (AIPI). Esta notificación se realiza a través del formulario oficial disponible en la sede electrónica de la AIPI y requiere:</p>
        <ul>
          <li>Datos de identificación de la empresa (NIF, razón social, sector).</li>
          <li>Datos del RSII designado (nombre, cargo, fecha de designación).</li>
          <li>URL del canal de denuncias ya operativo.</li>
          <li>Declaración de conformidad con los requisitos técnicos de la ley.</li>
        </ul>

        <div style="border-left:3px solid var(--accent);background:var(--bg-tertiary);padding:1.25rem 1.5rem;border-radius:0 8px 8px 0;margin:2rem 0;">
          <strong style="color:var(--accent);">Plazo crítico:</strong> Si tu empresa tenía la obligación desde 2023 y aún no ha notificado el RSII, el plazo habilitado por la AIPI desde febrero de 2026 vence el 10 de abril de 2026. No notificar es infracción grave (hasta 600.000€).
        </div>

        <h2 id="paso-8">Paso 8: Comunicar el canal a todos los empleados y grupos de interés</h2>
        <p>El canal solo funciona si la gente sabe que existe. La ley exige que sea accesible para empleados, exempleados, candidatos, proveedores, clientes y cualquier persona con relación con la empresa. La comunicación debe realizarse de forma activa:</p>
        <ul>
          <li>Email a toda la plantilla con la URL del canal y una explicación de su funcionamiento.</li>
          <li>Inclusión en el manual de acogida para nuevas incorporaciones.</li>
          <li>Comunicación en los contratos con proveedores y colaboradores.</li>
          <li>Recordatorio periódico (al menos anual) en comunicaciones internas.</li>
        </ul>

        <h2 id="paso-9">Paso 9: Publicar la URL del canal en la web corporativa</h2>
        <p>La ley exige que el canal sea accesible públicamente desde la web de la empresa. Debes añadir un enlace visible al canal en tu página web corporativa, habitualmente en el pie de página o en la sección de "Cumplimiento" o "Compliance". No basta con enviarlo por email a los empleados; debe estar accesible para terceros externos.</p>

        <h2 id="paso-10">Paso 10: Prueba funcional y mantenimiento continuo</h2>
        <p>Antes de dar por finalizado el proceso, realiza una prueba completa del canal:</p>
        <ul>
          <li>Envía una denuncia de prueba (anónima y nominal) y verifica que el RSII la recibe.</li>
          <li>Comprueba que el sistema de seguimiento anónimo funciona correctamente.</li>
          <li>Verifica que los plazos y recordatorios automáticos están activos.</li>
          <li>Confirma que el libro-registro se actualiza automáticamente con cada entrada.</li>
        </ul>
        <p>El mantenimiento continuo incluye: revisar la política del SII al menos una vez al año, actualizar los datos del RSII si cambia la persona designada, notificar cambios a la AIPI y mantener actualizada la configuración del canal.</p>

