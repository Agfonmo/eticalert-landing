        <h2 id="que-es">Qué es la política del sistema interno de información y por qué es obligatoria</h2>

        <p>La <strong>política del sistema interno de información</strong> es el documento que formaliza el funcionamiento del canal de denuncias en tu organización. No es el canal en sí (que es la plataforma tecnológica o el buzón), sino el reglamento interno que describe cómo funciona: quién lo gestiona, cómo se tramitan las comunicaciones, qué plazos se aplican y qué protecciones tienen los informantes.</p>

        <p>La Ley 2/2023 no exige explícitamente que se llame "política del sistema interno de información" — también puede denominarse reglamento del canal de denuncias, protocolo de gestión de comunicaciones internas o procedimiento de tramitación de denuncias. Lo importante es que el documento exista y contenga los elementos que la ley requiere.</p>

        <p>Sin este documento, incluso aunque la empresa tenga un canal de denuncias técnicamente operativo, la AIPI puede considerar que el sistema no está correctamente implantado y aplicar sanciones por <a href="/blog/aipi-sanciones-canal-denuncias" style="color:var(--accent);">incumplimiento formal</a>.</p>

