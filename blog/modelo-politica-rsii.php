        <h2 id="modelo-completo">Modelo completo de política del sistema interno de información (plantilla)</h2>

        <p>El texto siguiente es una plantilla adaptable. Los campos entre corchetes <code>[XXXX]</code> deben completarse con los datos de tu organización. Consúltala con tu asesor legal antes de aprobarla si tu empresa tiene particularidades sectoriales o estructura de grupo.</p>

        <div style="background:var(--bg-secondary);border:1px solid var(--border);border-radius:12px;padding:2rem;margin:2rem 0;font-family:Georgia,serif;line-height:1.8;">

          <p style="text-align:center;font-weight:700;font-size:1.1rem;margin-bottom:0.5rem;">POLÍTICA DEL SISTEMA INTERNO DE INFORMACIÓN</p>
          <p style="text-align:center;color:var(--text-secondary);margin-bottom:2rem;">[NOMBRE DE LA EMPRESA / ENTIDAD] · Versión [1.0] · Aprobada el [DD/MM/AAAA]</p>

          <p><strong>1. OBJETO Y ÁMBITO DE APLICACIÓN</strong></p>
          <p>La presente política regula el Sistema Interno de Información (en adelante, el "Canal") establecido por [NOMBRE DE LA EMPRESA], con CIF [XXXXXXXXX] y domicilio en [DIRECCIÓN COMPLETA], en cumplimiento de la Ley 2/2023, de 20 de febrero, reguladora de la protección de las personas que informen sobre infracciones normativas y de lucha contra la corrupción (en adelante, "Ley 2/2023").</p>

          <p>Pueden utilizar el Canal las siguientes personas:</p>
          <ul>
            <li>Empleados y trabajadores de la organización, con independencia de su tipo de contrato.</li>
            <li>Trabajadores autónomos que presten servicios para la organización.</li>
            <li>Socios, accionistas y miembros de órganos de administración o supervisión.</li>
            <li>Personas en prácticas, becarios y voluntarios.</li>
            <li>Proveedores, contratistas y subcontratistas cuando la infracción se haya conocido en el contexto de la relación contractual.</li>
            <li>Personas cuya relación laboral haya finalizado, cuando la infracción se haya conocido antes de la extinción del vínculo.</li>
          </ul>

          <p><strong>2. MATERIAS OBJETO DE COMUNICACIÓN</strong></p>
          <p>El Canal está habilitado para recibir comunicaciones sobre posibles infracciones de:</p>
          <ol>
            <li>Derecho de la Unión Europea en los ámbitos de contratación pública, servicios financieros, prevención del blanqueo de capitales y financiación del terrorismo, seguridad de los productos, seguridad del transporte, protección del medio ambiente, nuclear, seguridad alimentaria, salud pública, protección de los consumidores, protección de la vida privada y datos personales, y seguridad de las redes y sistemas de información.</li>
            <li>Normas que afecten a los intereses financieros de la Unión Europea.</li>
            <li>Normas del mercado interior, incluidas la competencia y las ayudas estatales.</li>
            <li>La normativa penal, administrativa o laboral española cuando el informante tenga motivos razonables para creer que la infracción se ha producido o es probable que se produzca.</li>
            <li>[OPCIONAL: Normas internas de [NOMBRE DE LA EMPRESA]: código de conducta, política anticorrupción, reglamento interno.]</li>
          </ol>
          <p>El Canal no es adecuado para comunicar conflictos laborales individuales, quejas de clientes sobre productos o servicios, ni reclamaciones civiles entre particulares sin conexión con las materias anteriores.</p>

          <p><strong>3. RESPONSABLE DEL SISTEMA INTERNO DE INFORMACIÓN (RSII)</strong></p>
          <p>El Responsable del Sistema Interno de Información es [NOMBRE Y APELLIDOS / CARGO: por ejemplo, "el Director de Cumplimiento" o "el Responsable de RRHH"]. El RSII es la persona encargada de recibir, acusar recibo, tramitar e investigar las comunicaciones recibidas a través del Canal, así como de mantener el libro-registro y garantizar la confidencialidad del proceso.</p>
          <p>[OPCIONAL PARA EXTERNALIZACIÓN: La gestión del Canal ha sido externalizada a [NOMBRE DEL PROVEEDOR], que actúa como encargado del tratamiento según lo establecido en el contrato de encargo de tratamiento suscrito al efecto. El RSII interno de la organización mantiene la supervisión y responsabilidad última del sistema.]</p>
          <p>En caso de conflicto de interés del RSII con respecto al asunto denunciado, la comunicación será gestionada por [CARGO SUSTITUTO o EMPRESA EXTERNA]. El informante puede excluir al RSII desde el propio formulario de denuncia cuando exista conflicto de interés.</p>

          <p><strong>4. CANALES DE RECEPCIÓN DE COMUNICACIONES</strong></p>
          <p>Las comunicaciones pueden enviarse a través de los siguientes medios:</p>
          <ul>
            <li><strong>Canal digital:</strong> [URL del canal, por ejemplo: https://denuncias.miempresa.com] — disponible las 24 horas, permite el envío anónimo y la comunicación bidireccional mediante código de seguimiento.</li>
            <li>[OPCIONAL] <strong>Correo postal:</strong> [DIRECCIÓN], a la atención del RSII, en sobre cerrado con la indicación "CONFIDENCIAL — RSII".</li>
            <li>[OPCIONAL] <strong>Reunión presencial:</strong> previa solicitud a [EMAIL/TELÉFONO], reservada para casos en que el informante prefiera comunicación verbal.</li>
          </ul>
          <p>Todos los canales garantizan la confidencialidad de la identidad del informante.</p>

          <p><strong>5. PROCEDIMIENTO DE TRAMITACIÓN</strong></p>
          <p><em>5.1 Recepción y acuse de recibo</em></p>
          <p>El RSII acusará recibo de toda comunicación recibida en un plazo máximo de <strong>siete (7) días hábiles</strong> desde su recepción. Si la comunicación ha sido enviada de forma anónima, el acuse de recibo se emitirá a través del canal digital mediante el código de seguimiento facilitado al informante.</p>

          <p><em>5.2 Admisión a trámite</em></p>
          <p>El RSII evaluará la admisibilidad de la comunicación en un plazo razonable. No se admitirán a trámite las comunicaciones que: (a) sean manifiestamente infundadas, (b) no guarden relación con las materias del apartado 2, o (c) constituyan un abuso del Canal. La inadmisión se comunicará al informante con indicación del motivo.</p>

          <p><em>5.3 Investigación</em></p>
          <p>Las comunicaciones admitidas a trámite serán investigadas por el RSII con la diligencia debida. El RSII puede solicitar información adicional al informante a través del canal, sin revelar su identidad a terceros. Cuando la investigación requiera la participación de otras personas de la organización, el RSII adoptará las medidas necesarias para preservar la confidencialidad de la identidad del informante.</p>

          <p><em>5.4 Resolución y comunicación al informante</em></p>
          <p>El RSII comunicará al informante el resultado de la investigación en un plazo máximo de <strong>tres (3) meses</strong> desde el acuse de recibo, plazo que podrá prorrogarse hasta <strong>seis (6) meses</strong> en casos de especial complejidad, con notificación al informante. La comunicación indicará si se han adoptado medidas, sin revelar información que pudiera identificar a las personas investigadas.</p>

          <p><em>5.5 Libro-registro</em></p>
          <p>El RSII mantendrá un libro-registro de todas las comunicaciones recibidas, con indicación de la fecha de recepción, el estado de tramitación y las medidas adoptadas. El libro-registro se conservará durante un mínimo de diez (10) años y estará disponible para la AIPI en caso de inspección.</p>

          <p><strong>6. CONFIDENCIALIDAD Y PROTECCIÓN DE DATOS</strong></p>
          <p>La identidad del informante es estrictamente confidencial. Nadie podrá revelarla sin el consentimiento expreso del informante, salvo que sea estrictamente necesario en el marco de una investigación judicial o administrativa. Esta obligación de confidencialidad se extiende a todas las personas que participen en la tramitación.</p>
          <p>El tratamiento de los datos personales se realiza de conformidad con el Reglamento (UE) 2016/679 (RGPD) y la Ley Orgánica 3/2018 (LOPDGDD). La base jurídica del tratamiento es el cumplimiento de una obligación legal (art. 6.1.c RGPD). Los datos se conservarán durante el tiempo necesario para la tramitación y, como máximo, durante diez años. El interesado puede ejercer sus derechos dirigiéndose a [EMAIL DE PROTECCIÓN DE DATOS].</p>

          <p><strong>7. PROHIBICIÓN DE REPRESALIAS</strong></p>
          <p>[NOMBRE DE LA EMPRESA] prohíbe expresamente cualquier forma de represalia contra las personas que hayan efectuado una comunicación de buena fe a través del Canal. Se consideran represalias, entre otras: el despido, la no renovación del contrato, la degradación, el cambio de puesto o condiciones de trabajo sin justificación objetiva, las evaluaciones negativas injustificadas o cualquier trato desfavorable relacionado con la denuncia.</p>
          <p>Las personas que sufran represalias pueden denunciarlo a la AIPI (Autoridad Independiente de Protección del Informante) o ante los tribunales competentes.</p>

          <p><strong>8. CONSECUENCIAS DE LAS COMUNICACIONES DE MALA FE</strong></p>
          <p>El Canal está concebido para comunicaciones de buena fe. Las comunicaciones realizadas con conocimiento de su falsedad o con finalidad de perjudicar a terceros no estarán amparadas por las protecciones de la Ley 2/2023 y podrán dar lugar a las responsabilidades disciplinarias, civiles o penales que correspondan.</p>

          <p><strong>9. CANAL EXTERNO DE LA AIPI</strong></p>
          <p>Con independencia del Canal interno, cualquier persona puede comunicar infracciones directamente a la Autoridad Independiente de Protección del Informante (AIPI) o a los organismos sectoriales competentes. El uso del Canal interno no es un requisito previo para acceder al canal externo.</p>

          <p><strong>10. APROBACIÓN, VIGENCIA Y REVISIÓN</strong></p>
          <p>La presente política ha sido aprobada por [ÓRGANO DE GOBIERNO: consejo de administración / gerencia / socios administradores] el [DD/MM/AAAA] y entra en vigor en esa misma fecha. Será revisada anualmente o cuando se produzcan cambios normativos o en la estructura de la organización que lo hagan necesario.</p>

          <p style="margin-top:2rem;color:var(--text-muted);font-size:0.875rem;">Versión [1.0] — Aprobada por [CARGO] el [DD/MM/AAAA] — Próxima revisión: [DD/MM/AAAA+1 año]</p>
