        <h2 id="como-aprobar">Cómo aprobar y comunicar la política</h2>

        <p>Una vez redactada la política adaptada a tu empresa, sigue estos pasos para que tenga validez frente a la AIPI:</p>

        <ol>
          <li>
            <p><strong>Aprobación formal por el órgano competente.</strong> En sociedades con consejo de administración, apruébala en acta de reunión del consejo. En pymes con administrador único, basta con un acuerdo firmado por el administrador. Guarda el acta o documento de aprobación con la firma del responsable y la fecha.</p>
          </li>
          <li>
            <p><strong>Comunicación a toda la plantilla.</strong> La ley exige que todos los empleados conozcan el canal y sus protecciones. Lo habitual es enviar un correo corporativo con la política adjunta, publicarla en la intranet y añadir una mención en el manual del empleado o en el contrato de trabajo de los nuevos incorporados.</p>
          </li>
          <li>
            <p><strong>Registro de la comunicación.</strong> Guarda evidencia de que la comunicación se realizó: el email enviado con la lista de destinatarios, o la firma de recepción en el manual del empleado. La AIPI puede solicitar esta evidencia.</p>
          </li>
          <li>
            <p><strong>Activar el canal digital.</strong> Si todavía no tienes el canal técnico, actívalo junto con la aprobación de la política. La URL del canal debe aparecer en el documento. EticAlert permite activar el canal en menos de 5 minutos: <a href="/registro" style="color:var(--accent);">regístrate aquí →</a></p>
          </li>
          <li>
            <p><strong>Notificación a la AIPI (si aplica).</strong> Las entidades obligadas deben notificar a la AIPI la designación del RSII usando el formulario RSII-01. Consulta nuestra <a href="/blog/rsii-guia-formulario-aipi" style="color:var(--accent);">guía sobre el formulario RSII →</a></p>
          </li>
        </ol>

        <h2 id="errores">Los 5 errores más comunes al redactar la política</h2>

        <ol>
          <li><strong>No identificar al RSII con suficiente precisión.</strong> No basta con poner "el departamento de RRHH". La ley exige que la persona o función responsable esté identificada de forma que el informante sepa exactamente a quién va dirigida su denuncia.</li>
          <li><strong>Omitir el ámbito de personas que pueden denunciar.</strong> Muchas políticas solo mencionan a empleados en activo y olvidan a los ex-empleados, los trabajadores en prácticas o los proveedores, que también tienen derecho a usar el canal.</li>
          <li><strong>No incluir los plazos exactos de la ley.</strong> Los plazos de 7 días hábiles para el acuse de recibo y 3 meses para la resolución son obligatorios y deben aparecer textualmente. Una política que diga "en un plazo razonable" puede ser sancionada.</li>
          <li><strong>Copiar literalmente la ley sin adaptar a la empresa.</strong> La política debe recoger los datos reales de la empresa: el nombre del RSII, la URL concreta del canal, el email de protección de datos. Una plantilla genérica sin adaptar puede ser considerada como ausencia de política real.</li>
          <li><strong>Aprobarla pero no comunicarla.</strong> Una política que existe en un cajón pero que ningún empleado conoce no cumple el requisito de la ley. La AIPI puede sancionar tanto la ausencia de política como la falta de comunicación efectiva.</li>
        </ol>

