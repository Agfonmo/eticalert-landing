        <p>Las características técnicas del libro-registro en EticAlert incluyen:</p>
        <ul>
          <li><strong>Registro append-only:</strong> las entradas no pueden modificarse ni eliminarse retroactivamente, garantizando la integridad del historial.</li>
          <li><strong>Exportación en PDF</strong>: el RSII puede exportar el libro-registro completo o por expediente en formato PDF para auditorías o requerimientos de la AIPI.</li>
          <li><strong>Control de acceso por roles:</strong> solo el RSII y las personas que él autorice pueden acceder al registro.</li>
          <li><strong>Supresión programada:</strong> el sistema puede configurarse para recordar al RSII cuándo debe suprimir datos según los plazos legales.</li>
        </ul>

