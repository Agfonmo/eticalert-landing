        <h1>El libro-registro del canal de denuncias: qué es, qué debe contener y cómo mantenerlo</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a febrero 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-02-22">22 de febrero de 2026</time> por el equipo de EticAlert</p>

        <p>Entre todos los requisitos de la Ley 2/2023, el libro-registro es quizá el más técnico y el que más empresas pasan por alto. Sin embargo, es una obligación legal explícita recogida en el artículo 26 de la ley, y su ausencia o mantenimiento deficiente puede ser constitutiva de infracción grave. En este artículo explicamos qué es, qué debe contener, quién puede acceder a él y cómo automatizar su gestión.</p>

        <h2 id="que-es">¿Qué es el libro-registro? (art. 26 Ley 2/2023)</h2>
        <p>El libro-registro del Sistema Interno de Información es el registro formal de todas las comunicaciones recibidas a través del canal de denuncias y de todas las actuaciones realizadas en relación con cada una de ellas.</p>
        <p>El artículo 26 de la Ley 2/2023 establece que el responsable del sistema está obligado a llevar un <strong>registro de las informaciones recibidas y de las investigaciones internas</strong> que se realicen. Este registro debe mantenerse con las garantías de confidencialidad adecuadas y por el plazo que la ley establece.</p>
        <p>Su función es doble: por un lado, garantiza la trazabilidad del sistema y permite demostrar que cada denuncia ha sido tramitada correctamente; por otro, sirve como evidencia ante una posible inspección de la AIPI o ante un procedimiento judicial.</p>

        <div style="border-left:3px solid var(--accent);background:var(--bg-tertiary);padding:1.25rem 1.5rem;border-radius:0 8px 8px 0;margin:2rem 0;">
          <strong style="color:var(--accent);">Importante:</strong> El libro-registro no es solo una buena práctica de gestión. Es una obligación legal del artículo 26 de la Ley 2/2023. Su ausencia puede considerarse infracción grave, con multas de hasta 600.000€.
        </div>

        <h2 id="que-registrar">Qué debe registrarse en el libro</h2>
        <p>Para cada denuncia recibida, el libro-registro debe contener, como mínimo, los siguientes campos:</p>

        <p><strong>Datos de recepción:</strong></p>
        <ul>
          <li>Fecha y hora exacta de recepción de la comunicación.</li>
          <li>Número de expediente único asignado a la denuncia.</li>
          <li>Canal utilizado para la comunicación (formulario web, email, teléfono, presencial).</li>
          <li>Tipo de denuncia según las categorías predefinidas (contratación pública, fraude interno, acoso, RGPD, etc.).</li>
        </ul>

        <p><strong>Acuse de recibo:</strong></p>
        <ul>
          <li>Fecha del acuse de recibo enviado al informante.</li>
          <li>Confirmación de que el acuse se realizó dentro del plazo de 7 días.</li>
        </ul>

        <p><strong>Acciones de investigación:</strong></p>
        <ul>
          <li>Decisión de admisión o inadmisión y motivos (en caso de inadmisión).</li>
          <li>Resumen de las actuaciones de investigación realizadas (sin nivel de detalle que comprometa la confidencialidad).</li>
          <li>Personas o áreas implicadas en la investigación.</li>
          <li>Documentación solicitada y obtenida.</li>
          <li>Incidencias o dificultades encontradas durante la investigación.</li>
        </ul>

        <p><strong>Resolución:</strong></p>
        <ul>
          <li>Fecha de la resolución.</li>
          <li>Tipo de resolución: archivo, medidas disciplinarias, derivación a autoridades externas, comunicación a AIPI.</li>
          <li>Resumen de las medidas adoptadas o de los motivos del archivo.</li>
        </ul>

        <p><strong>Comunicaciones al informante:</strong></p>
        <ul>
          <li>Fecha en que se informó al denunciante sobre las medidas adoptadas.</li>
          <li>Confirmación de que la comunicación se realizó dentro del plazo de 3 meses.</li>
        </ul>

        <p><strong>En caso de desestimación:</strong></p>
        <ul>
          <li>Motivos detallados de la desestimación (denuncia manifiestamente infundada, falta de datos suficientes, asunto fuera del ámbito del SII, etc.).</li>
        </ul>

        <h2 id="plazos">Plazo de conservación: 10 años mínimo</h2>
        <p>La Ley 2/2023 establece que los datos del sistema deben conservarse durante el tiempo necesario para la tramitación del expediente. La interpretación habitual, respaldada por las directrices de la AEPD, fija el plazo máximo en <strong>10 años</strong>, vinculado a la posible prescripción de delitos graves.</p>
        <p>Sin embargo, la conservación no es uniforme para todos los registros:</p>
        <ul>
          <li>Si la denuncia fue archivada sin investigación: los datos personales deben suprimirse a los <strong>3 meses</strong>, aunque puede conservarse un registro anonimizado (número de expediente, fecha, tipo de infracción) de forma indefinida para estadísticas.</li>
          <li>Si se abrió investigación y se adoptaron medidas: los datos completos se conservan durante el tiempo que sea necesario para defender posibles acciones legales derivadas, hasta el máximo de 10 años.</li>
          <li>El libro-registro en sí mismo (sin datos personales identificativos) puede conservarse más allá de 10 años como parte de los archivos corporativos de compliance.</li>
        </ul>

        <h2 id="acceso">¿Quién puede acceder al libro-registro?</h2>
        <p>El acceso al libro-registro debe ser extremadamente restringido para garantizar la confidencialidad del sistema:</p>
        <ul>
          <li><strong>El RSII</strong>: acceso pleno como responsable del sistema.</li>
          <li><strong>Personas expresamente autorizadas por el RSII</strong> para apoyar en investigaciones concretas, con acceso limitado al expediente en cuestión.</li>
          <li><strong>La AIPI</strong>: durante una inspección, la autoridad puede solicitar acceso al libro-registro completo. El RSII está obligado a facilitarlo.</li>
          <li><strong>Órganos judiciales</strong>: si hay un procedimiento judicial relacionado con una denuncia concreta, el juez puede requerir el expediente.</li>
        </ul>
        <p>El acceso del área de RRHH general, de los responsables del área investigada o de la alta dirección debe limitarse a los casos en que sea estrictamente necesario y con las medidas de protección adecuadas.</p>

        <h2 id="formato">Formato digital vs papel</h2>
        <p>La ley no prescribe un formato concreto para el libro-registro, pero las implicaciones prácticas hacen que el <strong>formato digital sea claramente superior</strong>:</p>
        <ul>
          <li>Mayor seguridad: el acceso puede controlarse por credenciales con registros de auditoría.</li>
          <li>Trazabilidad automática: cada acción queda registrada con marca de tiempo sin intervención manual.</li>
          <li>Búsqueda y exportación: permite generar informes para la AIPI en segundos.</li>
          <li>Cumplimiento del RGPD: la supresión de datos en plazos puede programarse o gestionarse fácilmente.</li>
          <li>Integridad: los sistemas digitales bien diseñados son append-only (no se pueden modificar entradas pasadas), lo que garantiza la autenticidad del registro.</li>
        </ul>
        <p>Un libro-registro en papel no solo es ineficiente: es difícil de proteger, fácil de manipular y complicado de auditar. En 2026, no tiene sentido mantener el registro en formato físico.</p>

        <h2 id="automatizacion">Cómo EticAlert automatiza el libro-registro</h2>
