        <h2 id="diferenciadores-reales">Lo que realmente diferencia a EticAlert</h2>
        <p>Hay tres funcionalidades de EticAlert que merecen explicación porque no son habituales en el mercado y tienen impacto legal directo:</p>

        <p><strong>1. El informante puede excluir gestores por conflicto de interés</strong><br>
        En el momento de hacer la denuncia, el informante puede señalar qué personas de la empresa no deben tener acceso al expediente. Esto no es una función de administración — la controla el propio informante en el formulario público. Es una garantía de imparcialidad que la Ley 2/2023 exige en el diseño del canal.</p>

        <p><strong>2. Los expedientes tienen hash SHA-256 verificable públicamente</strong><br>
        Cada expediente cerrado genera un hash criptográfico que puede verificarse en <code>/verificar</code> sin necesidad de acceder al panel. Esto permite demostrar ante la AIPI o ante un juez que el expediente no ha sido modificado después del cierre — es integridad documental real, no solo un número de caso.</p>

        <p><strong>3. El canal no se activa si faltan requisitos legales</strong><br>
        EticAlert bloquea la publicación del canal si no hay RSII nombrado, si la evidencia de nombramiento ha caducado, o si el plan no es compatible con el número de empleados declarado. No es una advertencia — es un bloqueo real. Esto evita que una empresa publique un canal que no cumple los requisitos sin darse cuenta.</p>

