        <h1>Canal de denuncias para asesorías y despachos profesionales</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a febrero 2026 · 6 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el 17 de febrero de 2026 por el equipo de EticAlert</p>

        <p>Las asesorías laborales, fiscales, jurídicas y los despachos de abogados se encuentran ante una situación particular frente a la Ley 2/2023: tienen una <strong>doble dimensión</strong>. Por un lado, pueden estar obligadas a tener su propio canal (si superan los umbrales de la ley). Por otro, sus clientes —pymes, constructoras, comercios, empresas de servicios— les preguntan a ellos cómo cumplir con la obligación del canal. Esto convierte el tema en una oportunidad de negocio además de una obligación.</p>

        <h2 id="doble-oportunidad">La doble oportunidad de las asesorías</h2>
        <p>Pocas normativas crean una oportunidad tan clara para las asesorías como la Ley 2/2023. La ley obliga a miles de pymes españolas a implementar algo que la mayoría no sabe cómo hacer: un canal de denuncias conforme a requisitos técnicos y legales específicos. Estas pymes tienen asesor fiscal, asesor laboral, abogado de cabecera. Y les van a preguntar a ellos.</p>
        <p>Las asesorías que estén preparadas pueden ofrecer:</p>
        <ul>
          <li>Diagnóstico de obligación (verificar si el cliente está obligado y desde cuándo).</li>
          <li>Implantación del canal (apoyándose en una herramienta SaaS como EticAlert).</li>
          <li>Redacción de la política del SII y nombramiento del RSII.</li>
          <li>Notificación a la AIPI en nombre del cliente.</li>
          <li>Servicio de RSII externo o de apoyo al RSII interno del cliente.</li>
          <li>Instrucción de denuncias complejas como servicio de asesoramiento.</li>
        </ul>
        <p>Todo esto se puede ofrecer como servicio añadido a clientes existentes o como captación de nuevos clientes que aún no tienen asesor de compliance.</p>

        <h2 id="obligacion-propia">Obligación propia de la asesoría (si tiene 50+ empleados)</h2>
        <p>Si tu asesoría o despacho supera los 50 empleados, estás obligado a tener tu propio canal de denuncias desde diciembre de 2023. Esto es independiente de lo que hagas para tus clientes.</p>
        <p>Muchas asesorías medianas han llegado al umbral de 50 empleados sumando empleados propios con los de filiales o despachos asociados. La norma computa la media de trabajadores de los últimos 12 meses, por lo que hay que hacer el cálculo correctamente.</p>
        <p>Si tu despacho tiene menos de 50 empleados, no estás obligado por ley, pero tener canal propio envía una señal muy positiva a los clientes a quienes se lo recomendas: no puedes vender lo que tú mismo no tienes.</p>

        <div style="border-left:3px solid var(--accent);background:var(--bg-tertiary);padding:1.25rem 1.5rem;border-radius:0 8px 8px 0;margin:2rem 0;">
          <strong style="color:var(--accent);">Consejo:</strong> Si asesoras a clientes sobre el canal de denuncias, activar EticAlert en tu propio despacho te permite conocer la herramienta de primera mano, hablar con criterio y demostrar que practicas lo que predicas.
        </div>

        <h2 id="nuevo-servicio">Un nuevo servicio para tus clientes</h2>
        <p>La implantación del canal de denuncias es un servicio que muchas asesorías aún no tienen en su catálogo. Esto crea una ventana de oportunidad clara, especialmente para aquellas que ya tienen relación de confianza con sus clientes.</p>
        <p>El perfil de cliente típico que necesita este servicio es:</p>
        <ul>
          <li>Empresa de 50-250 empleados en cualquier sector.</li>
          <li>Con obligación desde diciembre de 2023, pero que aún no ha actuado.</li>
          <li>Sin departamento de compliance propio.</li>
          <li>Con presupuesto limitado para contratar una gran consultora.</li>
        </ul>
        <p>Es el cliente que ya tienes en cartera: la empresa familiar de segunda generación que ha crecido, la filial española de un grupo internacional sin equipo local, la empresa industrial que cumple con todo lo tributario pero no se ha enterado de la Ley 2/2023.</p>

        <h2 id="modelo-negocio">Modelo de negocio para asesorías</h2>
        <p>Hay varias formas de estructurar el servicio:</p>
        <p><strong>Modelo de implantación + mantenimiento:</strong> cobras una tarifa de implantación (diagnóstico, política SII, configuración del canal, notificación AIPI) más una tarifa mensual de mantenimiento y soporte. El canal técnico corre a cuenta del cliente directamente con EticAlert.</p>
        <p><strong>Modelo de RSII externo:</strong> te conviertes en el RSII del cliente, gestionando el canal y tramitando las denuncias. Este servicio tiene un valor añadido mayor y puede facturarse a un precio superior. EticAlert permite configurar un RSII con acceso al panel del cliente.</p>
        <p><strong>Modelo de instrucción por caso:</strong> no gestionas el día a día del canal, pero el cliente puede contratarte para instruir denuncias complejas (acoso, fraude interno, irregularidades graves) cuando le supere la capacidad interna.</p>

        <h2 id="multi-empresa">Gestión multi-empresa con EticAlert</h2>
        <p>Si gestionas canales de varios clientes, EticAlert permite administrar múltiples organizaciones desde una única cuenta de partner. Esto significa:</p>
        <ul>
          <li>Panel unificado con acceso a todos los clientes que gestionas.</li>
          <li>Configuración independiente para cada cliente (marca, categorías, URL).</li>
          <li>Separación total de datos: los datos de un cliente son absolutamente inaccesibles para otro.</li>
          <li>Facturación consolidada: un solo contrato con EticAlert para todos tus clientes, con condiciones de volumen.</li>
        </ul>

