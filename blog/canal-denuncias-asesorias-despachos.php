        <ul>
          <li>Panel unificado con acceso a todos los clientes que gestionas.</li>
          <li>Configuración independiente para cada cliente (marca, categorías, URL).</li>
          <li>Separación total de datos: los datos de un cliente son absolutamente inaccesibles para otro.</li>
          <li>Facturación consolidada: un solo contrato con EticAlert para todos tus clientes, con condiciones de volumen.</li>
        </ul>

