        <h1>Canal de denuncias para corredurías de seguros: obligación y cómo cumplir</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a mayo 2026 · 6 minutos de lectura</p>
        <p style="font-size:0.875rem;color:var(--text-muted);margin-bottom:2.5rem;">Publicado el <time datetime="2026-05-26">26 de mayo de 2026</time> por el equipo de EticAlert</p>

        <p>El sector asegurador tiene un perfil regulatorio complejo. Las corredurías y mediadores de seguros se preguntan si la <a href="/blog/ley-2-2023-canal-de-denuncias" style="color:var(--accent);">Ley 2/2023</a> les afecta, y la respuesta no es simple: <strong>depende de su actividad concreta y de en qué marcos normativos europeos opera</strong>. Muchas corredurías de tamaño mediano y pequeño pueden estar obligadas sin saberlo.</p>

        <h2 id="cuando-aplica">¿Cuándo aplica la obligación a una correduría?</h2>

        <h3>Vía 1: umbral de 50 empleados (Ley 2/2023 general)</h3>
        <p>Si la correduría tiene 50 o más trabajadores (media de los últimos 12 meses), está obligada directamente por la Ley 2/2023 con independencia de cualquier consideración sectorial. Esta es la vía más clara.</p>

        <h3>Vía 2: normativa sectorial UE de distribución de seguros (IDD)</h3>
        <p>La Directiva (UE) 2016/97 de Distribución de Seguros (IDD), transpuesta en España por la Ley 26/2006 y sus modificaciones, regula la actividad de los mediadores de seguros (corredores, agentes, operadores de banca-seguros). Las entidades que operan bajo este marco están en el ámbito de la normativa UE de servicios financieros.</p>
        <p>La Directiva Whistleblowing incluye expresamente las entidades del ámbito de la normativa UE de <em>«mercados de instrumentos financieros, así como la prevención del blanqueo de capitales y la financiación del terrorismo»</em> y <em>«seguros»</em>. Por tanto, una correduría regulada bajo IDD puede estar en el perímetro de obligación aunque tenga 15 empleados.</p>

        <h3>Vía 3: sujetos obligados PBC en el sector asegurador</h3>
        <p>Algunas entidades aseguradoras y mediadores que operen en ramos de vida con componente de ahorro o inversión pueden ser sujetos obligados PBC bajo la Ley 10/2010, lo que añade una capa adicional de obligación.</p>

        <h2 id="riesgos-sector">Riesgos específicos del sector en el canal de denuncias</h2>
        <p>El canal de una correduría puede recibir comunicaciones sobre:</p>
        <ul>
          <li><strong>Mis-selling:</strong> comercialización de productos de seguro inadecuados para el perfil del cliente, con ocultación de comisiones o sin análisis de necesidades previo.</li>
          <li><strong>Conflictos de interés en la recomendación:</strong> el mediador recomienda el producto de la aseguradora que le paga mayor comisión, no el que más conviene al cliente.</li>
          <li><strong>Presión para cerrar ventas con métodos agresivos:</strong> incumplimiento de las normas de conducta IDD en el proceso de venta.</li>
          <li><strong>Primas no ingresadas:</strong> primas cobradas a clientes que no se transfieren a la aseguradora en plazo.</li>
          <li><strong>Falsedad en peritajes o partes de siniestro:</strong> manipulación de documentación para facilitar liquidaciones fraudulentas.</li>
          <li><strong>Incumplimientos PBC:</strong> operaciones de seguro de vida o de ahorro que presentan señales de alerta de blanqueo no comunicadas.</li>
        </ul>

        <h2 id="solvencia-ii">Solvencia II y el canal: la doble capa para aseguradoras</h2>
        <p>Las aseguradoras sujetas a Solvencia II (la gran mayoría de las que operan en España) ya tienen obligaciones de reporte interno de incidentes de control interno y de gestión de riesgos. La Directiva Whistleblowing se superpone a estos marcos: el canal de denuncias de la Ley 2/2023 no reemplaza los mecanismos de reporte de Solvencia II, sino que los complementa con un canal específico para los empleados que quieran comunicar irregularidades de forma anónima y protegida.</p>

