        <h1>Acoso laboral y canal de denuncias: cómo actuar como empresa</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a enero 2026 · 9 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-01-21">21 de enero de 2026</time> por el equipo de EticAlert</p>

        <p>El acoso laboral —en cualquiera de sus formas— es probablemente la categoría de denuncia más frecuente en los canales internos de las empresas españolas. No es casual: las situaciones de acoso son precisamente aquellas en las que la víctima siente que no puede hablar directamente con su superior (que muchas veces es el agresor) y necesita un canal alternativo y confidencial para comunicar lo que está viviendo. Por eso, el diseño y gestión del canal ante denuncias de acoso es especialmente crítico.</p>

        <h2 id="tipos-acoso">Tipos de acoso que pueden denunciarse</h2>
        <p>Bajo el término "acoso" se agrupan varias conductas con características y consecuencias legales diferentes:</p>
        <ul>
          <li><strong>Acoso moral o mobbing:</strong> conducta sistemática y reiterada de hostigamiento psicológico en el entorno laboral, con el objetivo de degradar las condiciones de trabajo de la víctima y lograr que abandone. Puede ser vertical (jefe a empleado) u horizontal (entre compañeros).</li>
          <li><strong>Acoso sexual:</strong> cualquier comportamiento de naturaleza sexual no consentido que cree un entorno laboral intimidatorio, hostil o humillante para quien lo sufre. Tipificado en el artículo 184 del Código Penal.</li>
          <li><strong>Acoso por razón de sexo:</strong> comportamiento discriminatorio basado en el género de la víctima, aunque no tenga componente sexual explícito.</li>
          <li><strong>Acoso discriminatorio:</strong> hostigamiento basado en el origen racial, orientación sexual, religión, discapacidad, edad o cualquier otra condición protegida por la ley.</li>
        </ul>
        <p>Todas estas conductas son denunciables a través del canal de denuncias. Además, el acoso sexual y algunas formas graves de acoso moral tienen relevancia penal, lo que puede justificar derivar el caso a las autoridades externas.</p>

        <h2 id="puede-denunciarse">¿Se puede denunciar el acoso por el canal de denuncias?</h2>
        <p>Sí. El acoso laboral es una infracción normativa (del Estatuto de los Trabajadores, de la Ley de Igualdad, del Código Penal en los supuestos más graves) y, como tal, entra dentro del ámbito del canal de denuncias de la Ley 2/2023.</p>
        <p>Algunas empresas han cometido el error de configurar su canal para recibir solo denuncias de irregularidades financieras o contractuales, excluyendo las cuestiones laborales. Esto es un error legal: el ámbito de la Ley 2/2023 en España incluye las infracciones laborales graves, y el acoso lo es.</p>
        <p>La víctima puede usar el canal de denuncias como alternativa al protocolo de acoso del Plan de Igualdad (si existe), o de forma complementaria. No hay incompatibilidad entre los dos mecanismos.</p>

        <h2 id="que-hacer">Qué debe hacer la empresa paso a paso</h2>
        <p>Cuando llega una denuncia de acoso a través del canal, el RSII debe seguir un procedimiento cuidadoso que garantice tanto los derechos del denunciante como los del denunciado:</p>

        <p><strong>1. Acuse de recibo (máximo 7 días)</strong></p>
        <p>El primer paso es acusar recibo al denunciante en el plazo máximo de 7 días. En el caso del acoso, el acuse debe incluir una indicación de que la empresa toma el asunto con la máxima seriedad y que se adoptarán medidas cautelares si fuera necesario para proteger a la víctima mientras se investiga.</p>

        <p><strong>2. Apertura del expediente de investigación</strong></p>
        <p>El RSII debe decidir si admite la denuncia y abre expediente. En casos de acoso, la admisión debería ser la regla general ante cualquier relato verosímil; la inadmisión solo procede si la denuncia es manifiestamente infundada o anónima sin datos suficientes para investigar.</p>

        <p><strong>3. Adopción de medidas cautelares inmediatas</strong></p>
        <p>Si hay riesgo de que continúe la situación de acoso durante la investigación, el RSII debe recomendar a la dirección la adopción de medidas cautelares: separación física del agresor y la víctima, suspensión del agresor con carácter preventivo (en casos graves), cambio temporal de área o turno, o cualquier medida que proteja a la víctima sin perjuicio para ella.</p>

        <p><strong>4. Investigación discreta y confidencial</strong></p>
        <p>La investigación debe ser discreta. El RSII recaba testimonios de testigos, solicita documentación (mensajes, emails, partes de trabajo), puede recurrir a asesores externos (psicólogos, abogados laboralistas). El denunciado no debe ser informado de la denuncia hasta que la investigación esté en una fase que no pueda verse comprometida.</p>

        <p><strong>5. Resolución y comunicación de medidas</strong></p>
        <p>Una vez concluida la investigación, el RSII emite una resolución: si se acredita el acoso, se derivan medidas disciplinarias (hasta el despido procedente); si no se acredita, se archiva con constancia de los motivos. La víctima debe ser informada de la resolución en un plazo máximo de 3 meses.</p>

        <p><strong>6. Comunicación al organismo externo si procede</strong></p>
        <p>Si la conducta tiene relevancia penal (acoso sexual, violencia en el trabajo), el RSII puede recomendar a la empresa comunicarlo a las autoridades externas o derivar a la víctima para que lo haga directamente si lo desea.</p>

        <h2 id="proteccion-informante">Protección del informante: inversión de la carga de la prueba</h2>
        <p>En las denuncias de acoso, la protección del informante es especialmente importante porque el agresor suele tener más poder en la organización que la víctima. La Ley 2/2023 establece la inversión de la carga de la prueba: si el denunciante sufre algún perjuicio laboral después de presentar la denuncia, se presume que es una represalia y corresponde a la empresa demostrar lo contrario.</p>
        <p>Esto significa que si el directivo acusado de acoso consigue que la víctima sea trasladada, degradada o despedida después de la denuncia, la empresa tiene muy difícil demostrar que ese perjuicio no está relacionado con la denuncia.</p>

        <h2 id="plan-igualdad">Relación con el protocolo de acoso del Plan de Igualdad</h2>
        <p>Las empresas con 50 o más empleados están obligadas a tener Plan de Igualdad, que incluye un protocolo de actuación ante el acoso sexual y por razón de sexo. Este protocolo y el canal de denuncias de la Ley 2/2023 son instrumentos complementarios, no excluyentes.</p>
        <p>La buena práctica es que el canal de denuncias pueda recibir cualquier tipo de denuncia de acoso, y que el RSII coordine con el equipo de RRHH la activación del protocolo del Plan de Igualdad cuando la denuncia lo requiera.</p>

        <h2 id="denuncia-directivo">¿Y si la denuncia es contra un directivo?</h2>
        <p>Este es el caso más delicado y el que más a menudo lleva a las empresas a gestionar mal el canal. Si la denuncia es contra un directivo o el propio RSII, el sistema de gestión interna puede tener un conflicto de interés.</p>
        <p>La solución es establecer un protocolo de escalado: si la denuncia afecta al RSII, la gestión debe asumiría el órgano de administración directamente o un RSII suplente. Si afecta a un miembro del órgano de administración, puede ser necesario acudir directamente a la AIPI o a asesores externos independientes.</p>

