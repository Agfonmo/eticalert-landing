              <p style="margin:0.375rem 0 0;font-size:0.875rem;color:var(--text-secondary);">Empresas promotoras de construcción residencial, terciaria o industrial que vendan directamente al comprador final. También aplica a las cooperativas de vivienda cuando el promotor actúa como gestor.</p>
