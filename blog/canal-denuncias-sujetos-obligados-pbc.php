        <h2 id="riesgos-pbc">¿Qué tipo de denuncias recibirá el canal en un sujeto obligado PBC?</h2>
        <p>El canal de denuncias en un contexto PBC tiene un perfil de comunicaciones muy específico. Los empleados y colaboradores de estas entidades pueden detectar y necesitan poder comunicar de forma segura:</p>
        <ul>
          <li>Operaciones inusuales o sospechosas que no han sido reportadas al SEPBLAC como deberían.</li>
          <li>Clientes cuya identificación (KYC) no se ha realizado o se ha hecho de forma insuficiente.</li>
          <li>Estructuración de operaciones para evitar los umbrales de notificación.</li>
          <li>Presión interna para no reportar una operación sospechosa por miedo a perder al cliente.</li>
          <li>Uso de la entidad por parte de un directivo para facilitar operaciones de blanqueo propias.</li>
          <li>Falsedad en la documentación de identificación del cliente o del origen de los fondos.</li>
        </ul>
        <p>En muchos de estos casos, el empleado que detecta el problema sabe que está ante una situación irregular, pero también sabe que si lo dice en alto puede ser presionado, trasladado o despedido. El canal de denuncias anónimo es la única vía que le permite comunicarlo de forma protegida.</p>

        <h2 id="doble-canal">La doble obligación: canal interno + reporte al SEPBLAC</h2>
        <p>Los sujetos obligados PBC tienen dos vías de reporte que conviven y no se sustituyen:</p>
        <ul>
          <li><strong>El canal de denuncias interno (Ley 2/2023):</strong> para que los empleados y colaboradores comuniquen internamente irregularidades, incluyendo incumplimientos de las obligaciones PBC. Gestión interna, confidencial, con protección al informante.</li>
          <li><strong>El reporte al SEPBLAC (Servicio Ejecutivo de la Comisión de Prevención del Blanqueo de Capitales):</strong> cuando se detecta una operación sospechosa que debe notificarse externamente al supervisor. Este reporte lo realiza el compliance officer o el representante ante el SEPBLAC, no el empleado directamente.</li>
        </ul>
        <p>El canal interno no reemplaza la obligación de reporte al SEPBLAC. Pero sí puede ser la vía por la que un empleado avisa de que hay una operación sospechosa que no está siendo reportada, lo que permite corregir el incumplimiento antes de que derive en una sanción del SEPBLAC.</p>

