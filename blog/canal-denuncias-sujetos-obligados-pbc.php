        <h3 id="inmobiliarias">Sector inmobiliario</h3>
        <p>El sector inmobiliario es uno de los vectores clásicos de blanqueo de capitales, razón por la que está ampliamente regulado en la Ley 10/2010.</p>

        <div style="display:flex;flex-direction:column;gap:0.875rem;margin:1.25rem 0;">
          <div style="display:flex;gap:1rem;background:var(--bg-secondary);border:1px solid var(--border);border-radius:var(--radius-lg);padding:1.125rem;">
            <span style="font-size:1.25rem;flex-shrink:0;">🏠</span>
            <div>
              <p style="margin:0;font-weight:600;">Agencias inmobiliarias y agentes</p>
              <p style="margin:0.375rem 0 0;font-size:0.875rem;color:var(--text-secondary);">Cualquier empresa o profesional que actúe como intermediario en la compraventa o alquiler de inmuebles, sin importar su tamaño ni su forma jurídica.</p>
            </div>
          </div>
          <div style="display:flex;gap:1rem;background:var(--bg-secondary);border:1px solid var(--border);border-radius:var(--radius-lg);padding:1.125rem;">
