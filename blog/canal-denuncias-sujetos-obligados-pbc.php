              <p style="margin:0.375rem 0 0;font-size:0.875rem;color:var(--text-secondary);">Los despachos que actúan como administrador, socio o accionista nominal de sociedades, los que constituyen o gestionan fundaciones y fideicomisos para clientes, o los que ejercen de director nominal de una empresa.</p>
