        <h1>Canal de denuncias para sujetos obligados PBC: asesorías, notarías, inmobiliarias y más</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a mayo 2026 · 8 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-05-26">26 de mayo de 2026</time> por el equipo de EticAlert</p>

        <p>Existe un colectivo muy numeroso de empresas y profesionales que están obligados a tener canal de denuncias aunque tengan muchos menos de 50 empleados: los <strong>sujetos obligados por la Ley 10/2010 de prevención del blanqueo de capitales y financiación del terrorismo (PBC)</strong>. Se trata de sectores que la normativa europea considera de alto riesgo de blanqueo y que, precisamente por eso, la <a href="/blog/ley-2-2023-canal-de-denuncias" style="color:var(--accent);">Directiva Whistleblowing</a> incluye en su ámbito de aplicación con independencia de su tamaño.</p>

        <p>Si tu empresa o despacho está en esta lista y no tienes canal de denuncias, no es que "quizás" estés obligado: es que <strong>ya debías tenerlo desde que entró en vigor la Ley 2/2023</strong>.</p>

        <h2 id="conexion-legal">La conexión legal: por qué la Ley 10/2010 te obliga al canal de denuncias</h2>
        <p>La conexión no es directa. La Ley 10/2010 de PBC no impone por sí misma el canal de denuncias. Lo que ocurre es lo siguiente:</p>
        <ol>
          <li>La Directiva (UE) 2019/1937 (Whistleblowing) establece que la obligación aplica a empresas del <em>ámbito de la normativa de la Unión sobre prevención del blanqueo de capitales</em>.</li>
          <li>La Ley 2/2023 transpone esa directiva al derecho español.</li>
          <li>Los sujetos obligados por la Ley 10/2010 operan, por definición, en el ámbito de esa normativa UE.</li>
          <li>Por tanto, les aplica la obligación de canal de denuncias de la Ley 2/2023 independientemente de su número de trabajadores.</li>
        </ol>

        <div class="callout">
          <p><strong>Consecuencia práctica:</strong> una asesoría fiscal de 8 personas, una notaría unipersonal o una inmobiliaria familiar de 15 trabajadores pueden estar tan obligadas como una empresa de 200 empleados. El criterio no es el tamaño: es la actividad.</p>
        </div>

        <h2 id="quienes-son">¿Quiénes son sujetos obligados PBC?</h2>
        <p>El artículo 2 de la Ley 10/2010 lista los sujetos obligados. Los más relevantes para este análisis son:</p>

        <h3 id="asesores">Asesores, auditores y profesionales del sector financiero-contable</h3>
        <div style="display:flex;flex-direction:column;gap:0.875rem;margin:1.25rem 0;">

          <div style="display:flex;gap:1rem;background:var(--bg-secondary);border:1px solid var(--border);border-radius:var(--radius-lg);padding:1.125rem;">
            <span style="font-size:1.25rem;flex-shrink:0;">🧾</span>
            <div>
              <p style="margin:0;font-weight:600;">Asesorías fiscales y contables externas</p>
              <p style="margin:0.375rem 0 0;font-size:0.875rem;color:var(--text-secondary);">Cuando prestan servicios de gestión tributaria, contabilidad o elaboración de cuentas anuales para terceros. La externalización de la contabilidad y la asesoría fiscal recurrente son el core de este supuesto.</p>
            </div>
          </div>

          <div style="display:flex;gap:1rem;background:var(--bg-secondary);border:1px solid var(--border);border-radius:var(--radius-lg);padding:1.125rem;">
            <span style="font-size:1.25rem;flex-shrink:0;">📋</span>
            <div>
              <p style="margin:0;font-weight:600;">Auditores de cuentas (ROAC)</p>
              <p style="margin:0.375rem 0 0;font-size:0.875rem;color:var(--text-secondary);">Todos los auditores inscritos en el Registro Oficial de Auditores de Cuentas son sujetos obligados PBC. Incluye tanto los auditores individuales como las sociedades de auditoría.</p>
            </div>
          </div>

          <div style="display:flex;gap:1rem;background:var(--bg-secondary);border:1px solid var(--border);border-radius:var(--radius-lg);padding:1.125rem;">
            <span style="font-size:1.25rem;flex-shrink:0;">⚖️</span>
            <div>
              <p style="margin:0;font-weight:600;">Abogados en operaciones específicas</p>
              <p style="margin:0.375rem 0 0;font-size:0.875rem;color:var(--text-secondary);">Los abogados son sujetos obligados cuando participan en nombre de su cliente en operaciones de compraventa de inmuebles, gestión de fondos y valores, creación de estructuras societarias o en operaciones bancarias o crediticias. No aplica en la representación en juicio ni en asesoramiento jurídico general.</p>
            </div>
