        <h1>Canal de denuncias interno vs externo: diferencias y cuál elegir</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a enero 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el 27 de enero de 2026 por el equipo de EticAlert</p>

        <p>Cuando hablamos del canal de denuncias según la Ley 2/2023, en realidad hablamos de dos cosas diferentes: el <strong>canal interno</strong>, que es el que cada organización obligada debe tener, y el <strong>canal externo</strong>, que es el de la AIPI. Entender la diferencia entre ambos es fundamental para cumplir correctamente la ley y para que el sistema funcione como está diseñado.</p>

        <h2 id="dos-niveles">Dos niveles según la Ley 2/2023</h2>
        <p>La arquitectura de la Ley 2/2023 (siguiendo el modelo de la Directiva 2019/1937) establece dos niveles de comunicación:</p>
        <ul>
          <li><strong>Nivel 1 - Canal interno:</strong> cada organización obligada debe disponer de su propio sistema para recibir y gestionar las comunicaciones. Es el canal que la empresa controla y gestiona internamente a través del RSII.</li>
          <li><strong>Nivel 2 - Canal externo:</strong> la Autoridad Independiente de Protección del Informante (AIPI) tiene su propio canal al que cualquier persona puede acudir directamente, sin necesidad de pasar antes por el canal interno de la empresa.</li>
        </ul>
        <p>La ley no establece ninguna jerarquía obligatoria entre los dos niveles: el informante puede elegir usar el canal interno, el externo, o ambos. No está obligado a agotar el canal interno antes de acudir a la AIPI.</p>

        <h2 id="canal-interno">Canal interno: qué es y quién lo gestiona</h2>
        <p>El canal interno es el sistema de la propia organización. La empresa lo diseña, lo configura, lo gestiona y es responsable de su correcto funcionamiento. Sus características principales:</p>
        <ul>
          <li><strong>Gestionado por el RSII:</strong> el Responsable del Sistema Interno de Información es quien recibe, tramita e investiga las denuncias. Debe ser una persona interna a la organización con independencia funcional.</li>
          <li><strong>Alcance:</strong> puede recibir denuncias sobre cualquier infracción que afecte a la organización, incluyendo infracciones del Derecho nacional más allá de las contempladas en la directiva europea.</li>
          <li><strong>Confidencialidad gestionada internamente:</strong> la empresa es responsable de garantizar la confidencialidad del informante dentro de su propio sistema.</li>
          <li><strong>Respuesta en 3 meses:</strong> el RSII tiene la obligación de informar al denunciante sobre las medidas adoptadas en un plazo máximo de 3 meses.</li>
          <li><strong>Libro-registro:</strong> la empresa debe mantener un registro de todas las comunicaciones y actuaciones.</li>
        </ul>

        <h2 id="canal-externo">Canal externo: la AIPI</h2>
        <p>El canal externo de la AIPI es el sistema público gestionado por la Autoridad Independiente de Protección del Informante. Sus características:</p>
        <ul>
          <li><strong>Accesible a cualquier persona:</strong> no hace falta tener relación laboral con ninguna empresa concreta. Cualquier ciudadano puede denunciar directamente a la AIPI.</li>
          <li><strong>Competencia sobre infracciones del Derecho de la UE y del Estado:</strong> la AIPI tiene competencia sobre las infracciones que la ley le atribuye. Para infracciones que sean competencia autonómica, puede derivar a las autoridades correspondientes.</li>
          <li><strong>Investigación propia:</strong> la AIPI puede investigar directamente o derivar al organismo competente.</li>
          <li><strong>Potestad sancionadora:</strong> la AIPI puede sancionar a las organizaciones que incumplan la ley, incluyendo las que tengan un canal deficiente o tomen represalias.</li>
          <li><strong>No es un canal de segunda oportunidad:</strong> el informante puede ir directamente a la AIPI sin pasar antes por el canal interno.</li>
        </ul>

        <h2 id="externalizar-gestion">¿Se puede externalizar la gestión del canal interno?</h2>
        <p>Esta es una pregunta frecuente: ¿puede una empresa contratar a un tercero (despacho de abogados, consultora) para que gestione el canal interno?</p>
        <p>La respuesta es <strong>sí, parcialmente</strong>. Lo que puede externalizarse:</p>
        <ul>
          <li>La <strong>herramienta técnica</strong> (plataforma SaaS como EticAlert).</li>
          <li>La <strong>instrucción de denuncias complejas</strong> (el RSII interno puede pedir apoyo a asesores externos para casos difíciles).</li>
          <li>La <strong>formación del RSII</strong>.</li>
        </ul>
        <p>Lo que no puede externalizarse completamente:</p>
        <ul>
          <li>El <strong>RSII debe ser una persona interna</strong> a la organización. No puede ser un externo asumiendo todas las funciones del RSII.</li>
          <li>La <strong>decisión final</strong> sobre las medidas a adoptar es siempre responsabilidad de la organización.</li>
          <li>La <strong>notificación a la AIPI</strong> del RSII designado debe ser de una persona interna.</li>
        </ul>

