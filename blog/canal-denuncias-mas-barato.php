        <h2 id="garantias">Barato no puede significar incompleto</h2>
        <p>La Ley 2/2023 exige lo mismo a un canal de 9€ que a uno de 199€. Antes de elegir el más barato, verifica que incluye los cinco mínimos legales:</p>
        <ul>
          <li><strong>Denuncia anónima real</strong> — no solo confidencial: el sistema no debe poder identificar al informante si este elige el anonimato.</li>
          <li><strong>Acuse de recibo en 7 días</strong> naturales, automático o no.</li>
          <li><strong>Plazo de resolución de 3 meses</strong>, con seguimiento del expediente.</li>
          <li><strong><a href="/blog/libro-registro-canal-denuncias">Libro-registro</a></strong> de todas las comunicaciones (artículo 26).</li>
          <li><strong>Confidencialidad y protección de datos</strong>: cifrado y acceso restringido al RSII y personas autorizadas.</li>
        </ul>
        <p>EticAlert incluye los cinco en el plan de 9€/mes — además de <a href="/funcionalidades">verificación de expedientes con hash SHA-256</a>, que ningún otro proveedor de la tabla ofrece a ningún precio.</p>

        <h2 id="costes-ocultos">Los 4 costes ocultos que encarecen un canal "barato"</h2>
        <ol>
          <li><strong>Cuota de alta o setup.</strong> Un canal de 19€/mes con 99€ de alta cuesta 327€ el primer año — más del cuádruple que uno de 81€/año sin alta.</li>
          <li><strong>Precio por usuario.</strong> Las suites de RRHH parecen baratas por usuario, pero multiplica por tu plantilla: a 5€/usuario, 40 empleados son 2.400€/año.</li>
          <li><strong>Permanencia anual obligatoria.</strong> Algunos proveedores solo facturan por año completo. Si el servicio no te convence, no hay salida hasta la renovación.</li>
          <li><strong>Módulos aparte.</strong> Formación, multiidioma o informes pueden ser extras de pago. En EticAlert el <a href="/precios">multiidioma está incluido en todos los planes</a>.</li>
        </ol>

