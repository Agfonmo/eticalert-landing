        <h1>Denuncias falsas en el canal de denuncias: consecuencias y cómo gestionarlas</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a enero 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el 18 de enero de 2026 por el equipo de EticAlert</p>

        <p>Una de las objeciones más frecuentes que escuchamos de directivos reticentes a implantar un canal de denuncias es: <em>¿y si alguien lo usa para hacer daño a un compañero con una denuncia falsa?</em> Es una preocupación legítima que merece una respuesta honesta. La ley tiene mecanismos específicos para proteger al denunciado de las denuncias de mala fe, y el RSII tiene herramientas para gestionarlas. En este artículo lo explicamos en detalle.</p>

        <h2 id="ley-no-protege">La ley NO protege denuncias falsas</h2>
        <p>Este punto es el más importante y, paradójicamente, el menos conocido. La Ley 2/2023 protege al informante, sí. Pero solo protege al informante que actúa de <strong>buena fe</strong>: quien tiene motivos razonables para creer que lo que comunica es cierto en el momento de la comunicación.</p>
        <p>La ley es explícita al respecto: quien presenta una comunicación <strong>sabiendo que la información es falsa</strong> no está protegido por la ley. Al contrario: está expuesto a consecuencias legales. La buena fe no significa certeza absoluta (no se exige que el denunciante tenga pruebas irrefutables), pero sí exige que no conozca positivamente la falsedad de lo que denuncia.</p>
        <p>La distinción es importante:</p>
        <ul>
          <li><strong>Denuncia de buena fe no probada:</strong> el informante cree honestamente que los hechos son ciertos, pero la investigación no puede confirmarlos. El informante sigue protegido.</li>
          <li><strong>Denuncia de mala fe:</strong> el informante sabe que los hechos son falsos pero denuncia igualmente (por venganza, para perjudicar a un rival, para distraer atención de sus propios problemas). El informante no está protegido.</li>
        </ul>

        <h2 id="consecuencias">Consecuencias de una denuncia de mala fe</h2>
        <p>El ordenamiento jurídico español prevé varias consecuencias para quien presenta denuncias falsas:</p>

        <p><strong>Medidas disciplinarias laborales:</strong></p>
        <p>La empresa puede imponer sanciones disciplinarias al empleado que haya presentado una denuncia con conocimiento de su falsedad, incluyendo el despido disciplinario en los casos más graves. Esta sanción disciplinaria no vulnera la Ley 2/2023 porque la protección solo alcanza a las denuncias de buena fe.</p>

        <p><strong>Responsabilidad civil:</strong></p>
        <p>El denunciante de mala fe puede ser demandado civilmente por el denunciado para reclamar los daños y perjuicios causados por la denuncia falsa: daño moral, daño reputacional, perjuicios profesionales (ej. si el denunciado fue suspendido cautelarmente durante la investigación).</p>

        <p><strong>Responsabilidad penal:</strong></p>
        <p>Dependiendo de la gravedad de los hechos falsos comunicados, el denunciante puede incurrir en:</p>
        <ul>
          <li><strong>Acusación y denuncia falsa (art. 456 del Código Penal):</strong> cuando se imputa falsamente a alguien un delito ante la autoridad o sus agentes. Pena de prisión de 6 meses a 2 años (si el delito imputado es grave) o multa (si es menos grave o leve).</li>
          <li><strong>Calumnia (art. 205 del Código Penal):</strong> imputación a alguien de un hecho con conocimiento de su falsedad o temerario desprecio hacia la verdad. Pena de multa de 12 a 24 meses.</li>
        </ul>
        <p>Estas consecuencias penales son relevantes cuando la denuncia imputa conductas delictivas falsas (acoso sexual que no existió, fraude que no ocurrió). Para denuncias sobre infracciones administrativas falsas, las consecuencias son más limitadas.</p>

        <p><strong>Pérdida de la protección de la Ley 2/2023:</strong></p>
        <p>El denunciante de mala fe pierde automáticamente todos los derechos que la ley otorga al informante: no puede invocar la inversión de la carga de la prueba, no está protegido frente a medidas disciplinarias, y no tiene derecho a las medidas de apoyo previstas para los informantes.</p>

        <h2 id="diferencia-crucial">Diferencia crucial: denuncia falsa vs denuncia no probada</h2>
        <p>Esta distinción es fundamental y el RSII debe tenerla muy clara para no cometer errores graves:</p>
        <p>Una denuncia es <strong>falsa</strong> cuando el denunciante conoce que los hechos que comunica son mentira en el momento de comunicarlos. Una denuncia es <strong>no probada</strong> cuando el informante creía honestamente en lo que comunicaba, pero la investigación no ha podido confirmar los hechos.</p>
        <p>La denuncia no probada no convierte al denunciante en mentiroso ni en sujeto de consecuencias. Las investigaciones a menudo no encuentran evidencias suficientes aunque los hechos hayan ocurrido, especialmente en situaciones de acoso donde no hay testigos ni documentación.</p>
        <p>El error más común —y más peligroso— que puede cometer una empresa es tratar automáticamente como denuncia falsa toda denuncia cuya investigación no ha podido confirmar los hechos. Eso puede constituir una represalia contra el informante, con las consecuencias legales correspondientes para la empresa.</p>

        <div style="border-left:3px solid var(--accent);background:var(--bg-tertiary);padding:1.25rem 1.5rem;border-radius:0 8px 8px 0;margin:2rem 0;">
          <strong style="color:var(--accent);">Importante:</strong> Archivar una denuncia por falta de pruebas no es lo mismo que concluir que fue de mala fe. El RSII debe ser muy cauteloso antes de iniciar acciones contra un denunciante por supuesta mala fe.
        </div>

        <h2 id="gestion-rsii">Cómo gestiona el RSII una sospecha de mala fe</h2>
        <p>Si el RSII tiene indicios de que una denuncia puede haber sido presentada de mala fe, el proceso recomendado es:</p>
        <ol>
          <li><strong>Concluir la investigación ordinaria:</strong> primero hay que investigar los hechos denunciados con la misma diligencia que cualquier otro caso. No se puede presuponer la mala fe antes de investigar.</li>
          <li><strong>Documentar los indicios de mala fe:</strong> si durante la investigación aparecen evidencias de que el denunciante conocía la falsedad (mensajes en los que admite que los hechos no ocurrieron, testigos que contradicen directamente su versión, etc.), documentarlos en el expediente.</li>
          <li><strong>Elevar a la dirección:</strong> el RSII debe elevar su valoración a la dirección con la documentación que sustenta la sospecha de mala fe, para que sea la empresa quien decida si toma medidas disciplinarias.</li>
          <li><strong>No actuar de forma automática:</strong> la empresa nunca debe imponer sanciones disciplinarias al denunciante sin antes haber documentado sólidamente la mala fe. El riesgo de que una sanción precipitada sea interpretada como represalia es real.</li>
        </ol>

        <h2 id="miedo-infundado">El miedo a denuncias falsas no justifica no tener canal</h2>
        <p>Volviendo a la preocupación inicial: el miedo a que el canal sea usado para denuncias falsas no es motivo suficiente para no implantarlo. En la práctica, las denuncias de mala fe son una minoría pequeña del total de comunicaciones recibidas. La mayoría de las personas que usan el canal tienen motivos genuinos para hacerlo.</p>
        <p>Además, no tener canal implica que los problemas reales —los que sí existen— no tienen cómo salir a la superficie internamente y acaban llegando a la AIPI, a la prensa o a los tribunales sin que la empresa haya tenido oportunidad de gestionarlos.</p>
        <p>Un canal bien diseñado, con proceso claro y RSII competente, filtra naturalmente las denuncias de mala fe y permite a la empresa gestionar los problemas reales antes de que escalen.</p>

