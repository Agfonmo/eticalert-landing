        <h1>RSII: guía práctica para designar y notificar tu responsable del canal de denuncias</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Actualizado a marzo 2026 · 7 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el <time datetime="2026-03-10">10 de marzo de 2026</time> por el equipo de EticAlert</p>

        <p>Toda empresa obligada a tener un Sistema Interno de Información necesita un Responsable: el RSII. Desde febrero de 2026, esta designación debe comunicarse oficialmente a la AIPI, y el plazo para hacerlo expira el <strong>10 de abril de 2026</strong>. Esta guía explica paso a paso cómo hacerlo correctamente.</p>

        <h2 id="que-es-rsii">¿Qué es el RSII?</h2>
        <p>El <strong>Responsable del Sistema Interno de Información (RSII)</strong> es la persona o el órgano colegiado encargado de gestionar el canal de denuncias de forma independiente. Es la figura central del sistema de compliance interno que exige la Ley 2/2023.</p>
        <p>El RSII recibe las denuncias, decide su admisión, las investiga o las remite al órgano competente, comunica las conclusiones al denunciante y garantiza en todo momento la confidencialidad del informante. No puede tener conflicto de interés en los asuntos que gestiona.</p>
        <p>Su existencia y correcta designación es uno de los elementos que la AIPI verifica cuando inspecciona el sistema interno de una empresa.</p>

        <h2 id="quien-puede-ser-rsii">¿Quién puede ser RSII?</h2>
        <p>La Ley 2/2023 no impone un perfil profesional concreto, pero la AIPI ha emitido orientaciones prácticas al respecto (Recomendación 1/2026):</p>
        <ul>
          <li>Se recomienda que sea un <strong>directivo de la entidad</strong> con peso suficiente para actuar con independencia real.</li>
          <li>Debe ejercer su cargo con <strong>independencia funcional</strong> del órgano de administración o gobierno de la empresa. Esto significa que no puede recibir instrucciones de las personas investigadas.</li>
          <li>Puede ser una <strong>persona física unipersonal</strong> o un <strong>órgano colegiado</strong> (comité de ética, comisión de auditoría, etc.).</li>
          <li>En grupos de empresas, se puede designar un <strong>RSII único para todo el grupo</strong> conforme al artículo 11 de la Ley 2/2023, lo que simplifica enormemente la gestión.</li>
          <li>Si es un órgano colegiado, <strong>todos los miembros</strong> deben ser comunicados a la AIPI.</li>
        </ul>

        <div class="callout">
          <p><strong>¿Puede ser un abogado externo?</strong> Sí, la ley permite que sea una persona o entidad externa con independencia. Sin embargo, el abogado externo debe tener acceso real al canal y capacidad efectiva de actuar, no ser únicamente una figura formal.</p>
        </div>

        <h2 id="como-designar">¿Cómo se designa formalmente?</h2>
        <p>La designación debe ser formal y documentada. Los pasos son:</p>
        <ol>
          <li><strong>Aprobación por el órgano competente:</strong> el consejo de administración, la junta general o el órgano equivalente debe tomar el acuerdo formal de designar al RSII. Este acuerdo debe constar en acta.</li>
          <li><strong>Garantías de independencia:</strong> el nombramiento debe incluir las garantías de independencia funcional, los medios materiales y técnicos asignados y la formación que recibirá.</li>
          <li><strong>Aprobación previa de la política del SII:</strong> la empresa debe haber aprobado formalmente su Política del Sistema Interno de Información, que describe el funcionamiento del canal, los procedimientos y las garantías.</li>
          <li><strong>Comunicación interna:</strong> informar a toda la plantilla de quién es el RSII y cómo contactar con él, garantizando que este dato sea accesible para todos los empleados.</li>
        </ol>

        <h2 id="como-notificar-aipi">¿Cómo notificar el RSII a la AIPI?</h2>
        <p>Desde el 10 de febrero de 2026, la AIPI tiene habilitado el formulario oficial de notificación:</p>
        <ol>
          <li>Acceder a la sede electrónica de la AIPI: <strong>sede.proteccioninformante.gob.es</strong></li>
          <li>Seleccionar el trámite "Comunicación de designación de RSII".</li>
          <li>Autenticarse con <strong>certificado digital</strong> (de empresa o personal) o con la aplicación <strong>Autofirma</strong>.</li>
          <li>Rellenar el formulario con los datos de la empresa y del RSII designado (nombre, cargo, datos de contacto).</li>
          <li>Adjuntar el acta o resolución de nombramiento y la documentación acreditativa.</li>
          <li>Enviar y guardar el justificante de presentación.</li>
        </ol>
        <p>Navegadores recomendados por la AIPI: Chrome o Firefox en su versión actualizada. Internet Explorer no es compatible.</p>

        <div class="callout">
          <p><strong>Plazos críticos:</strong><br>
          · Plazo inicial: 10 de abril de 2026 (2 meses desde la apertura del formulario el 10 de febrero).<br>
          · Cambios posteriores: cada nuevo nombramiento o cese del RSII debe comunicarse en 10 días hábiles.<br>
          · La AIPI ha publicado en su web un Manual Práctico, FAQ y Guía de Notificación.</p>
        </div>

        <h2 id="errores-comunes">Errores comunes que debes evitar</h2>
        <p>Basándonos en las consultas que recibimos, estos son los errores más frecuentes que cometen las empresas:</p>
        <ul>
          <li><strong>Notificar sin tener el canal operativo:</strong> la AIPI espera un sistema real y funcional, no solo un RSII designado. Si notificas pero no tienes canal, el incumplimiento sigue siendo evidente.</li>
          <li><strong>No documentar formalmente el nombramiento:</strong> una decisión verbal o un email interno no son suficientes. El nombramiento debe constar en acta del órgano competente.</li>
          <li><strong>Designar un RSII sin independencia real:</strong> nombrar como RSII al director financiero que depende directamente del CEO que podría ser investigado es un conflicto de interés evidente que la AIPI puede cuestionar.</li>
          <li><strong>No comunicar en plazo:</strong> el plazo del 10 de abril de 2026 no es orientativo. No comunicar implica incumplimiento adicional a la falta de canal.</li>
          <li><strong>No actualizar la notificación:</strong> si el RSII cesa o cambia, hay que comunicarlo a la AIPI en 10 días hábiles. Muchas empresas olvidan este trámite.</li>
          <li><strong>Confundir el DPO con el RSII:</strong> el Delegado de Protección de Datos y el RSII son figuras diferentes con funciones distintas. Pueden ser la misma persona, pero deben ejercer ambos roles de forma diferenciada.</li>
        </ul>

