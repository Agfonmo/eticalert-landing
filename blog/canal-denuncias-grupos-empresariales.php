        <h2 id="que-dice-la-ley">Qué dice exactamente la Ley 2/2023 sobre grupos de empresas</h2>

        <p>La Ley 2/2023, de 20 de febrero, transpone la Directiva Whistleblowing (2019/1937). La directiva europea incluyó una previsión específica para grupos: el artículo 8.6 de la Directiva establece que "las personas jurídicas del sector privado que tengan entre 50 y 249 trabajadores podrán compartir recursos para la recepción de denuncias y para cualquier investigación que deba realizarse".</p>

        <p>La Ley 2/2023 española recoge este principio en su artículo 5.2, que permite que varias personas jurídicas de un mismo grupo compartan un único sistema de información siempre que se cumplan una serie de condiciones que analizamos a continuación.</p>

        <div style="background:rgba(59,130,246,0.08);border:1px solid rgba(59,130,246,0.2);border-radius:12px;padding:1.5rem;margin:2rem 0;">
          <p style="margin:0 0 0.5rem;font-weight:600;color:#93c5fd;">⚖️ Artículo 5.2 Ley 2/2023 (extracto)</p>
          <p style="margin:0;color:var(--text-secondary);font-style:italic;">"Las personas jurídicas del sector privado podrán compartir el sistema interno de información siempre que se mantengan, en todo caso, las garantías establecidas en esta ley respecto de la confidencialidad, el secreto de las comunicaciones y la protección de datos."</p>
        </div>

        <p>Lo fundamental aquí: <strong>compartir el canal no exime a cada entidad de sus obligaciones individuales de cumplimiento</strong>. La filial sigue siendo responsable ante la AIPI aunque el canal lo gestione la matriz.</p>

        <h2 id="cuando-compartir">Cuándo se puede compartir un canal para todo el grupo</h2>

        <p>El canal compartido es legalmente válido cuando se cumplen estas condiciones simultáneamente:</p>

