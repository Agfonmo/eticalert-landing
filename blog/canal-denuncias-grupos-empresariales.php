        <details style="border:1px solid var(--border);border-radius:8px;padding:1rem;margin:1rem 0;">
          <summary style="font-weight:600;cursor:pointer;">¿Una filial con menos de 50 empleados necesita canal propio si la matriz tiene más de 50?</summary>
          <p style="margin:0.75rem 0 0;color:var(--text-secondary);">Depende. Si la filial es una entidad jurídica independiente con menos de 50 empleados propios, en principio no está obligada individualmente (salvo que opere en sectores regulados). Puede acogerse al canal de la matriz. Pero si la AIPI detecta que en la práctica la filial funciona con autonomía operativa, puede exigir un canal específico.</p>
        </details>

        <details style="border:1px solid var(--border);border-radius:8px;padding:1rem;margin:1rem 0;">
          <summary style="font-weight:600;cursor:pointer;">¿Quién debe ser el RSII en un grupo empresarial?</summary>
          <p style="margin:0.75rem 0 0;color:var(--text-secondary);">Cada entidad obligada debe designar formalmente a su RSII y notificarlo a la AIPI. En un grupo, la solución habitual es designar como RSII de cada filial a la misma persona (o función) de la matriz: por ejemplo, el Director de Compliance del grupo. Debe quedar documentado en la política de cada entidad y notificado individualmente en el formulario de la AIPI.</p>
        </details>

        <details style="border:1px solid var(--border);border-radius:8px;padding:1rem;margin:1rem 0;">
          <summary style="font-weight:600;cursor:pointer;">¿Pueden empresas no vinculadas compartir canal de denuncias?</summary>
          <p style="margin:0.75rem 0 0;color:var(--text-secondary);">No. La posibilidad de compartir canal está reservada a entidades que forman parte del mismo grupo, con vínculos de propiedad o control. Dos empresas independientes del mismo sector no pueden compartir un canal aunque tengan los mismos accionistas a título individual. Cada una necesita su propio sistema.</p>
        </details>

        <details style="border:1px solid var(--border);border-radius:8px;padding:1rem;margin:1rem 0;">
          <summary style="font-weight:600;cursor:pointer;">¿Qué pasa con las filiales extranjeras de un grupo español?</summary>
          <p style="margin:0.75rem 0 0;color:var(--text-secondary);">Las filiales extranjeras están sujetas a la normativa de cada país. Una filial en Francia aplica la loi Sapin II; una en Alemania, el HinSchG. El grupo puede usar una plataforma tecnológica unificada, pero la configuración legal (plazos, categorías, idioma, RSII local) debe adaptarse a cada jurisdicción. La gestión centralizada no exime del cumplimiento local.</p>
