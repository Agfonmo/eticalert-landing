        <h2 id="cuando-canal-propio">Cuándo cada filial necesita obligatoriamente su propio canal</h2>

        <p>Aunque el grupo pueda compartir tecnología, hay situaciones en las que la filial no puede acogerse simplemente al canal de la matriz:</p>

        <h3>1. Filiales con más de 249 empleados</h3>
        <p>Si una filial tiene más de 249 trabajadores, está en el umbral de grandes empresas y necesita un sistema que pueda procesar sus denuncias de forma completamente autónoma. En la práctica esto suele resolverse con una instancia separada dentro de la misma plataforma, pero con gestión independiente.</p>

        <h3>2. Filiales en sectores regulados</h3>
        <p>Las filiales que operen en sectores con legislación sectorial propia (entidades financieras, auditores, notarías, empresas de criptoactivos, etc.) deben cumplir con los requisitos específicos de su sector, que pueden ir más allá de la Ley 2/2023. Una filial bancaria del grupo no puede asumir que el canal genérico del grupo cumple con la normativa del Banco de España.</p>

        <h3>3. Cuando la matriz está fuera de España</h3>
        <p>Si la matriz es una empresa extranjera y el canal está configurado conforme a la normativa de otro país, la filial española necesita asegurarse de que el sistema cumple con la Ley 2/2023 española. No hay equivalencia automática entre normativas nacionales de transposición.</p>

        <h3>4. Conflicto de interés estructural</h3>
        <p>Si existe riesgo razonable de que la matriz pueda ser objeto de una denuncia por parte de empleados de la filial (situación frecuente en grupos con subordinación operativa intensa), el canal compartido con gestión matricial puede comprometer la independencia del proceso. En estos casos es recomendable externalizar la gestión a un tercero independiente.</p>

        <h2 id="rsii-grupo" style="margin-top:2.5rem;">Cómo designar al RSII en un grupo empresarial</h2>

        <p>Este punto genera mucha confusión. La solución correcta es:</p>

        <ol style="color:var(--text-secondary);line-height:1.8;">
          <li><strong>Identificar qué entidades del grupo están obligadas</strong> (50+ empleados o sector regulado). Las filiales no obligadas no necesitan RSII propio, aunque puedan adherirse al canal del grupo voluntariamente.</li>
          <li><strong>Designar formalmente al RSII en cada entidad obligada</strong>. Puede ser la misma persona (ej. Director de Compliance del grupo) o una función del grupo (Compliance Group). El nombramiento debe quedar en acta del órgano de gobierno de cada filial.</li>
          <li><strong>Notificar a la AIPI individualmente por cada entidad</strong>. No existe un formulario de grupo: cada sociedad hace su notificación separada en el <a href="/blog/rsii-guia-formulario-aipi" style="color:var(--accent);">sistema de la AIPI</a>.</li>
          <li><strong>Incluir al RSII en la política de cada entidad</strong>. El documento no puede ser el mismo para todo el grupo si las entidades son jurídicamente distintas.</li>
        </ol>

        <div style="background:rgba(234,179,8,0.08);border:1px solid rgba(234,179,8,0.3);border-radius:12px;padding:1.5rem;margin:2rem 0;">
          <p style="margin:0 0 0.5rem;font-weight:600;color:#fbbf24;">⚠️ Error habitual</p>
          <p style="margin:0;color:var(--text-secondary);">Notificar solo la RSII de la matriz y asumir que cubre a todas las filiales. La AIPI identifica a las entidades obligadas por CIF, no por grupo empresarial. Si inspeccionan a una filial y no tiene RSII notificado a su nombre, hay incumplimiento formal independientemente de lo que tenga la matriz.</p>
        </div>

