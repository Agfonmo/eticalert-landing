        <h2 id="filiales-extranjeras">El caso especial de las filiales extranjeras</h2>

        <p>Los grupos españoles con presencia internacional o los grupos extranjeros con filiales en España se enfrentan a una complejidad adicional: <strong>cada país ha transpuesto la Directiva Whistleblowing de forma diferente</strong>.</p>

        <p>Algunos datos relevantes:</p>

        <ul style="color:var(--text-secondary);line-height:1.8;">
          <li><strong>Francia</strong> (loi Sapin II + décret de transposición): exige que el canal pueda recibir denuncias en francés y que el responsable pueda responder en ese idioma.</li>
          <li><strong>Alemania</strong> (Hinweisgeberschutzgesetz - HinSchG): permite el canal compartido para grupos, pero el componente de investigación debe ser independiente.</li>
          <li><strong>Italia</strong> (D.Lgs. 24/2023): muy similar a España en requisitos. Permite canal de grupo.</li>
          <li><strong>Portugal</strong>: mismo plazo de acuse de recibo que España (7 días) pero diferente denominación del responsable.</li>
        </ul>

        <p>La solución práctica para grupos multinacionales es <strong>una plataforma unificada con configuración jurisdiccional por entidad</strong>: misma tecnología, pero adaptada en idioma, plazos, categorías de denuncia y denominación del responsable según cada país.</p>

        <div style="background:rgba(74,222,128,0.08);border:1px solid var(--accent-border);border-radius:12px;padding:1.5rem;margin:2rem 0;">
          <p style="margin:0 0 0.5rem;font-weight:600;">✅ Regla práctica para grupos internacionales</p>
          <p style="margin:0;color:var(--text-secondary);">Para cada filial en un país nuevo, pregúntate: ¿qué normativa local aplica?, ¿cuál es el plazo de acuse de recibo?, ¿cómo se llama el responsable?, ¿hay autoridad supervisora designada? Adapta la configuración del canal antes del lanzamiento en ese país, no después.</p>
        </div>

        <h2 id="errores">Errores frecuentes en la configuración de grupos</h2>

        <p>Basándonos en la experiencia de implantación en grupos empresariales, estos son los fallos más habituales:</p>

        <ol style="color:var(--text-secondary);line-height:2;">
          <li><strong>Un único RSII notificado solo para la matriz.</strong> Error crítico: la AIPI inspecciona entidad por entidad. Solución: notificación individual por CIF.</li>
          <li><strong>Una política de grupo genérica sin adaptación por entidad.</strong> La política debe identificar a qué persona jurídica corresponde, cuál es su RSII y cuál es su canal específico.</li>
          <li><strong>Acceso del Compliance Group a todas las denuncias.</strong> La supervisión de grupo debe ser a nivel de métricas, no de casos individuales, salvo escalado formal.</li>
          <li><strong>Asumir que las filiales no obligadas no necesitan nada.</strong> Aunque no estén obligadas individualmente, si se adhieren al canal del grupo, deben quedar reflejadas en la política y sus empleados deben saber que el canal existe y cómo acceder.</li>
          <li><strong>Libro-registro unificado de grupo.</strong> El artículo 26 Ley 2/2023 exige el libro-registro por entidad obligada. Un solo registro para todo el grupo no es válido ante una inspección.</li>
          <li><strong>No actualizar las notificaciones cuando cambia el RSII.</strong> En grupos donde la rotación de directivos es alta, el cambio de RSII debe notificarse a la AIPI también en las filiales, no solo en la matriz.</li>
        </ol>

        <h2 style="margin-top:2.5rem;">Conclusión: canal de grupo sí, pero con estructura jurídica correcta</h2>

        <p>Un grupo empresarial puede y debería centralizar la tecnología y la gestión operativa del canal de denuncias. Es más eficiente, más barato y permite un mejor análisis de riesgos a nivel corporativo. Pero esta centralización técnica no puede ignorar la estructura jurídica: <strong>cada entidad obligada necesita su RSII notificado, su política documentada y su libro-registro</strong>.</p>

        <p>La clave está en distinguir entre <em>plataforma compartida</em> (perfectamente válida) y <em>cumplimiento compartido</em> (que no existe: cada entidad responde individualmente ante la AIPI).</p>

