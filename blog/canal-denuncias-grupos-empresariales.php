                <td style="padding:0.75rem;border:1px solid var(--border);font-weight:500;">Vínculo de grupo</td>
                <td style="padding:0.75rem;border:1px solid var(--border);">Las entidades deben pertenecer al mismo grupo (relación de control o participación significativa). No es válido para empresas meramente asociadas o del mismo sector.</td>
              </tr>
              <tr style="background:var(--bg-secondary);">
                <td style="padding:0.75rem;border:1px solid var(--border);font-weight:500;">Confidencialidad garantizada</td>
