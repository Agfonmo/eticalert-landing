        <h2 id="que-incluye-sesame">¿Qué incluye el canal de denuncias de Sesame HR?</h2>
        <p>Sesame HR integra el canal de denuncias dentro de su suite de RRHH. Según la información pública disponible, el módulo incluye:</p>
        <ul>
          <li>Canal de denuncias anónimo con acceso desde cualquier dispositivo.</li>
          <li>Comunicación bidireccional cifrada entre el informante y los gestores.</li>
          <li>Registro trazable de casos con gestión de roles.</li>
          <li>Integración con firma electrónica, gestión documental, nómina y otros módulos de la suite.</li>
          <li>Datos almacenados en la UE con certificaciones ISO.</li>
        </ul>
        <p>Para una empresa que ya usa Sesame para gestión de personal, la ventaja obvia es la integración: no hay que añadir otra herramienta, los datos del equipo ya están ahí y los gestores trabajan en el entorno que conocen.</p>

