        <h2 id="riesgo-integracion">El riesgo oculto de integrar el canal con RRHH</h2>
        <p>La integración del canal de denuncias con la suite de RRHH tiene un riesgo legal que conviene conocer. La <a href="/blog/ley-2-2023-canal-de-denuncias">Ley 2/2023</a> exige que el canal sea gestionado con <strong>independencia funcional</strong> por el RSII y que el acceso esté estrictamente limitado al personal autorizado.</p>
        <p>Cuando el canal está integrado en la misma plataforma que gestiona nóminas, vacaciones y control horario, el aislamiento de acceso es más difícil de garantizar y de demostrar ante la AIPI. ¿Quién más tiene acceso a la plataforma de RRHH? ¿Los administradores de Sesame HR de tu empresa pueden ver los expedientes? Estas son preguntas que conviene hacerse antes de usar el canal integrado para casos sensibles.</p>
        <p>EticAlert es un sistema independiente y aislado por diseño. El canal de denuncias no comparte infraestructura ni accesos con ninguna otra herramienta de la empresa.</p>

        <h2 id="cuando-cada-uno">¿Cuándo usar el canal de Sesame HR y cuándo EticAlert?</h2>

        <p><strong>El módulo de Sesame HR puede ser suficiente si:</strong></p>
        <ul>
          <li>Ya pagas Sesame HR y el canal de denuncias te sale incluido sin coste adicional.</li>
          <li>Tu empresa tiene un departamento de RRHH activo que ya trabaja en Sesame y quiere centralizar herramientas.</li>
          <li>Los casos que recibes son de baja complejidad y no involucran a directivos o responsables de RRHH (que podrían tener acceso a la plataforma).</li>
        </ul>

        <p><strong>Usa EticAlert en cambio si:</strong></p>
        <ul>
          <li>Solo necesitas el canal de denuncias y no quieres pagar la suite de RRHH completa.</li>
          <li>Necesitas garantizar el aislamiento del canal respecto al resto de sistemas de la empresa.</li>
          <li>La sensibilidad de los casos (denuncias contra directivos, acoso por parte de responsables de RRHH) requiere el máximo control de acceso.</li>
          <li>Quieres el cumplimiento legal integrado en el producto: alertas de plazos, historial del RSII, bloqueo automático si falta documentación.</li>
        </ul>

