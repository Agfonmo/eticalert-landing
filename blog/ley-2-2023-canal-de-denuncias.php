        <h1>Ley 2/2023: todo lo que tu empresa necesita saber sobre el canal de denuncias</h1>
        <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 0.5rem;">Guía actualizada a 2026 · 8 minutos de lectura</p>
        <p style="font-size:0.875rem; color:var(--text-muted); margin-bottom:2.5rem;">Publicado el 17 de marzo de 2026 por el equipo de EticAlert</p>

        <p>Si tu empresa tiene 50 o más empleados en España, tienes una obligación legal que no puedes ignorar: disponer de un canal de denuncias operativo conforme a la Ley 2/2023. En esta guía explicamos qué es esta ley, a quién obliga, qué exige exactamente y qué consecuencias tiene no cumplir.</p>

        <h2 id="que-es-ley">¿Qué es la Ley 2/2023?</h2>
        <p>La <strong>Ley 2/2023, de 20 de febrero</strong>, es la ley española que transpone la Directiva (UE) 2019/1937 del Parlamento Europeo —conocida como "Directiva Whistleblowing"— al ordenamiento jurídico nacional.</p>
        <p>Su nombre oficial es <em>Ley reguladora de la protección de las personas que informen sobre infracciones normativas y de lucha contra la corrupción</em>. Fue publicada en el BOE el 21 de febrero de 2023 y tiene dos objetivos principales:</p>
        <ul>
          <li>Proteger a las personas que comunican infracciones normativas o hechos corruptos de posibles represalias por parte de la organización afectada.</li>
          <li>Establecer sistemas internos seguros, confidenciales y accesibles para canalizar esas informaciones.</li>
        </ul>
        <p>La ley es de cumplimiento obligatorio. No es una recomendación ni un estándar voluntario. Las empresas que no la cumplen están expuestas a sanciones económicas directas.</p>

        <div class="callout">
          <p><strong>Contexto europeo:</strong> España fue uno de los últimos países de la UE en transponer la Directiva Whistleblowing. La Directiva fijaba el plazo en diciembre de 2021; España aprobó la ley en febrero de 2023, con más de un año de retraso.</p>
        </div>

        <h2 id="a-quien-obliga">¿A quién obliga la Ley 2/2023?</h2>
        <p>La obligación de disponer de un Sistema Interno de Información recae sobre las siguientes entidades:</p>
        <ul>
          <li><strong>Empresas privadas con 50 o más trabajadores.</strong> El umbral se calcula como la media de trabajadores en los últimos 12 meses, no el número a una fecha concreta.</li>
          <li><strong>Todas las entidades del sector público</strong>, con independencia de su tamaño: Administración General del Estado, comunidades autónomas, entidades locales, organismos públicos, universidades, etc.</li>
          <li><strong>Partidos políticos, sindicatos y organizaciones empresariales</strong> que reciban o gestionen fondos públicos.</li>
          <li><strong>Fundaciones vinculadas a partidos políticos</strong> o que reciban financiación pública.</li>
          <li><strong>Municipios con más de 10.000 habitantes</strong> (los de menor tamaño pueden compartir canal con la diputación o mancomunidad correspondiente).</li>
          <li><strong>Personas jurídicas con facturación superior a 10 millones de euros</strong> en el ejercicio anterior, aunque no alcancen el umbral de 50 empleados.</li>
        </ul>
        <p>Las empresas de entre 50 y 249 empleados que pertenezcan a un grupo de empresas pueden compartir un único canal de denuncias. Sin embargo, cada empresa sigue siendo individualmente responsable del cumplimiento y del nombramiento de su RSII.</p>

        <h2 id="requisitos-canal">¿Qué requisitos debe cumplir el canal?</h2>
        <p>La Ley 2/2023 no solo obliga a tener un canal: exige que ese canal tenga unas características mínimas. Estos son los requisitos técnicos y procedimentales:</p>
        <ul>
          <li><strong>Confidencialidad garantizada</strong> mediante sistemas de cifrado y acceso restringido solo al personal autorizado que gestione el sistema.</li>
          <li><strong>Accesibilidad 24/7</strong> desde cualquier dispositivo, con interfaz clara e intuitiva para todo tipo de usuarios.</li>
          <li><strong>Posibilidad de denuncias anónimas</strong>: el sistema debe permitir comunicaciones sin revelar la identidad del informante, garantizando que este pueda hacer seguimiento sin identificarse.</li>
          <li><strong>Denuncias escritas y orales</strong>: el canal debe admitir tanto formularios escritos como comunicaciones verbales (teléfono o presencial) y, en caso de denuncia oral, debe quedar constancia documental de la misma.</li>
          <li><strong>Acuse de recibo automático</strong> en un máximo de 7 días naturales desde la recepción de la comunicación.</li>
          <li><strong>Gestión independiente e imparcial</strong> por un Responsable del Sistema Interno de Información (RSII) con autonomía funcional y libre de conflicto de interés en los asuntos que gestione.</li>
          <li><strong>Cumplimiento del RGPD</strong>: minimización de datos, plazos de conservación, derechos de los interesados y protección frente a accesos no autorizados.</li>
          <li><strong>Registro y trazabilidad</strong> de todas las comunicaciones y actuaciones, con constancia del estado de cada denuncia y las decisiones adoptadas.</li>
          <li><strong>Información al denunciante</strong> en un plazo máximo de 3 meses sobre las medidas adoptadas o en curso.</li>
          <li><strong>Acceso para todos los colectivos</strong> vinculados a la organización: empleados actuales, exempleados, candidatos, proveedores, contratistas, clientes, socios y cualquier tercero relacionado.</li>
        </ul>

        <div class="callout">
          <p><strong>Atención:</strong> el canal debe ser real y operativo, no un formulario de papel ni un email genérico. La AIPI verifica que el sistema funciona, que tiene procedimiento documentado y que el RSII está formalmente designado.</p>
        </div>

        <h2 id="plazos">Plazos de implementación</h2>
        <p>La Ley 2/2023 estableció diferentes plazos de obligatoriedad según el tamaño de la entidad:</p>
        <ul>
          <li><strong>13 de junio de 2023:</strong> obligatorio para empresas con 250 o más trabajadores y para todas las entidades del sector público.</li>
          <li><strong>1 de diciembre de 2023:</strong> obligatorio para empresas de entre 50 y 249 empleados, municipios de menos de 10.000 habitantes y demás entidades obligadas.</li>
          <li><strong>Febrero de 2026:</strong> la AIPI activa su sede electrónica y el formulario oficial para notificar la designación del RSII. Inicio del enforcement real por parte de la autoridad.</li>
          <li><strong>10 de abril de 2026:</strong> fecha límite del plazo de 2 meses habilitado por la AIPI para comunicar la designación del RSII.</li>
        </ul>
        <p>Si tu empresa tenía obligación desde 2023 y aún no ha implementado el canal, está en incumplimiento desde entonces. Cada mes que pasa sin actuar aumenta el riesgo de sanción.</p>

        <h2 id="sanciones">Régimen de sanciones</h2>
        <p>El incumplimiento de la Ley 2/2023 puede acarrear sanciones administrativas significativas. El régimen se estructura en tres niveles:</p>
        <ul>
          <li><strong>Infracciones muy graves</strong> (ej. no disponer de canal, represalias contra el informante): hasta <strong>1.000.000€</strong> para personas jurídicas y hasta <strong>300.000€</strong> para personas físicas.</li>
          <li><strong>Infracciones graves</strong> (ej. canal que no cumple los requisitos técnicos): hasta <strong>600.000€</strong> para personas jurídicas y hasta <strong>200.000€</strong> para personas físicas.</li>
          <li><strong>Infracciones leves</strong> (ej. incumplimientos procedimentales menores): hasta <strong>100.000€</strong> para personas jurídicas y hasta <strong>25.000€</strong> para personas físicas.</li>
        </ul>
        <p>Un aspecto especialmente relevante es la <strong>responsabilidad personal</strong>. Los administradores y directivos pueden ser sancionados individualmente si se demuestra que el incumplimiento deriva de una falta de diligencia o supervisión en el ejercicio de sus funciones.</p>
