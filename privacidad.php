        <ul>
          <li><strong>Acceso (art. 15 RGPD):</strong> obtener confirmación de si se están tratando sus datos y, en tal caso, acceder a ellos.</li>
          <li><strong>Rectificación (art. 16 RGPD):</strong> solicitar la corrección de datos inexactos o la completación de datos incompletos.</li>
          <li><strong>Supresión o "derecho al olvido" (art. 17 RGPD):</strong> solicitar la eliminación de sus datos cuando ya no sean necesarios para los fines para los que fueron recabados, cuando retire su consentimiento, o cuando el tratamiento no sea conforme al RGPD, entre otros supuestos.</li>
          <li><strong>Limitación del tratamiento (art. 18 RGPD):</strong> solicitar que suspendamos el uso de sus datos en determinadas circunstancias (mientras se verifica la exactitud de los datos, mientras se resuelve una objeción, etc.).</li>
          <li><strong>Portabilidad (art. 20 RGPD):</strong> recibir sus datos en un formato estructurado, de uso común y lectura mecánica, y transmitirlos a otro responsable, cuando el tratamiento se base en el consentimiento o en la ejecución de un contrato y se realice por medios automatizados.</li>
          <li><strong>Oposición (art. 21 RGPD):</strong> oponerse al tratamiento de sus datos cuando este se base en el interés legítimo o en el cumplimiento de una misión de interés público, incluida la elaboración de perfiles.</li>
          <li><strong>No ser objeto de decisiones automatizadas (art. 22 RGPD):</strong> no ser objeto de una decisión basada únicamente en el tratamiento automatizado, incluida la elaboración de perfiles, que produzca efectos jurídicos o le afecte significativamente.</li>
          <li><strong>Revocación del consentimiento:</strong> retirar en cualquier momento el consentimiento prestado para tratamientos basados en él, sin que ello afecte a la licitud del tratamiento anterior a la retirada.</li>
