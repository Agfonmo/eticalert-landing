          <span class="overline">Guía completa 2026</span>
          <h1>Canal de denuncias para empresas: guía completa 2026</h1>
          <p style="font-size:1.125rem;color:var(--text-secondary);margin:1rem 0 2rem;">Todo lo que necesitas saber para cumplir la Ley 2/2023: qué es un canal de denuncias, quién está obligado, qué requisitos debe cumplir y cómo implementarlo.</p>

          <!-- ================================================== -->
          <h2 id="que-es">1. ¿Qué es un canal de denuncias?</h2>
          <p>Un canal de denuncias —también llamado Sistema Interno de Información (SII)— es un mecanismo que permite a los empleados, proveedores, clientes y terceros relacionados con una organización comunicar de forma segura y confidencial posibles infracciones normativas, actos corruptos o conductas contrarias a la ética empresarial.</p>
          <p>El denunciante puede hacerlo de forma anónima o identificada, y el sistema garantiza que su información sea tratada con confidencialidad, sin que pueda sufrir represalias por haberla comunicado.</p>
          <p>Desde la aprobación de la Ley 2/2023 en España, disponer de un canal de denuncias no es solo una buena práctica de governance: es una obligación legal para todas las empresas con 50 o más empleados.</p>

          <div class="callout">
            <p><strong>Importante:</strong> Un canal de denuncias no es un buzón de sugerencias ni un sistema de quejas de RRHH. Es un instrumento legal con requisitos técnicos y procedimentales específicos regulados por ley.</p>
          </div>

          <!-- ================================================== -->
          <h2 id="marco-legal">2. Marco legal: Ley 2/2023 y Directiva UE</h2>
          <p>La <strong>Ley 2/2023, de 20 de febrero</strong>, reguladora de la protección de las personas que informen sobre infracciones normativas y de lucha contra la corrupción, es la transposición española de la Directiva (UE) 2019/1937 del Parlamento Europeo y del Consejo (la llamada "Directiva Whistleblowing").</p>
          <p>Fue publicada en el BOE el 21 de febrero de 2023 y entró en vigor al día siguiente. Su objetivo principal es doble:</p>
          <ul>
            <li>Proteger a las personas que informan sobre infracciones del Derecho de la Unión o de la normativa española de posibles represalias.</li>
            <li>Establecer canales seguros y confidenciales para que esas informaciones lleguen a quien debe actuar sobre ellas.</li>
          </ul>
          <p>La ley aplica tanto al sector público como al privado, y establece un régimen sancionador explícito para las organizaciones que no cumplan con sus obligaciones.</p>

          <!-- ================================================== -->
          <h2 id="quien-obligado">3. ¿Quién está obligado a tener canal de denuncias?</h2>
          <p>La obligación de disponer de un Sistema Interno de Información recae sobre:</p>
          <ul>
            <li><strong>Empresas del sector privado</strong> con 50 o más trabajadores (umbral calculado como media de los últimos 12 meses).</li>
            <li><strong>Todas las entidades del sector público</strong>, con independencia de su tamaño.</li>
            <li><strong>Partidos políticos, sindicatos y organizaciones empresariales</strong> que reciban o gestionen fondos públicos.</li>
            <li><strong>Fundaciones</strong> vinculadas a partidos políticos o que reciban financiación pública.</li>
            <li><strong>Municipios con más de 10.000 habitantes</strong>, que deben disponer de su propio canal (los de menor tamaño pueden adscribirse al de la diputación o cabildo).</li>
          </ul>

          <div class="callout">
            <p style="margin-bottom:0.75rem;"><strong>¿Y si tienes menos de 50 empleados?</strong> Muchas empresas están igualmente obligadas por su sector, sin umbral de plantilla:</p>
            <ul style="margin:0 0 0.875rem 1.25rem;padding:0;display:flex;flex-direction:column;gap:0.375rem;">
              <li>🏦 <strong>PBC · Ley 10/2010</strong> — Asesorías, notarías, inmobiliarias, casinos, cripto…</li>
              <li>💳 <strong>Finanzas reguladas UE</strong> — Gestoras, fintech, corredurías, entidades de pago…</li>
              <li>✈️ <strong>Transporte regulado</strong> — Aerolíneas AOC, navieras SOLAS, ferroviarias…</li>
              <li>🌿 <strong>Medioambiente industrial</strong> — SEVESO, AAI, EU ETS, gestores de residuos…</li>
            </ul>
            <p style="margin:0;font-size:0.875rem;"><a href="/blog/obligados-menos-50-empleados" style="color:var(--accent);">Ver guía completa de empresas obligadas sin umbral de empleados →</a></p>
          </div>

          <div class="callout">
            <p><strong>Grupos empresariales:</strong> Las empresas de entre 50 y 249 empleados que pertenezcan a un grupo pueden compartir un canal común. Sin embargo, cada empresa sigue siendo individualmente responsable del cumplimiento.</p>
          </div>

          <!-- ================================================== -->
          <h2 id="requisitos">4. Requisitos legales del canal de denuncias</h2>
          <p>La Ley 2/2023 no solo obliga a tener un canal: establece qué características debe tener ese canal para ser considerado conforme a la normativa. Los principales requisitos son:</p>
          <ul>
            <li><strong>Confidencialidad garantizada</strong> mediante sistemas de cifrado y acceso restringido a personal autorizado.</li>
            <li><strong>Accesibilidad 24/7</strong> con interfaz clara y uso intuitivo para cualquier tipo de usuario.</li>
            <li><strong>Posibilidad de denuncias anónimas</strong>: el sistema debe permitir comunicaciones sin revelar la identidad del informante.</li>
            <li><strong>Denuncias escritas y orales</strong>: el canal debe admitir tanto formularios escritos como comunicaciones verbales (por teléfono o presencialmente).</li>
            <li><strong>Acuse de recibo automático</strong> en un máximo de 7 días naturales desde la recepción.</li>
            <li><strong>Gestión independiente e imparcial</strong> por un Responsable del Sistema Interno de Información (RSII) con autonomía funcional.</li>
            <li><strong>Cumplimiento del RGPD</strong> en la protección, conservación y eliminación de los datos personales.</li>
            <li><strong>Registro y trazabilidad</strong> de todas las comunicaciones y actuaciones realizadas en el sistema.</li>
            <li><strong>Información al denunciante</strong> sobre las medidas adoptadas en un plazo máximo de 3 meses desde el acuse de recibo.</li>
            <li><strong>Acceso para todos los colectivos</strong>: empleados, exempleados, proveedores, clientes, contratistas y cualquier persona relacionada con la organización.</li>
          </ul>

          <!-- ================================================== -->
          <h2 id="rsii">5. El RSII: responsable del sistema</h2>
          <p>El <strong>Responsable del Sistema Interno de Información (RSII)</strong> es la persona o el órgano colegiado designado formalmente para gestionar el canal de denuncias. Es la figura central del cumplimiento de la Ley 2/2023 en tu empresa.</p>
          <p>Sus funciones principales son: recibir y gestionar las comunicaciones, garantizar la confidencialidad del informante, investigar los hechos reportados con independencia, y comunicar las conclusiones y medidas adoptadas.</p>
          <p>La AIPI recomienda que el RSII sea un directivo de la entidad con independencia funcional del órgano de administración o gobierno (Recomendación 1/2026). Puede ser una persona física o un órgano colegiado.</p>
          <p>Desde febrero de 2026, la designación del RSII debe comunicarse a la AIPI a través de su sede electrónica. El plazo inicial fue hasta el 10 de abril de 2026. Los cambios posteriores deben notificarse en 10 días hábiles.</p>
          <p>Consulta nuestra <a href="/blog/rsii-guia-formulario-aipi">guía completa sobre el RSII y el formulario de la AIPI →</a></p>

          <!-- ================================================== -->
          <h2 id="aipi">6. La AIPI y el enforcement</h2>
          <p>La <strong>Autoridad Independiente de Protección del Informante (AIPI)</strong> es el organismo creado por la Ley 2/2023 para supervisar el cumplimiento de la normativa en el sector privado. Es independiente del Gobierno y actúa con plena autonomía.</p>
          <p>Desde <strong>febrero de 2026</strong>, la AIPI activó su sede electrónica y está plenamente operativa. Puede:</p>
          <ul>
            <li>Requerir documentación sobre el canal y su funcionamiento.</li>
            <li>Inspeccionar el sistema interno de información de cualquier empresa obligada.</li>
            <li>Verificar que el canal es real, operativo y documentado (no basta con tenerlo "en papel").</li>
            <li>Comprobar que el RSII ha sido designado y notificado correctamente.</li>
            <li>Imponer sanciones administrativas en caso de incumplimiento.</li>
          </ul>
          <p>Consulta más detalles en nuestro artículo <a href="/blog/aipi-sanciones-canal-denuncias">La AIPI ya sanciona: qué significa para tu empresa →</a></p>

          <!-- ================================================== -->
          <h2 id="sanciones">7. Sanciones por incumplimiento</h2>
          <p>El régimen sancionador de la Ley 2/2023 establece tres niveles de infracciones:</p>
          <ul>
            <li><strong>Infracciones muy graves</strong>: hasta <strong>1.000.000€</strong> para personas jurídicas y hasta <strong>300.000€</strong> para personas físicas (administradores, directivos).</li>
            <li><strong>Infracciones graves</strong>: hasta <strong>600.000€</strong> para personas jurídicas y hasta <strong>200.000€</strong> para personas físicas.</li>
            <li><strong>Infracciones leves</strong>: hasta <strong>100.000€</strong> para personas jurídicas y hasta <strong>25.000€</strong> para personas físicas.</li>
          </ul>
          <p>Se considera infracción muy grave no disponer de un canal de denuncias cuando existe obligación legal, impedir o dificultar las comunicaciones, o adoptar represalias contra el informante.</p>
          <p>Además de la sanción económica, los administradores y directivos pueden incurrir en responsabilidad personal por incumplimiento de sus deberes de diligencia y supervisión.</p>
          <p>¿Estás en riesgo? Consulta el <a href="/sanciones-canal-de-denuncias" style="color:var(--accent);">checklist de sanciones →</a> o el <a href="/blog/sanciones-canal-denuncias-desglose">desglose completo por tipo de infracción →</a></p>

